|INFERNO CANTO 34~

|Chiose posteriori~

       Jam que domos stigias et tristia regna silentum
     Distituens sublimis agor jam noctis abymo
     Carcere felices rediens extollor ad auras
     Vidi ego diversis animarum tartara penis
     In curulam distinta novem lacrymosa que passim
     Flumina et horribilem ferratis postibus urbem
     Sit tibi summe deus nostre spes una salutis
     Gloria et eterne maneant per secula laudes.
