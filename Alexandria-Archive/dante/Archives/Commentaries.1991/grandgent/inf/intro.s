tainted with one vice which destroys his soul.  It is only
through Dante that we know of his sin, but there can be no doubt
of its reality; in his day assuredly many were aware of it.  Thus
the doctrine is again enforced that a single deadly fault,
unexpiated, will damn a man otherwise noble.  The contrast
between his general dignity and his fatal weakness is emphasized
at the end of the canto, when the elderly sage is suddenly forced
to put aside his gravity and run like a racer to rejoin his
fellows.

