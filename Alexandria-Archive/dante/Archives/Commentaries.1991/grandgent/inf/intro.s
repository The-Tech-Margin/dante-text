-- the abode of the violent -- and the description of the first
of the three concentric rings that compose it.  This first
^girone~ consists of a river of hot blood, a picture of
sanguinary relations to one's fellowmen.  The ^Visio Alberici~
also tells of homicides in a lake of boiling blood; and the
^Visio Sancti Pauli~ shows different kinds of sinners immersed to
varying depths in a fiery stream.  In the ^Inferno~, too the
degree of immersion varies between eyebrows and feet, according
to the wickedness of the offence.

	Along the narrow bank run centaurs, whose business it is
to keep the other souls in their proper place.  These half-human
guardians are not depicted as hateful or repulsive: they do not
seem to be demons, although their function is similar to that of
the devils beside the ditch of barrators in the eighth circle;
they appear to be rather the spirits of real centaurs, creatures
whose semi-equine character made their excesses more natural and
consequently less blameworthy.  They may be intended also to
serve as illustrations of Aristotle's doctrine of bestiality.

