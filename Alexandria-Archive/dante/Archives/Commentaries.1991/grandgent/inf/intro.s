	8.  The idea of a practical compendium of human knowledge
was not unknown to the old Romans: Pliny, for instance, composed
the ^Historia Naturalis~, Celsus the ^De Artibus.~, To the
borderland of ancient and medi@@aeval times belongs that famous
compilation, the ^Origines~ or ^Etymologi@@ae~ of St. Isidore of
