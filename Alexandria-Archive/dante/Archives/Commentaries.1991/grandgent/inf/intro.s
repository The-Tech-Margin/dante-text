	13.  `Sciendum est,' declares the letter to Can Grande,
referring to the ^Divina Commedia~,' quod istius operis non est
simplex sensus, immo dici potest polisemos, hoc est plurium
sensuum; nam alius sensus est qui habetur per literam, alius est
qui habetur per significata per litteram.  Et primus dicitur
litteralis, secundus vero allegoricus, sive mystticus.'
Allegorical interpretation had been applied by the ancient
Hebrews to the prophecies of the Old Testament, and by the Greeks
-- in late, sophisticated times -- to the Homeric poems.  It was
early applied also to some of the Latin poets.  The ^@@AEneid~
was regularly so expounded; a commentary of this kind was surely
known to Dante.  The method reached its highest development,
however, in the explanation of the Bible by the Church fathers.
In the second book of the ^Convivio~ Dante discusses it at
length; and there he differentiates theological from poetic
allegory.  In the former the literal as well as the mystic sense
is true, while in poetry the letter is fiction and truth resides
in the allegory alone.  The Old Testament is an accurate record
of fact, but at the same time a prophecy of the New; whereas the
fable of Orpheus is literally false and only metaphorically true.
Furthermore, Dante distinguishes, in addition to the literal and
the allegorical, a moral and an anagogical meaning: the one is
merely the useful inference that the reader may draw from a
story, for the guidance of his own life; the other, which is
obscurely defined, seems to be a revelation of spiritual truth,
hidden in the words of the text.  Dante himself, in the
interpretation of his ^canzoni~ in the ^Convivio~, confines his
attention to the literal and the allegorical senses.  Allegorical
composition was to Dante not an artificial but a natural process.
He lived in a world of mystic correspondences.  Numbers, stars,
stones, beasts, had a mysterious significance; even the events of
history were fraught with symbolic meaning.  The relation of fact
to symbol was not arbitrary nor fortuitous; it was real and
predestined; for Dante combined mystic symbolism with formal
allegory.+1~ Thus in his poem the outer and the inner narratives
seem indissolubly bound; neither obstructs the other, neither is
complete without the other, and to the intelligent reader the two
are of equal interest.  The ^Divine Commedy~ is perhaps the only
great allegory of which this can be said to-day;.  In Bunyan's
^Pilgrim's Progress~, for example, the literal story is too crude
and fantastic to please any but a child and the symbolism appeals
only to the adult.  In the ^Roman de la Rose~ the allegory is
ingenious and artistically attractive, but the literal fable is
insignificant; while the converse is true of Spenser's ^Faerie
Queene.~

	14.  In exposition, says Dante, `sempre lo litterale dee
andare innanzi'; and he adds, describing his interpretation of
his own ^canzoni~,' ragioner@o prima la litterale sentenza, e
appresso di quella ragioner@o la sua allegoria, cio@e l'ascosa
verit@a.'  We may pursue the same course.  Literally, then, the
^Divina Commedia~ is the narrative of a journey through Hell,
Purgatory, and Heaven.  The poet, in the middle of his life,
finds himself astray at night in a dark wood.  He tries to save
himself by climbing a mountain whose top is lit by the rays of
the rising sun; but three beasts, besetting his path, are about
to drive him back, when Virgil, summoned to Dante's help by
Beatrice, at the bidding of Mary and St. Lucia, appears and
offers to guide him.  They can escape from the wood only by going
through the earth from side to side.  This path leads them
through the whole of Hell, where Dante sees the punishment of
every kind of sin and converses with the damned.  Hell ends at
the earth's centre, and from that point the poets climb out by a
dark, undescribed channel to the opposite hemisphere.  They
emerge in the middle of the ocean, on the shore of an island
which consists mainly of a colossal mountain.  Cato of Utica, the
guardian of the place, meets and directs them.  Up the steep
mountain-side Dante drags himself, still accompanied by Virgil.
On the ledges are repentant souls preparing themselves by
discipline for the heavenly life.  As Dante and Virgil are
approaching the summit, they are joined by Statius, who has just
completed his penance.  The three mount together to the top,
where they find the Garden of Eden, and in it a fair, happy,
amorous young maiden, Matilda, who seems to embody the spirit of
the place.  Amid the trees and flowers they witness a pageant of
the Church, or Triumph of Revelation, whose culmination is the
appearance of Beatrice in a shower of lilies thrown by angels.
Now Virgil vanishes, and presently Statius is mentioned for the
last time.  Beatrice it is who leads Dante up from earth through
the revolving heavens into the real Paradise, which is the
presence of the Almighty, and consigns him to St. Bernard, the
great mystic.  There has been unrolled before us a picture of
mankind, past and present, and a view of the universe.  Dante's
conception of the world is essentially symmetrical and organic;
there is exact correspondence between the physical, the
intellectual, and the spiritual.  The poem ends with the vision
of God.

	15.  In its `allegorical and true sense' the ^Divine
Comedy~ is the history of a soul struggling with sin and, with
celestial help, winning peace.  The wood typifies the worldly
life; the sunlit mountain, righteousness; the three beasts, evil
habits, which make reform impossible for the unaided soul.  But
divine Mercy and Grace send Revelation to direct it -- that
heavenly enlightenment or superhuman Wisdom which ^Beatrix~, the
Bestower of Blessings, had always personified in Dante's eyes.
For direct Revelation the sinner is not yet fit; he must approach
it through Reason.  So Virgil, who typifies human understanding,
discloses to Dante the true nature of sin in all its hideousness
and folly: for the punishments of Hell so minutely described, are
but the image of the sins themselves.+1~ When Reason has probed
sinfulness to the bottom Dante horrified turns his back upon it
and painfully wrests his soul from its clutches; such is the
significance of the laborious but uneventful journey from the
centre of the earth to the Island of Purgatory.  As yet, however,
Dante has merely weaned himself from evil practices; he has still
to cleanse his soul so that wrongdoing shall no longer attract it
-- to purify it and prepare it for the sight of God.  This can be
accomplished only by discipline, under the guidance of the
Church.  Then, as the shackles of sin are removed, the soul once
more ehjoys its inborn liberty; it regains the Free Will, God's
most precious gift to man.  Of all the doctrines that Dante
expounds, that of Free Will is closest to his heart -- the
wholesome doctrine of individual responsibility.  And Cato,
Dante's favorite character in all history -- Cato, who gave up
life to assert his independence -- is made its exponent.  The
torments on the terraces of Purgatory represent the penances that
the soul, under proper direction, must undergo before it can
return to its first freedom and innocence.  As Dante approaches
the top, some questions confront him which Reason alone cannot
quite solve; and to answer them, apparently, comes Statius, or
human understanding illumined by Christianity.  When the soul has
regained its original purity -- has climbed to its Garden of Eden
-- and is restored to the primeval life of innocence and activity
(which Matilda seems to symbolize), it has no more need of
Reason, for all its instincts are unerring.  Then, after the true
glories of the Church are unfolded before its eyes, it can follow
Revelation through heaven after heaven, ever nearer and nearer to
the real Paradise, until at last it stands before its Maker.  In
the presence of the Source of all knowledge, even Revelation is
superfluous; Beatrice resigns her great office, leaving Dante in
the charge of St. Bernard, the type of Intuition. `Blessed are
the pure in heart, for they shall see God.' +1~

	16.  If to the literal and the allegorical we must add a
moral and an anagogical interpretation, we may assume that
morally the poem is a warning against sin, an exhortation and
guide to repentance, and an incentive to religious contemplation.
Anagogically, the poet may be said to portray in Hell the wicked
world as he knows it, in Purgatory the rescue of the elect, and
in Paradise the kingdom which is to come.

	17.  The epithet `Divine' became attached to the poem in
the 16th century through its use in the edition of 1555.+2~ The
title which Dante gave his work is ^Commedia~ (or, as he
pronounced it, ^Comed$ia~), meaning a poetic composition in a
style intermediate between the sustained nobility of tragedy and
the popular tone of elegy; according to the letter to Can Grande,
the name indicates also a sad beginning and a happy end.  The
author, in fact, does not scruple, on occasion, to sacrifice
elegance and even clearness to brevity, vigor, and pictorial
effect.  He expected to be minutely studied and weighed, not
cursorily read.  The characteristic detail, the specific term,
the appropriate coloring were worth more to him, when he wrote
the ^Commedia~, than any absolute standard of poetic propriety.
His imagery stamps on the reader's mind an unbroken sequence of
visual impressions.  An inexhaustible inventiveness, a compact
style, a richly varied and picturesque vocabulary make the
perusal of the ^Commedia~ a series of literary surprises.
Especially when we consider the poverty of the poetic idiom
before Dante, does the master's creative power seem almost beyond
belief.  His literary medium was virtually his own handiwork.
And this nervous strength, this irrepressible originality do not
preclude a haunting melodic beauty and a triumphant rhythm that
remind one of the tramp of many feet marching to sweet music.
Vowel harmonies, pervasive but seldom obtrusive alliteration,
skillful distribution of stress are the elements that combine to
work the spell.  His language is, in the main, the Florentine
speech of his day, as it sounded in the mouths of cultivated
people.  Mingled with this are a few Gallicisms, some archaisms,
some words borrowed from other Italian dialects, and a great many
Latinisms.  The foreign and unusual words and those employed in a
strange sense occur for the most part in the rime.  Dante was
generallv averse to periphrasis or deviation from his idea, and
was loath to end a verse with an insignificant word; so he was
sometimes forced to do violence to usage in his rimes.+1~

	18.  Of the external attributes of the ^Divine Comedy~,
the most wonderful is its symmetry.  With all its huge bulk and
bewilderingly multifarious detail, it is as sharply planned as a
Gothic cathedral.  Dante had the very uncommon power of fixing
his attention upon the part without losing sight of the whole:
every incident, every character receives its peculiar
development, but at the same time is made to contribute its exact
share to the total effect.  The more one studies the poem, the
clearer become its general lines, the more intricate its
correspondences, the more elaborate its climaxes.  At the end of
each ^cantica~ is one of these great culminating points -- the
sight of Lucifer, the appearance of Beatrice, the vision of God;
and for each of them the reader is insensibly prepared by a
series of gradations whose structure reveals itself only after
long repeated readings.  The arrangement of the ^Commedia~ is
based on the number three, the `mystic' number, the symbol of the
Trinity.  Dante shared with most philosophers of his day a
profound belief in the significance of numbers.  The ^Divine
Comedy~ falls into three books, or ^cantiche~, of nearly equal
length; each of these has thirtythree cantos, except that the
^Inferno~ has an additional first canto which serves as an
introduction to the whole.  The total number of cantos is
therefore 100, or the `perfect' number, ten, multiplied by
itself.+1~ The verse, invented by Dante for this use, is what is
called ^terza rima~, a succession of tiercets in which the first
and third lines rime together while the middle line rimes with
the first and third of the next ^terzina.~  The metre is the
^endecasillabo~, which had developed long before Dante's time.
It is really the same verse, essentially, as the French ten-
syllable line; but the French take the 'masculine,' or oxytonic,
verse as the standard, while the Italians take the' feminine,' or
paroxytonic.  The Italian line has normally, then, eleven
syllables, with an accent on the tenth: see, for instance, ^Inf.~
I, i.  Occasionally the final unstressed syllable is dropped, and
the verse becomes `masculine,' or ^tronco~: for example, ^Inf.~
IV, 60.  Sometimes, on the other hand, an extra unaccented
syliable is added, making a dactyllic ending, and the line is
called ^sdrucciolo~: e. g., ^Inf.~ XXIV, 66.  In counting
syllables, contiguous vowels, whether in the same word or in
adjacent words, are generally reckoned as one, being blended
together, as in ^Inf.~ I, 25-6:

     ` Co-s@i -- l' a-ni-mo -- mio -- ch' an-cor -- fug-gi-va
     Si -- vol-se a-re-tro a -- ri-mi-rar -- lo -- pas-so.'

But often two vowels which in prose may make separate syllables
are allowed to count as two in verse; this is regularly done at
the end of a line; so ^Inf.~ I, 12:

     ` Che -- lj -- ve-ra-ce -- vi-a ab-ban-do-na-i.'

It is done also in Latin words, as in ^Inf.~ XXXIV, 1:

     ` Vexilla regis prode-unt inferni.'

Besides the fixed accent on the tenth syllable of every line,
there must be a stress either on the fourth or on the sixth, as
in ^Inf.~ I, 1-2:

     `Nel mezzo del camm$in di nostra v$ita
     Mi ritrovii per una selva oscura.'

In point of fact, however, Dante almost always has a more regular
alternation of strong and weak syllables -- a movement more
nearly approaching that of English poetry -- than this
theoretical scheme would iridicate; see, for instance, ^Inf.~ V,
106:

     `Am$or cond$usse n$oi ad $una m$orte.' +1~

	19.  The ^Divina Commedia~ has come down to us in nearly
600 manuscripts, none of them in the author's hand, and none
taken directly from the original; upwards of 200 are in Florence,
the others are collected in Italy or scattered over Europe.  Many
are beautifully illuminated.  Although some of the manuscripts go
back to a time within fifteen or twenty years of Dante's death,
these copies are by no means in full agreement with one another;
furthermore, the early commentators cite variants: it is evident
that the corruption of the text set in as soon as the poet died,
perhaps even before his decease.  The poem was first printed in
1472.  In 1502 appeared the Aldo Manuzio edition, in 1595 that of
the Accademia della Crusca; these were for centuries regarded as
authoritative.  There was no critical edition until 1862, when K.
Witte published one based on four good manuscripts; for one canto
he also collected over 400 manuscripts.  Three years later A.
Mussafia brought out a collation of two manuscripts.  After a
long interval came editions by P.  Toynbee and G. Vandelli.  A
fruitless attempt to establish a genealogical sequence of
manuscripts was made by G. A. Scartazzini.  No satisfactory
classification of manuscripts has been made; but it is certain
that no existing manuscript or group of manuscripts can be
regarded as authoritative throughout.  In 1894 appeared the
'Oxford Dante,' ^Tutte le opere di Dante Alighieri~, edited by E.
Moore (3d ed., 1904); the ^Divine Comedy~ in this volume is based
primarily on Witte's text, but Dr.  Moore has examined for
crucial passages some 200 manuscripts, and has made use of the
investigations of other scholars.  The text published in 1921 by
the Society Dantesca Italiana (Bemporad, Florence) is due to G.
Vandelli, who from year to year had revised the Scartazzini
edition; Vandelli has clung as closely as possible to one
manuscript, the Trivulziano a carefully written one and the
earliest in real Florentine.+1~

