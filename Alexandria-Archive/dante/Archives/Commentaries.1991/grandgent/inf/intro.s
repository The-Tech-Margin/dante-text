
	ACCORDINnG to the Ptolemaic system, which was accepted
from antiquity down to the time of Copernicus, the earth is a
solid motionless sphere in the centre of the universe.  Around it
revolve nine transparent hollow spheres, each within its outside
neighbor up to the ninth, the Primum Mobile; this imparts its
movement to the others and constitutes the frontier of the
material world.  The eighth heaven carries with it all the fixed
stars.  Each of those below it contains one heavenly body: the
seventh, Saturn; the sixth, Jupiter; the fifth, Mars; the fourth,
the Sun; the third, Venus; the second, Mercury; the first, the
Moon.  They all circle around the earth together, from east to
west, once in twenty-four hours.  But each heaven except the
ninth has, besides, an independent motion of its own, so that it
is really moving in a compound curve, made up of two or more
different circular revolutions; for instance, the special
revolution of the moon is accomplished in a month; that of the
sun, in a year.  By these sets of motions, and an elaborate
system of computation by epicycles, the shifting positions of the
sun, moon, and stars were accurately accounted for.  Outside the
whole universe of matter is the spiritual Paradise, the Empyrean,
the true abode of God, the angels, and the blest.  The earth is
surrounded by air, and between this air and the heaven of the
moon is a sphere of fire, toward which all the fire on earth is
striving to return.  All natural operations on earth are
controlled by the movements of the spheres, which are directed by
nine orders of angels, or Heavenly Intelligences, created by God
for this office.i

	We have seen that two of the four elements, fire and air,
are between the moon and our globe; this body itself consists of
the other two water and earth.  The four are arranged in the
order of their lightness and their purity.  Dante believed the
earth to be perfectly spherical and about 20,000 miles in
circumference.  The continents are all grouped on one side, the
Hemisphere of Land which contains not only Europe, Asia, Africa,
and some islands, but also the Mediterranean and a part of the
great ocean; at the exact centre of this hemisphere is Jerusalem.
On the other side is the Hemisphere of Water, in which is no
land, except (according to Dante's idea) the mountainous Island
of Purgatory, situated precisely opposite Jerusalem.  At the top
of the mountain of Purgatory is the Garden of Eden.  The greater
part of the land on the earth's surface is north of the equator,
the greater part of the water is south; but the Hemispheres of
Land and Water by no means coincide with the northern and
southern hemispheres.  At the eastern extremity of the Hemisphere
of Land is the River Ganges, at the western edge are the Straits
of Gibraltar.  The Mediterranean was thought to extend over 900,
or a quarter of the earth's circumference; Italy, midway between
Gibraltar and Jerusalem, is therefore 450, or three hours, from
each.i

	Hell is a vast cavity in the form of an inverted cone,
whose apex is at the centre of the earth and whose circular base
lies beneath the Hemisphere of Land, from which it is shut off by
a crust; it extends, apparently, from Italy to mid-Asia.  The
round declivity of -the cavern is broken into nine steps, each of
which runs all the way around it; they are of unequal width and
separated by cliffs of varying height and steepness.  Two
enormous precipices divide it roughly into three horizontal
sections.  A huge wall, circling around one of the terraces,
severs the outermost section from the other two, making an Upper
and a Lower Hell; the latter is called the City of Dis.  On each
of the steps is punished some particular kind of sin: in the
Upper Hell, the sins of Incontinence, due to lack of selfcontrol;
in the Lower Hell, the sins of Violence and Fraud, due
respectively to Bestiality (or rather ^bestilialis malitia) 2~
and Malace or ^malitia humana~).  Violence occupies the middle
section Fraud the lowest.  Four steps, or circles,i are devoted
to the four kinds of Incontinence -- lust, gluttony, avarice (and
prodigality) anger.  One circle suffices for Bestiality, but it
is divided into three parts, according to the object of the
violence: that object may be one's neighbor, one's self, or God.
Malice occupies two circles: in the first are the fraudulent,
those who deceived persons not bound to them by any special ties;
in the second are the traitors, destroyers of their kinsfolk,
their countrymen, their guests, or their benefactors.  This last
circle forms the very floor of Hell; it is a lake of ice at the
bottom of a pit; embedded in the middle, at the centre of the
earth, is Satan, in whose three mouths are the three arch-
traitors, Judas, Brutus, and Cassius.  Outside of this general
scheme, but within Hell, are three regions inhabited respectively
by the souls of sluggards and time-servers, those who were
neither good nor bad; the souls of unbaptized children and
virtuous pagans; and the souls of heretics.  All three are
circles, like those mentioned.  The first, sometimes called the
^Antinferno~, is a vestibule, just inside the entrance, but
outside the River Acheron.  The second, the Limbus, is within the
encircling Acheron, at a lower level than the Vestibule, and
forms the first of the nine steps.  The third, which constitutes
the sixth circle, lies close within the walls of the City of Dis,
but is separated from the rest of the Lower Hell by a mighty
precipice.  The souls in the nether world are, then, arranged in
this order: sluggish; ^unbaptized~; lustful, gluttonous,
avaricious (and prodigal), wrathful; ^heretical~; violent;
fraudulent, treacherous.  The sluggish, the unbaptized, and the
heretical lie outside the three great classes -- Incontinence,
Violence, Fraud.  The sluggish are in the Vestibule; all the
others are in the nine circles.  The punishments vary according
to the sins, each being a retaliation for the offence.  It must
not be forgotten, however, that allegorically the torments
represent the sins themselves. `Wherewith a man sinneth, by the
same also shall he be punished' (Wisdom xi, 16).  Dante, under
the guidance of Reason, ransacks the human heart and learns to
know wickedness as it really is, stripped of the false semblance
of good.  Thus, for instance, the furious blast that eternally
wafts the carnal sinners symbolizes irresistible passion; the ice
in which traitors are buried is the coldness of the heart from
which all love has been expelled.i

	In Dante's Purgatory the sinners are arranged as follows:
lustful, gluttonous, avaricious (and prodigal), ^slothful~,
wrathful, ^envious Proud.~  Sloth intevenes between avarice and
anger; envy and pride correspond to the violence, deceit, and
treachery of Hell; there is no place for paganism or heresy.  The
difference is a natural one.  Hell is the eternal abode of those
who die unrepentant; Purgatory is a-place of passage for those
who, whatever their crimes may have been, die penitent within the
Church.  In Purgatory we have to do only with man's fundamental
evil dispositions, of which the soul is to be cleansed; in Hell
souls are tortured for specific acts or states of the will, the
multifarious fruit of these dispositions.  The seven capital
vices had long been defined by Church writers, and their order,
in the main, was pretty well established; the relative positions
of sloth and wrath were the most doubtful.  In the ^Moralia~ of
Gregory the Great (XXXI, Cap. 45) and in one passage in St.
Thomas (^De Malo~, VIII, i) they are all arranged as in Dante,
but St. Thomas has them in three other orders, and apparently
regards their sequence as unimportant; he prefers ^inanis gloria~
to ^superbia~ as a designation of Pride.  All the sins in the
Lower Hell are directed against justice, and are due to some kind
of malice, originally caused, perhaps, by Envy and Pride.  Pride,
indeed, is the foundation of all sin, inasmuch as sin consists in
defying God's law; this doctrine is laid down by St. Cyprian, and
recurs in Gregory and St. Thomas.  Sloth, or lukewarmness in love
of the Lord and his creatures, corresponds to the philosophical
^ignavia~ and ^pusillaninimitas~; in so far as it belongs in the
nether world at all, it has its proper place in the Vestibule.
The unbaptized are beyond redemption, and therefore Purgatory is
denied them.  Heresy belongs to the speculative intellect, not to
the lower appetite nor to the will; it is neither incontinence
nor malice and therefore has no manifestly appropriate place in
Dante's system, nor in St. Thomas's.  What becomes of repentant
heretics we are not told, but we may assume that their penance
must be paid in the circle of pride.i

	The mystic journey occurs in 1300, the year of the great
Papal jubilee proclamed by Boniface VIII.  It was a time of
general religious enthusiasm, an appropriate moment for a moral
awakening.  The date is given vaguely in the opening line of the
poem definitely in^ Inf.~:XXI, 112-4.  This latter passage tells
us also that the descent was begun on the anniversary of the
crucifixion.  This may mean March 25, the real date, or Good
Friday, the movable Church anniversary.  Good Friday in 1300 fell
on April 8, and several references in the poem seem to fit that
day better than March 25.  ^Inf.~ XX, 127 and ^Purg.~ XXIII, 119
inform us that the moon was full the night before; in reality the
full moon occurred in 1300 on April 5, but in the ecclesiastical
calendar for that year the Paschal full moon was set down for the
night of April 7.  ^Purg.~ I, 1920 represents Venus as the
morning star two days later; this was the case in 1301, not in
1300, but here again it was surely the almanac that led Dante
astray.  There is a peculiar fitness in starting on the downward
journey on the evening of Good Friday, when day and hour are
conducive to gloom.  The ascent of Purgatory, on the other hand,
begins at a time when everything suggests hope, the morning of
Easter Sunday.  Throughout the poem we are apparently to think of
sunrise and sunset as occurring at six o'clock.  It is, then, on
the night of April 7, 1300, that Dante comes to his senses in the
dark wood-of sin.2 The next day he spends in trying to struggle
out, directing his steps toward the sunlit mountain of
righteousness; but three beasts -- his evil habits -- impede his
progress.  When all seems lost, Virgil, or Reason, appears and
offers to lead him out by another way.  They enter Hell at sunset
on April 8, and spend the night and the next day in their spiral
course, turning always to the left as they descend.  In Hell they
go by the time of Jerusalem, which is directly over the bottom of
the pit.  When they reach the centre of the earth, they pass
beyond, climbing along the shaggy side of Satan, who is planted
there; then, of course, they are under the opposite hemisphere,
whose middle point is Purgatory, between which and Jerusalem
there is a difference of twelve hours.  Dante represents Virgil
and himself, therefore, as gaining twelve hours when they pass
the earth's centre: they have a new Saturday before them, and
they use all that and the following night in climbing out, by a
dark, difficult cavern, to the other side of the earth, where
they emerge on the Island of Purgatory on Sunday morning.  Dante
has turned his back on sin, has laboriously weaned himself from
it, and is now ready to cleanse his soul by penance.

	Virgil evidently represents Reason, human understanding,
as opposed to Revelation, heavenly intelligence, embodied in
Beatrice.  One may ask why he was chosen for this function,
rather than Aristotle, `il filosofo,' `maestro di color che
sanno.'  For many centuries the ^@@AEneid~ had been the best of
school-books, the one from which pupils learned grammar,
rhetoric, history, mythology.  It was expounded literally and
allegorically.  Its author, at least until Aristotle was
discovered in the 12th century, was universally regarded as the
wisest man of antiquity, the personification of the best that
humanity, without superhuman enlightenment, could achieve; and
even in 1300 his fame was scarcely dimmed by the greater glory of
the Greek philosopher.  Moreover, he had already proved, in the
sixth book of the ^@@AEneid~, his competence as a guide to the
other world.  People generally believed, too, that in his fourth
Eclogue he had unconsciously prophesied the coming of Christ.
Furthermore, Aristotle was to Dante only a book, while Virgil had
been so long a figure in popular and scholarly legend that he had
become a distinct personality, one with whom it was a joy to
travel and from whom it was anguish to part.  Dante thought of
himself as in reality a disciple and successor of Virgil.  The
Mantuan was the bard of Imperial Rome, its beginnings in history
and its future greatness as ruler of the world.  The Florentine
was the prophet of the second advent of Rome, now seat of both
temporal and spiritual authority.  The ^Commedia~ is, then, as it
were, a second ^@@AEneid.~  Lastly, Dante felt for the master of
his childhood, his model in later years, a warm personal
gratitude that he was eager to express 1:

     `Tu se~ lo mio maestro e `l mio autore;
     Tu se' solo colui da cu' io tolsi
     Lo bello stilo che m' ha fatto onore.'
     In the ^Image du Monde~, of the first half of the 13th century, St.

Paul says of Virgil:

     ` Ha! quel je t'eusse rendu
     A Dieu, se tu fusses vivant!'
     One may compare a canticle, sung at the office of St. Paul in

Mantua, apparently as late as the 15th century:

     `Ad Maronis mausoleum
     Ductus, fudit super eum
     Pie rorem lacryme.
     "Quam te," inquit, " reddidissem,
     si te vivum invenissem,
     Poetarum maxime!"

