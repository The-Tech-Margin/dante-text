     The fourth circle contains both misers and spendthrifts -- those

who showed no moderation in the use of worldly goods, but handled
them so' undiscerningly' that they are themselves made
indistinguishable, `darkened beyond discernment,' their
individuality being sunk in their vice.  The insulting cries
which they exchange -- `Why hoard?' `Why squander?' -- proclaim,
or `bark,' their sin clearly enough.  So the usurers, in Canto
XVII, are altered beyond recognition, and can be picked out only
by their moneybags.  As Dante comes down among the sinners of the
fourth shelf, the avaricious, as the more despicable, are on his
left, the prodigal on his right.  The `clenched fist' is the sign
of greed; the `cropped hair,' of lavishness.  Clerics form a
large part of the miserly host; Dante was by no means alone in
regarding avarice as the besetting fault of the clergy.  In this
canto we find none of the gentleness with which our poet treats
the amorous; even the gluttonous receive more consideration.  The
verses bristle with derisive terms: ^cozzi, zuffa, ontoso metro,
rabbuffa.~  As in ^@@AEn.~ VI 616 `saxum insens volvunt alii,' so
these two classes of sinners, each traversing one-half the ring,
roll huge weights, pushing with might and main 'by strength of
chest,' the misers moving toward the right, the spendthrifts
toward the left.  When they reach a spot in front of Dante, they
clash together; then, with mutual execration, they turn about and
laboriously work their way to the opposite side of the ring,
where the encounter is repeated.  And so the weary, futile round
goes on to all eternity, a fit image of the incessant and useless
efforts of humanity to transfer worldy possessions.

	For this transfer God created a special minister,
Fortune, a power similar to the celestial intelligences that move
the heavens.  She may be called the Angel of Earth.  It is her
mission to shift prosperity to and fro, without apparent plan,
seeing that it remain not too long with one person, family, or
nation.  In many of his utterances about Fortune, Dante evidently
follows Boethius, in whom, as later in Albertus Magnus, there
appears not only the pagan but also a distinctly Christian
conception of her as God's instrument; but her rank as an angelic
intelligence is bestowed by Dante himself.

