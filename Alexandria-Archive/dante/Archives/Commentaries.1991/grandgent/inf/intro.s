	THIS canto, which serves as a general introduction to the
poem, is more formal in its allegory than those which follow; it
affords, in some measure, a key to the whole interpretation.
This announcement and summary of the whole ^Commedia~ may be
compared to the opening lines of the ^@@AEneid~, the ^Pharsalia~,
and the ^Thebaid.~+1~ The author has purposely enveloped its
incidents in a veil of mystery, which enhances its
impressiveness.

	It is the night of April 7, the night before Good Friday
in the great jubilee year 1300.  Dante, at the age of thirty-
five, suddenly becomes aware that he is astray in the dark wood
of worldiness.  In terror he seeks refuge at the foot of the
mountain of rectitude, whose summit is lit by the rising sun.
The sun, here and elsewhere, typifies enlightenment, perhaps more
specifically, as Flamini suggests, righteous choice, the
intelligent use of the free will.  When Dante tries to scale the
hill, three beasts beset his path, a leopard, a lion, and a wolf
-- the same creatures that appear in Jer. v, 6: `Wherefore a lion
out of the forest shall slay them, and a wolf of the evenings
shall spoil them, a leopard shall watch over their cities: every
one that goeth out thence shall be torn in pieces.'  Apparently
he has a fair prospect of passing the first two, at least the
leopard, but the wolf drives him back.  These animals evidently
stand for Dante's vicious habits, which prevent his reform.  The
old commentators interpreted them respectively as luxury, pride,
and avarice; this would imply (unless we understand the poet's
whole experience to be generic, not individual) that Dante's
dominant sin was avarice, which is scarcely believable.  A modern
view, upheld by Flamini, is, in spite of some grave objections
far more satisfactory in itself and more in harmony with the
whole structure of the poem.  Inasmuch as the sins of Hell fall
under the three heads, Incontinence, Violence, and Fraud, it is
natural that the beasts should stand for corresponding practices:
the ravening wolf is Incontinence of any kind, the raging lion is
Violence, the swift and stealthy leopard is Fraud.  St. Thomas
and Richard of St. Victor, two of Dante's favorite authors, saw
in the spotted pard a fit symbol of fraudulence.  We may
understand, then, from the episode, that Dante could perhaps have
overcome the graver sins of Fraud and Violence, but was unable,
without heavenly aid to rid himself of some of the habits of
Incontinence.

	At this crisis Reason, personified in Virgil, comes at
divine bidding, to the sinner's rescue.  He declares that escape
is possible only by another route, which will lead them through
Hell: we cannot run away from evil before we know what it really
is; a rational understanding of human wickedness must precede
reformation.  The wolf, he says, is ravaging the world, and will
continue to do so until a Hound shall appear and drive it back
into Hell, whence it first came.  This Hound is obviously a
redeemer who shall set the world aright.  If we compare this
passage with another prophecy in ^Purg.~ XXXIII, 40-45, it is
tolerably clear that he is to be a temporal rather than a
spiritual saviour -- a great Emperor whose mission it shall be to
establish the balance of power, restore justice, and guide erring
humanity.  Such an Emperor, destined to come at the end of the
world, was not unknown to legend; his advent appears to have been
sometimes associated with the ^annus canicularis~, the period of
Sirius, the Dog Star.  As the prediction was still unfulfilled at
the time of writing, Dante naturally made it vague; in fact, he
rendered Delphic obscurity doubly obscure by adding the
mysterious words `tra Feltro e Feltro.'  We know that the poet
entertained great hopes of the youthful leader, Can Grande della
Scala in Dante's last years the chief representative of the
Imperial power in Italy.  It is possible that he so constructed
his prognostication as to make its application to Can Grande
evident in case those hopes should be realized, but not obtrusive
in case they were not. `Veltro' easily suggests Can Grande;
`Feltro e Feltro' may point to the towns of Feltre and Monte
Feltro.  Dante's conception of the just Emperor was perhaps
influenced by current stories of the Grand Khan of Tartary, who
was said to despise wealth and to live simply in a `felt' tent,
and whose title had a strange likeness to the name of the
Imperial Vicar General.

