	5.  Visionary as he was, saddened by his own misfortunes,
and exasperated by human wickedness, he had a fundamentally
healthy disposition.  In his character fierce passion was mated
with equally vigorous self-control; vehemence was balanced by
introspection and self-judgment; imagination was yoked with
logic.  He admired simplicity, even asceticism, but he was far
from being a foe to culture or to the usages of polite society.
He was fond of courtly pursuits, and erudite even to pedantry.
In the great writings of pagan times he found a source of endless
delight, and he did not hesitate to put them on a par with his
Christian authorities.  His admiration was less excited by
Christian martyrs than by heroic pagan suicides.  His Christ is
always sublime, a part of the triune God, never the meek lamb nor
the humble preacher of Galilee.  His outlook upon life was
persistently hopeful.  Bad as the world was, there could be no
doubt of ultimate reform.  The Lord, in his unfathomable wisdom,
might allow evil to triumph for a while, but his kingdom was sure
to come.  Dante's political views were entirely abstract and
theoretical, and reactionary even for his own day.  He had always
before him the general principle rather than the particular case.
Man being naturally a social creature, political organization is
necessary, both in order to supply the manifold needs of the
community, assigning different functions to different members,
and in order to check greed and insure justice and peace, so that
every citizen may have an opportunity to attain his highest
development.  The State, then, is as necessary -- though not so
venerable -- as the Church.  God ordained both, and decreed that
C@@aesar should found the one, Christ the other.  Empire and
Papacy are co,ordinate powers, neither subservient to the other,
but both responsible directly to God.  The goodness and happiness
of the world depend on the balance of the temporal and the
spiritual authorities.  The rapacity of the clergy and the
negligence of sovereigns have disturbed this equilibrium by
transferring to the Papacy what belongs to the Empire.  Mankind
cannot thrive until the adjustment is restored.  In his treatment
of civics, and in the importance given to avarice as a disturber
of society, Dante follows Aristotle (^Ethics~, V), whom he first
knew through the commentary of St. Thomas.+1~ From the same
master he derived vast stores of physical information, as well as
a philosophical method and terminology.  His general
classification of sins and his definition of moral virtues are
Aristotelian, but his essential conception of sin and virtue is
quite different, being thoroughly Christian.  One can only guess
what Dante would have been had he really known Plato, to whom he
was temperamentally so much more akin.  On the ethical side Dante
was an eclectic, as were his ancient masters, Cicero and
Boethius; Senecan stoicism and Franciscan mysticism dwelt in him
side by side.  St. Thomas, the interpreter of Aristotle, the
learned and ingenious expounder of moral philosophy and Christian
dogma, furnished Dante with an abundance of religious doctrine
and a host of subtle observations and arguments (not always
devoid of inconsistency), and reinforced his inborn fondness for
intricate reasoning.  To St. Thomas's teacher, Albertus Magnus,
the `Universal Doctor, ' perhaps the greatest scholar and
philosopher of the Middle Ages, Dante was directly indebted for
not a few of his physical and ethical ideas.  Like St. Augustine,
Dante takes as his starting-point the Pauline doctrine of
predestination and grace; but he draws from it very different
consequences.  In Dante, as in St. Paul, love is supreme.  It is
the moving force of the universe and of God himself.  Through
love God was impelled to create, that there might be others to
share his happiness.  Everything created is filled with love of
the Creator.  Animals, plants, lifeless things express their love
by being what he made them: the stone, bv its hardness and its
magic power; the star, by its light and its influence upon earth;
the beast, by following its instinct.  They cannot do wrong,
having no choice.  It was God's purpose, however, that there
should be creatures with an individual consciousness, a real life
of their own; this could not be without freedom of the will, and
such freedom implies the possibility of sin.  Angels and men
alone were given ability to sin, because they alone were created
free.  But almost instantly after their creation the angels were
endowed with such overwhelming grace of vision that their will
was identical with that of their Maker.  The revolt of Lucifer
and his fellows occurred in the moment between creation and the
acceptance of this grace.  The power of vision bestowed on the
Heavenly Intelligences is not the same for all: no two angels see
God alike, and consequently their natures and functions are
diverse, though entirely good.  So it is with men.  God, in his
grace, gives to different men, as he shapes their souls,
different degrees of vision.  On this vision all their knowledge
and all their love depend.  According to its clearness, the love
of God is more or less intense, wisdom is greater or less, the
choice of good and evil is easier or harder, and the eternal
state of the soul, if Heaven be attained, is a higher or lower
degree of blessedness.  Every soul has sight enough to win
salvation, and is therefore fully responsible; but some are
capable of greater beatitude than others.  Predestination
becomes, then, in Dante, a mysterious manifestation of God's
love: he loves all men, but he fashions them for different ends,
on earth and in Heaven, and his love for all is not identical.
The natural instinct of man is to return to his Creator and to
love all that is like him.  But through inexperience and lack of
guidance he may at first mistake evil for good.  He possesses,
however, the grace of vision, which enables him to discriminate;
if he persists in wrongdoing, he rejects grace and sins.  If he
dies unrepentant, he loses grace forever; if he repents before
death, he retains grace and innocence by discipline.  Had mankind
from the beginning made the proper use of the free will, there
would have been no death; all human beings would have lived
happily on earth until the Judgment Day, and would then have been
taken up to Heaven in the flesh.  Adam's disobedience brought
death and sin into the world, and suspended salvation until
atonement was made by Christ.  Only by faith in Christ, before or
after his coming, can man be saved; but this faith is
(theoretically, at least) within the reach of all.  We have seen
that man's path is made unduly hard by the lack of temporal
direction, for which the impotence of the State is to blame.
Men's talents and dispositions differ, too, being the product of
the stars -- governed by Heavenly Intelligences -- that presided
over their birth; but every human creature has power to overcome
his natural defects so far as to make himself worthy of Heaven.
The origin of imperfection in the universe is a difficult (not to
say insuluble) problem -- one to which Dante often reverted,
without ever finding a satisfactory answer.  His main argument is
that whatsoever is directly shaped by God is perfect what is
fashioned by nature (that is, by the influence of the stars) is
faulty.  God created brute matter, the heavens, the angels and
creates human souls as they are born.  All the rest is the work
of nature.  Why nature, itself the work of God, should operate
defectively, we are not told.  Elsewhere Dante says that God, in
making material things, has to work with matter, which being
imperfect, the divine plan is not realized; but inasmuch as God
created matter, this statement can be reconciled with the other
only by the supposition that Dante here means, not brute matter,
but matter already differentiated and compounded by nature.  His
whole explanation reduces itself to this: the angels, having an
incomplete vision of the divine mind, cannot execute its intent
so well as God himself.  If we ask why -- this being the case --
the angels were given this ministry, or, having the charge, were
not endowed with complete sight, no reply is offered.  The theory
that evil was introduced into the world that man might have
exercise for his free will is not formulated in the ^Divine
Comedy.+1~~

	6.  Outside of the ^Divine Comedy~, Dante's ideas are to
be found, first of all, in the ^Convivio~, an unfinished
encyclopedic work, in Italian, in the form of a discursive prose
commentary on fourteen of the author's ^canzoni~; of the
projected fifteen books, only four were written, and only three
^canzoni~ are included.  The logically constructed, but purely
idealistic and theoretical ^Monarchia~, a Latin treatise in three
books, contains his political views.  Another Latin treatise, the
uncompleted ^De Vulgari Eloquentia~, gives us his opinions on
language in general, the use of the modern idiom as a literary
medium, the relative merits of the various Italian dialects, and
the principles of poetic composition in the vulgar tongue; he
believed that an ideal, universal Italian, different from any of
the actually spoken dialects, was fit, not onlv for amatory
verse, but for martial and moral themes as well.  A third Latin
work ascribed to him, the ^De Aqua et Terra~, is a controversial
lecture delivered in Verona in 1320, debating technically the
question whether the water of the sea rises in any part higher
than the land.  Some fourteen Latin letters, written at different
periods, are attributed to him in manuscripts, and there is
record of others, now lost; the authenticity of all the fourteen
has been doubted, but it is probable that at least ten are his.
Of a political character are the epistles to the princes and
peoples of Italy, to the Emperor, to the Florentines, to the
Italian cardinals, to a Florentine friend, and an early one to
Niccol@o da Prato.  The most important from a literary
standpoint, if he really wrote it, is the later, ponderously
exegetical ^Epistola ad Canem Grandem~, accompanying the first
canto of the ^Paradiso~ with a minute comment on its opening
lines and a general discussion of allegory.  Many letters and
many poems have doubtless perished.  On the other hand, some
pieces of verse that have gone under his name are, in all
likelihood, not his.  There seems to be no sufficient reason to
ascribe to him the long sonnet sequence called ^Il Fiore~, an
abridged paraphrase of the ^Roman de la Rose.+1~~ Among the
miscellaneous poems, not contained in his longer works, that are
attached to him, we may reckon as his a dozen ^canzoni~, a half
dozen or more ^ballate~, two ^sestine~, and some twenty-five or
thirty sonnets; they were composed at various times, and treat of
love, philosophy, ethics, and sundry occasional topics.  Some of
them are similar in tone to those comprised in the ^Convivio~,
others are in the same vein as the thirty-one (mostly sonnets)
that form the skeleton of the ^Vita Nuova.~  This `little book'
consists of a carefully selected and artistically arranged series
of amatory poems, surrounded by a dainty prose commentary telling
of the poet's early love for Beatrice.  There is reason to
believe that the prose was written in 1292.+2~ Now the final
chapter of the ^Vita Nuova~ speaks of a wondrous vision which
determined the lover to write no more of his lady until he should
be worthier of the theme; to prepare himself, he was studying
with might and main, and he hoped, if his life were spared a few
years, to say of her what never had been said of woman.  This
study, begun for comfort's sake in the darkest hour of mourning
for his dead love, as he declares in the ^Convivio~ soon became
an object in itself, and aroused a new passion that threatened to
quench the memory of the old.  Beginning with Cicero's ^De
Amicitia~ and the ^Consolatio Philosophi@@ae~ of Boethius, he
plunged deep into philosophy and theology.  Of the vast knowledge
thus accumulated the scholar was naturally proud, and he planned
to set it before his fellow-men in the ^Convivio~, which was to
be a guide to others and a defence of himself.  This treatise
doubtless occupied him between 1305 and 1308.  Only when vast
hopes were awakened and then temporarily blighted by the advent
and death of his Imperial hero, did Dante forsake this project;
then, leaving it two thirds unrealized -- leaving unfinished,
too, it would seem, his ^De Vulgari Eloquentia~, -- he returned
to the fulfilment of his earlier purpose, which, in all
probability, he had never quite abandoned.  Conceived, then, it
appears, as early as 1292, the plan of the ^Divina Commedia~ was
probably not fully matured until after the death of Henry VII,
the Emperor whose advent in 1310 had aroused such wild
expectations in Dante's breast.  As Henry died in 1313 and the
poet in 1321, we may ascribe the composition of the work, in the
main, to the years that lie betsveen these dates.  Some critics,
however, put the ^Inferno~ much earlier.  Both the ^Inferno~ and
the ^Purgatorio~ were made public, either singly or together,
considerably before the ^Paradiso.~  If report is to be trusted,
this last ^cantica~ busied the author until the very end of his
life.  Yet he found time, in his last years, to write two
graceful Latin eclogues and (if they be his) the letter to Can
Grande and the ^Qu@@aestio de Aqua et Terra.+1~~ There is no
external and no definite internal evidence to fix the date of the
^Monarchia~; its general style and maturity point to the latest
possible period.+1~

