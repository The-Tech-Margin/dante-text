	The descent from Limbus to the second circle is not
described; we have no means of conjecturing the size or the
steepness of the cliff.  The journey through Hell being
physically impossible, Dante purposely refrains from furnishing
particulars that might destroy the illusion, while abounding in
such details as serve to heighten it.  As the pit narrows
progressively toward the bottom, the terraces correspondingly
decrease in circumference, but the penalties become more and more
severe.  At one point in the round of this shelf is a break,
where the rock has fallen.  When Dante mentions this ^ruina~,+1~
in l. 34, he offers no explanation: shrieks and curses are
redoubled here, but we know not why.  Our suspense lasts until we
reach Canto XII, ll. 31-45.  There we are told that when Christ
descended into Hell, his coming was preceded by an earthquake,
which shook down the walls of the abyss in three spots.  Mat.
xxvii, 51: And behold, the veil of the temple was rent in twain
from the top to the bottom; and the earth did quake, and the
rocks rent.'  Those broken places lie beside the circle of the
pagans just beyond the enclosure of the heretics, and over the
hypocrites by whom Christ had been condemned (XXI, 112-4; XXIII
133-8) -- all close to the abodes of those who had offended the
Saviour by disbelief in his mission.  In each case the word
`ruin' is used.  The sight of the first ^ruina~ moves the souls
of the second circle to lamentation, because it reminds them of
the time when the neighboring Hebrew spirits in the Limbus were
rescued, while all the other souls in Hell were left to eternal
torment.+1~

	Most of the fallen angels, or fiends, are in the lower
Hell; a few, however, appear as presiding genii outside the City
of Dis: so Charon, Cerberus, Plutus, Phlegyas, and, at the
threshold of the second circle, Minos, the judge.  Both
theologians and simple folk were prone to look upon the heathen
gods as demons who had beguiled men into their worship.  It is
not strange, therefore to find in a Christian Hell many classic
personages, especially such as were already associated with the
lower world.  Dante did not treat all the pagan divinities alike;
if he depicted Plutus as a dei, the Muses and Apollo were to him
simply allegorical figures, while Jove apparently represented the
ancient poets' dim conception of the Supreme Being.  Minos, the
great king and legislator of Crete, holds in the 11th book of the
^Odyssey~ the noble office of judge of the dead.  In the
^@@AEneid~, VI, 432-3, though briefly sketched, he retains the
same honorable function:

     `Qu@@aesitor Minos urnam movet: ille silentum
     Conciliumque vocat, vitasque et crimina discit.'

In Dante he has become a hideous demon, arbiter of the damned --
the symbol, it would seem, of the guilty conscience.

	The second circle punishes ^lussuria~, or lust, the first
of the sins of Incontinence.  The luxurious are forever blown
about in the darkness by stormy blasts, typifying the blind fury
of passion.  In some previous tales of Hell a wind torments
evildoers, notably in the ^Visio Alberici~, XIV, where souls are
driven by the fiery breath of a dog and a lion.  Dante divests
the torment of all grotesqueness, and, indeed, treats the sinners
of this class with special consideration.  This may be due in
part to sympathy, and partly, no doubt, to a sense that their
fault is the result of a mistaken following of love, the noblest
of human emotions.  Theologically speaking, the fate of lost
souls should arouse no pity, as the sight of sin should excite
only repugnance.  But we must remember that the Dante who is
visiting Hell is himself still a sinner.  Moreover, allegorically
interpreted, these harassed souls are men and women loving and
suffering on earth; and even the most sinful, as long as they
live, are fit objects of compassion.

	Compassion, tenderness, sympathetic curiosity, anguish,
reach their climax when Dante meets and converses with Francesca
da Rimini.  This unhappy lady was the daughter of Guido Minore da
Polenta, a powerful citizen of Ravenna, and was married to
Giovanni di Malatesta da Verrucchio (called Sciancato, or Gian
Ciotto), master of Rimini.  Of her love for Paolo, her husband's
brother, and the murder of the two by Giovanni, we have no record
before Dante, although the event must have been well known.  It
probably occurred about 1285: when Dante was eighteen, in 1282-3,
Paolo was for five months in Florence as Capitano del Popolo; he
is not mentioned among Malatesta's sons in 1287; and in 1288
there is evidence of a child born to Giovanni by a second wife.
In 1285 Paolo was some 35 years old, had been married sixteen
years, and had two children; Francesca had one child.  Paolo's
daughter married a son of Aghinolfo of Romena.  After Francesca's
adventure had been made eternally famous by Dante's poem, many
fables grew up about it; her fate is still a favorite theme for
artists and authors.  Of all the episodes in the ^Commedia~, this
has always been the most popular.

	It is not alone the undying passion of Francesca that
moves us, but even more her gentleness and modest reticence.  In
her narrative she names none of the participants; not even her
city is called by name.  Her identity is revealed by Dante, who,
recognizing her, addresses her as' Francesca.'  Everything in her
story that could mar our pity is set aside, and nothing remains
but the quintessence of love.  Amid the tortures of Hell, where
all is hatred, her love does not forsake her, a-dn she glories in
the thought that she and Paolo shall never be parted.

	Should we be inclined to question whether mere impersonal
sympathy, however natural and profound, could have sufficed to
lead a religious poet, a stern moralist, thus to idealize an
adulteress and mitigate her punishment, we might feel ourselves
justified in seeking some special reason for his kindliness.  As
we look through the ^Commedia~, we find that in one place or
another the exiled poet contrived to pay an appropriate tribute
to all those who had befriended him in his need: it was the only
return his grateful heart could make.  His last and probably his
happiest years were spent in Ravenna under the protection of
Guido Novello da Polenta, himself a poet, a nephew of our
Francesca.  Now, we do not know exactly when Dante went to that
city, but in any case it was almost certainly at a period later
than the time when the ^Inferno~ was composed.  His son Pietro,
however, established himself in Ravenna, perhaps as early as
1317, receiving a benefice from Guido's wife; and his daughter
Beatrice entered a convent there.  It is possible that previous
courtesies, of which we have no record, were extended to Dante or
his kindred before this ^cantica~ was completed.  Guido Minore da
Polenta, Francesca's father, was ^podest@a~ of Florence in 1290,
when Dante was 25.  There is, then, some slight ground for the
supposition that this passage was intended as an incidental
homage to Guido's family (there being no other tribute to it in
the poem), a rehabilitation of Francesca's memory.  Love, she
says, comes to gentle hearts with irresistible force --' a nullo
amato amar perdona.'  Had she lived, she would have repented; it
was her sudden taking off that damned her.  Her fate is
contrasted with that of her husband: her soul is one of the
highest in Hell; his, one of the lowest.

