	Taken together, these passages corroborate the ancient
tradition of the revolt and fall of the angels (cf. the note to
III, 7), and at the same time furnish ground for an
identification of Lucifer with Satan, the Devil, the Serpent, and
the Dragon.  As we shall see from 1. 127, Dante regarded
Beelzebub as still another name for the same demon.

	In spite of the abundance of realistic detail that makes
us share with Dante the experiences of this canto, we must
consider his portrayal of Satan as essentially allegorical.  His
Devil is the image of sin, the principle of evil, the negative
counterpart of God, who is the principle of good.  As the Godhead
comprises three persons, -- Father, Son, and Holy Ghost,
representing the three attributes, Power, Wisdom, and Love, -- so
Lucifer is pictured three-faced: his red visage betokens Love of
Evil, or Hate; the black face is the emblem of Ignorance, the
opposite of Wisdom and the source of pride; the pale yellow one
signifies Impotence, the opposite of Power and the begetter of
envy.  Del Lungo sees in the three faces also a symbol of the
three continents of the earth.  Just as the Holy Ghost, or Love,
continualiy proceeds from Father and Son, so, in Satan, Hate is
the result of ignorant Pride and impotent Envy.  Dante's Lucifer,
though less grotesque and fantastic than the usual diabolical
monster of vision literature, is ugly beyond description.  Like
the four beasts surrounding God's throne in Rev. iv, 8, and like
the seraphim of Isaiah vi, 2, he has six wings; a pair of them
sprouts beneath each face, and the three winds produced by their
flapping freeze Cocytus.  Immovable and helpless in the ice of
his own making he holds sway over his' doloroso regno' -- so it
would seem -- by these winds alone.  They are the Satanic
instigations, the inspiration of sin.  Presumably they correspond
to the three great divisions of Hell, the' tre disposizion che `l
ciel non vuole' of XI, 81-3.  From the wings of Love of Evil
issues the blast of Fraud or Malice; from the wings of Ignorance,
the blast of Violence or Bestiality; from the wings of Impotence,
the wind of Incontinence or weakness to resist the passions.  In
Eph. ii, 2, Satan is called "Prince of the power of the air.'

	Having explored all the manifestations of sin, and having
finally scrutinized its very essence, Dante, with the help of
Reason, turns his back upon it and laboriously wrests himself
from its attraction.  That is the allegory of the long and
uneventful climb from the bottom of Hell, at the earth's centre,
out to the surface on the other side: it is the steady,
monotonous effort by which the remorseful wrongdoer is weaned
from evil practices.  In this journey Dante has no light to guide
him -- only the encouraging murmur of a streamlet of discarded
sin (perhaps the tears of penitence), flowing constantly from
Purgatory, where wickedness is washed away, down to its original
Satanic source.

	When the practice of evil has been brought to a stop,
there still remains the duty of penitentially restoring the soul
to its first purity and cleansing it of all disposition to sin.
This discipline is symbolized by the ascent through the torments
of the mountain of Purgatory, a task which is performed in the
light and under heavenly direction.

