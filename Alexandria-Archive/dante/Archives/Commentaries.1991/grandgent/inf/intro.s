grieved with her sorrow at the loss of a friend. and again when
her father was taken from her; he was tormented by a foreboding
of her death.  Stirred by feminine criticism, he determined to
exclude supplication from his verse and make all his love-poetry
a hymn of praise.  So much he tells us, in the ^Vita Nuova~, of
his relations with the living Beatrice.  After her death, in
1290, her image seems to have become clearer and more fixed; but
her influence could not preserve him from morbid dejection and
unworthy pursuits.  Book-learning and worldliness engrossed him
for a while, in spite of the recurring prick of conscience.  Even
in early youth his fancy had strayed to other women, and his
comradeship with the disreputable Forese Donati is perhaps to be
ascribed to a boyish period.  After the passing of Beatrice he
was, as he thought, unduly moved by the pity of an unnamed lady,
who soon, however, became in his mind a mere visible picture of
the object of his great passion, Philosophy.  At some time,
perhaps later, in the Casentino, he apparently became violently
enamored of a young woman to whom he addressed the most wildly
beautiful lyrics in all amatory literature; but even these poems
are not beyond the suspicion of allegorical intent.+1~

