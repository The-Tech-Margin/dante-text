	9.  Among the travelers' tales that delighted the
wonder-loving public none were better liked than those which told
of journeys to the Garden of Eden; and of these the most famous
was the ^Voyage of St. Brendan~, the narrative of an Irish monk
who sailed out into the Atlantic and after marvelous adventures
discovered the Isles of the Blest.  It is in Dante's
^Purgatorio~, and especialiy in his description of the Earthly
Paradise, that the influence of such stories is most evident.
The inaccessibility, the wall of fire, the birds, the flowers,
the streams, the eternal springtime are traditional features;
even the lovely youthful figure of Matilda, genius of the place,
has a counterpart in the ^St. Brendan.~  Some authorities put the
Garden on a remote mountain-top, others consigned it to a distant
island.  Dante combined these locations, and made his Eden the
summit of a lone peak rising sky-high from the midst of the great
ocean.  The spot where man first sinned is directly opposite
Jerusalem, where he was redeemed.+1~

	10.  In 2 Corinthians xii, St Paul declares that `he was
caught up into paradise, and heard unspeakable words, which it is
not lawful for a man to utter.'  What he saw and heard he refused
to tell, but posterity was less discreet.  Toward the end of the
4th century there appeared a Greek document, found, it was said,
in the apostle's house in Tarsus -- the ^Apocalypse of St. Paul.~
Though denounced by St. Augustine, and never accepted by the
Church, it enjoyed an immense vogue, and was turned into Syriac
and Latin.  The Latin ^Visio Sancti Pauli~ -- which tells how the
Chosen Vessel, led by an angel, visited the realms of the dead --
formed the basis of several versions in the vulgar tongues of
Europe.  But it stood by no means alone.  Visions too numerous to
tell were seen and invented for many centuries; conspicuous among
them are those described in two Irish tales, the ^Tundal~ and
^St. Patrick's Purgatory~; in Dante's own country there was
recorded the ^Visio Alperici.~ Most of these treat of the lower
world, in which Hell and Purgatory lie side by side.  A large
part of the punishments portrayed by Dante were commonplaces of
vision literature; but he avoided the extremes of coarseness and
grotesqueness, and introduced system and fitness where all had
been chaotic.  It was his happy idea, moreover, to lift Purgatory
to the earth's surface, place it far from Hell, next to the
Garden of Eden, and surround it with an atmosphere of light and
hope.  Meanwhile St. Paul's reticence had borne other fruit.  A
Neo-Platonic treatise, not earlier than the 5th century, ^On the
Celestial Hierarchy~, dealing with the heavens and the Heavenly
Intelligences, had been ascribed to Dionysius the Areopagite, the
apostle's convert in Athens, and was supposed to contain an
authentic record of revelation confided by the master to his
disciple.  Dante's classification of heavens and angels is
founded on this work.  But heathen as well as Christian could
dream dreams of the hereafter.  The ^Somnium Scipionis~ in
Cicero's ^De Re Publica~ depicts good souls rising to the stars,
and the petty earth in the centre of nine revolving spheres.  If
Homer's account of a descent into the world of the departed was
unknown in the Middle Ages, those of Ovid, Lucan, and Statius
were familiar to scholars, and Virgil's was before every
schoolboy's eyes.  Dante's Hell is full of Virgilian names;
Christian and pagan figures are strangely commingled.  This must
have seemed less incongruous to the author than to us, for in his
day the gods of classic mythology were regarded as demons, fallen
angels who had seduced mankind to worship them.  Furthermore,
Dante thought of the ancient poets as seers, who had some inkling
of the truth, and in veiled language told of things that became
fully known only through the word of God: for instance, when Ovid
sang of the Golden Age, or the battle of the giants, he was dimly
conscious of the state of man before the fall, and the revolt of
the angels against their Maker.+1~

	11.  The ^Divine Comedy~ is not only an Encyclop@@aedia,
a Journey, a Vision -- it is the Autobiography of a soul.  The
events of his external life Dante scrupulously excluded from his
works: he never mentions his parents, his children, nor, in all
probability, his wife; an apparent reference to his sister, in
the ^Vita Nuova~, is couched in the vaguest terms; only
incidentally and rarely does he afford a passing glimpse of his
material affairs.  His ^Vita Nuova~, professedly the story of his
youth, is the most baffling record ever penned.  In his opinion,
it is not meet to speak of one's self:' parlare alcuno di s@e
medesimo pare non licito,' he declares in the ^Convivio.~  This
maxim evidently does not apply, however, to the inner self,
provided that self be generalized into a type of mankind, and
provided the recounting of its experiences be helpful to other
erring souls -- `quando per ragionare di s@e grandissima utilit@a
ne segue altrui per via di dottrina.'  Like St. Paul and
@@AEneas, Dante had a mission, a vital message for humanity.  The
^Divine Comedy~ is the epic of remorse, repentance, purification,
and final uplifting.  Incidentally it depicts the depravity; of
the world and points the way to social regeneration.  For a work
of this kind Dante had a great example in the ^Confessions~ of
St. Augustine, and another, allegorically conceived, in
Boethius's ^Consolatio Philosophi@@ae.~

	12.  From the dawn of amorous poetry in Provence it was
