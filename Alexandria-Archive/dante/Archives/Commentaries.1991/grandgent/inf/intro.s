	TO form an idea of the general structure of the eighth
and ninth circles, one may think of a funnel with a shallow mouth
and a thick spout.  The upper part will represent the eighth
circle, sloping down to the edge of a central hole ('un pozzo
assai largo e profondo') at the bottom of which is the ninth
circle.  Now let one imagine this upper part, the mouth of the
funnel, horizontally corrugated so that ten deep grooves run
around it, one below the other.  These are the ditches (^bolge~
or ^valli~) in which the various types of Fraud are punished.
Furthermore, let one picture a number of strips running down the
inside of the funnel from the outer edge to the beginning of the
neck-like spokes converging upon a hub; and these strips should
be conceived, not as flat, but as undulating, arching up over the
grooves.  The strips will correspond to certain sharp ridges of
natural rock that traverse the circle, at intevals, from top to
bottom, rising into high, steep bridges over the valleys, and
resting on the intervening banks.  These ^scogli~, as they are
called, form a set of embankments on which wayfarers -- if such
there be -- may cross the circle without descending into the
ditches.  Dante gives to this circle the fantastic name of
^Malebolge~, or' Evil Pouches.' He compares the ^bolge~ and the
^scogli~ to a series of moats, with drawbridges, surrounding a
castle in concentric rings.

	In describing the double march of the lost souls in the
first ^bolgia~, Dante recalls a scene witnessed by many thousands
in Rome in the Jubilee year of 1299-1300.  The following account
is borrowed from C. E. Norton's translation of the ^Hell~, pp.
112-3: `The jubilee was instituted by Boniface VIII, who issued a
Bull granting plenary indulgence for a year from Christmas, 1299,
to all pilgrims to Rome who should spend fifteen days in the
city, visit the churches of St. Peter and St. Paul " and should
confess and repent their sins.  The throng of pilgrims from all
parts of Europe was enormous, and among other precautions for
their safety was that here alluded to, a barrier erected
lengthwise along the bridge of Sant' Angelo, in order that the
crowd going to and coming from St. Peter's might pass in opposite
directions without interference.'

	The fraudulent are divided into ten categories, and some
of these are subdivided.  In the first ^bolgia~ are betrayers of
women, who fall into two classes, panders (^ruffiani~) and
seducers.  They walk ceaselessly around the bottom of their
ditch, in two files, moving in opposite directions (like the
pilgrims on the bridge), the panders on the outer, the seducers
on the inner side.  Horned devils scourge them with whips as they
pass.  Thus are sinners of this sort constantly goaded on by
their mean passions.  In Malebolge demons abound -- not mere
guardians, similar to those of the classic underworld but
malignant, tormenting fiends.  There are no such spirits,
however, in the second valley, where the punishment is simply
nauseous filth and stench, a symbol of the unclean life of
flatterers.  To portray their abject vileness, the author deems
no terms too gross.

	THE sin punished in the third ^bolgia~ is simony, the use
of ecclesiastical office for private gain.  Dante classifies it
as Fraud but it was generally regarded as an offence against the
Holy Ghost.  It derives its name from the Simon Magus of Acts
viii, 9-24: `But there was a certain man, called Simon, which
beforetime in the same city used sorcery....And when Simon saw
that through laying on of the apostles' hands the Holy Ghost was
given, he offered them money, saying, Give me also this
power....But Peter said unto him, Thy money perish with thee,
because thou hast thought that the gift of God may be purchased
with money.'  Inasmuch as this offence is one of perversion, it
is symbolized by the culprit being turned upside down; and as the
perverted trust is a holy one God's anger falls upon the
incumbent in the shape of fire, which, owing to his distorted
attitude, burns not his head but his feet.  The bottom and sides
of this ditch are perforated with little round holes, from each
of which project the writhing feet and ankles of a sinner.
Somewhat similarly, in the ^Visio Alberici~, XI, simonists are
confined in a fiery well.  Dante's flame, however, merely plays
upon the surface of the soles, from which it seems to suck its
food.  This grotesque penalty is inflicted even upon mercenary
Popes, all of whom appear to be sunk in one hole.  The latest
comer is at the top, `planted like a stake,' with his feet in the
air; the others, with each new arrival, are pushed further and
further down, to flatten in the crevices of the rock.  Dante and
Virgil descend into this ^bolgia~ to converse with Pope Nicholas
III.  To him our traveler addresses, with the approval of Reason,
though with some misgiving, a stinging rebuke, made all the more
impressive by Dante's professed reluctance to show disrespect to
a former wearer of the papal mantle.

	The burrows of the simonists are compared to the pits for
baptizers in the Baptistery of Florence, which Dante lovingly
calls `his beautiful San Giovanni-' Here all the new-born
children of the city were baptized by immersion either on the
Saturday before Easter or on June 24th, the day of St. John the
Baptist.  It would seem that to protect the priests from the
throng they were stationed in four cylindrical holes in the
waist-high marble wall that surrounded the large circular font in
the middle of the church Dante takes this occasion to declare
that he once broke down one of these receptacles to save some one
drowning inside and adds:

     `E questo sia suggel ch' ogni uomo sganni,'

'And let this be a seal -- i.e., a document an authoritative
final statement -- that shall undeceive every one.'  Apparently
he introduces the subject in order to put a stop to false rumors
concerning his act.  We really know nothing about the curious
incident, although Benvenuto da Imola, one of the early
commentators relates a circumstantial story of boys playing on
the wall and one of them falling into one of the holes, from
which he could not be extricated until rescued by Dante.  Many
such tales were quickly invented to explain obscure references in
