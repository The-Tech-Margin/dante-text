turned against their Creator.

	The hardest, coldest heart is that of the traitor; from
it all the warmth of human affection has been banished.  The
symbol of treachery is ice; and at the bottom of the well, buried
in the circular plain formed by the freezing of Cocytus, are the
sinners of the ninth and last circle.  In the middle of the
plain, at the centre of the earth, is Lucifer or Satan, called
Dis by the ancients.  Ice is used as a means of punishment in
other visions of Hell, and in the ^Visio Alberici~ we find a
graded immersion in ice; but nowhere else has it the significance
that our poem gives it.  Dante's traitors have no desire to be
remembered on earth: the best they can hope is to be forgotten.
Their evil disposition is unchanged, and even in Hell they are
eager to betray one another.  The cold, cruel spirit that
pervades their congregation communicates itself to the beholder;
the mere thought of their odious crimes arouses an instinct of
vindictiveness.  Scorn and hatred possess Dante as he
contemplates them, and he feels impelled to pay them in their own
coin.

	The traitors fall into four divisions, according to the
relation between themselves and their victims.  They are arranged
in the round plain in four concentric circles; taking them in
order, from ^Tolom@ea~ ('Ptolemea'), ^Giudecca~ ('Judecca ').
They are distinguished only by the position of the sinners in the
ice: in the first three, the souls are embedded up to their
heads; in the last, Giudecca, they are entirely covered.  In
Caina, the heads are bowed down; in Antenora they are apparently
erect; in Tolomea they are thrown back.  Caina contains traitors
to kindred, Antenora traitors to country or party, Tolomea
traitors to guests, Giudecca traitors to benefactors (or,
according to Del Lungo, to constituted authorities).  In all
cases the treachery involves murder.  Caina and Giudecca are
named respectively for Cain and Judas.  Antenora derives its
title from the Trojan Antenor, who bears an excellent character
in the ^Iliad~; in the later narratives, however, ascribed to
Dares and Dictys, and regarded in the Middle Ages as an authentic
account, he figures as the arch-traitor who hands over the
Palladium to the Greeks (cf. Servius's commentary on ^Aen.~, I,
242).  Tolomea is so called after the Ptolemy of I Macc. xvi,
11-6, a captain of Jericho, who murdered his father-in-law and
two brothers-in-law at a banquet to which he had invited them.
In this canto occurs an episode second only to that of Francesca
da Rimini in its appeal to popular sympathy.  It is in Antenora
that Dante hears from Count Ugolino the frightful story of his
death.  Here, as in the case of the `injured souls' of Francesca
and her lover, the poet is stirred to the depths by the wrong
done on earth to the lost sinner.  Francesca's fate moves him to
an agony of pity not unmixed with indignation; that of Ugolino
and his children kindles in him even more wrath than compassion.
In the two narratives we find the same exclusion of all detail
that might blur the one overwhelming impression to be produced
upon the reader; in both, the same concentration on that part of
the experience to which no human heart can be indifferent.  As
Francesca's guilty love follows her to Hell and binds her forever
to the partner of her sin, so Ugolino is coupIed to the object of
his just hate, on whom he wreaks eternal vengeance.

	Ugolino della Gherardesca, count of Donoratico, belonged
to an old and powerful family and held vast estates in western
Tuscany.  Inasmuch as he was vicar, in Sardinia, of King Enzo
(son of Frederick II) and married his eldest son to Enzo's
daughter, he must have been originally of the Imperial faction;
but as early as 1275 he for some reason allied himself to the
Tuscan Guelfs against Pisa, which was then Ghibelline.  It was
doubtless for this political treason that Dante condemned him to
Antenora.  His party was successful, and he secured readmission
to the city.  In fact, after 1284 he governed Pisa for the
Guelfs, at first alone, later in company with his grandson, Nino
Visconti, who appears in ^Purg.~ VIII, 53.  But the Pisan
Ghibellines, led by the turbulent and intriguing Archbishop
Ruggieri degli Ubaldini, -- who in 1288 plotted to surrender his
city to the Genoese, and then broke faith with them, -- revolted
in that same year against the Guelf control.  Ugolino was absent
at the time, and Nino was driven from the town.  In June Ugolino
was invited to confer with the Ghibelline leaders.  He returned
to Pisa and held parley with the archbishop.  Suddenly an alarm
was given, and the Ghibellines, with a frenzied mob led by
Ruggieri, attacked their opponents, whom they finally besieged in
the city hall and, after severe fighting, captured.  Ugolino was
incarcerated with two of his sons, Gaddo and Uguccione, and two
grandsons, Ugolino or Nino (called `il Brigata ') and Anselmuccio
the children of Guelfo, his firstborn.  After some months the
door of their jail was nailed up, and they were left to starve.
When their bodies were taken out, several days after their death,
they were found to be badly rat-bitten; and a couple of old
chroniclers declare that the unfortunate prisoners had eaten one
another's flesh, or, more specifically, their hands and arms.  It
was perhaps a desire to explain the mutilation in a more fitting
way that led Dante to conceive of Ugolino, in his intolerable
anguish, as gnawing his own hands.  Nevertheless some modern
commentators have imagined that Dante intended, in the last verse
of Ugolino's speech, to imply that the bereaved father was driven
to cannibalism.

	Treachery to a traitor was thought to be not only
permissible, but meritorious; and this belief is illustrated by
Dante's treatment of one of the wretches in Tolomea.  To be rude
to him, he avers, was the part of true courtesy.  In this
division of the ninth circle are those who assassinated their own
guests.  Such betrayal as this severs all social bonds and puts
the betrayer outside the pale of humanity.  Dante expresses this
idea allegorically by a startling device, which at the same time
enables him to place in his lower world two or three heinous
offenders still alive in 1300.  As soon as this crime is
committed, -- so we are informed, -- the sinner's soul descends
to Hell, leaving the body, which, however, seems to remain alive,
being occupied by a devil during the remainder of its natural
term of existence.  Thus it is written of Judas, at the Last
Supper: `after the sop Satan entered into him' (John xiii, 27).
In Ps. lv, 15 it is said of treacherous friends, `let them
descend quick (i.e., alive) into hell.'

