	As the poets cross the broad bank that intervenes between
the tenth ^bolgia~ and the great central pit of Hell, Dante sees
looming through the dusk, like the towers of a city, the forms of
giants, visible from the waist up all around the mouth of the
well.  As far as we can judge, they are from sixty to eighty feet
in total height.  But the apparently precise dimensions given are
vague to us, because of the variability of standards: one
creature measures from neck to midde thirty palms, probably
something like twenty feet; another, five ells, perhaps some
thirty feet.  One of these monsters, Antaeus, picks up the
travelers and sets them on the ice at the bottom of the hole.
Dante speaks as if he did so without quitting his post; nothing
but the giant's stoop is described, as he lifts them up, and his
straightening when he has put them down.  We are told, however,
that, unlike his mates, he is not bound.  Now, inasmuch as the
last ^bolgia~ is eleven miles in circumference, and inasmuch as
the poets walk for some time over the plain of ice before seeing,
in the midde of it, the enormous figure of Lucifer, we must think
of this pit as at least a mile wide; and since it is described in
XVIII, 5, as `un pozzo assai largo e ^profondo,'~ it can hardly
be less than twice as deep as it is broad.  It is obvious, then,
that the giants, the upper half of whose bodies appears above the
edge, cannot be standing on the bottom: their feet must rest on a
ledge or shelf near the top of the wall; in fact, in XXXII, 16-7,
we are told that when Dante and Virgil were on the bottom of the
`pozzo scuro,' they were,' sotto i pi@e del gigante, e assai pi@u
bassi.'  Antaeus, therefore, carrying the poets, must have left
his place and climbed down the precipice; but of this descent our
author, for reasons of his own, says not a word.  Perhaps he
conceived of himself as so terrified hat he could recall nothing
of the adventure but its awful beginning and end.  It is likely,
too, that he preferred to leave a gap for the reason set forth in
the argument to Canto V.

	We do not know how many giants there are in all.  Those
named are Nimrod, Ephialtes, Briareus, and Antaeus -- all, except
the last damned for their presumption in attempting to scale
Heaven.  Ephialtes and Briareus were among the most active at
Phlegra when the giants piled mountain upon mountain, and
threatened the Gods.  This combat is mentioned by Ovid (^Met.~,
I, 151-5) Statius (^Theb.~, II, 595-6), and Lucan (^Phars.~, IV,
593-7); and the two latter authorities speak of Briareus.
Ephialtes is not named by any of the ancient poets that Dante
seems to have known, but he is to be found in Servius's
commentary on the ^Georgics~ I 180.  Antaeus, so Lucan tells us
(^Phars.~, IV, 597), did not participate in the fight, and
therefore he is unbound in Dante's Hell; in ^Phars.~ IV, 593 ff.,
his misdeeds and his defeat by Hercules are related at length.
The `fable' of the battle of Phlegra doubtless represented to
Dante merely the old pagan sages' idea of the revolt of the
angels; the giants are stricken down by `il sommo Giove,' the
supreme Power.  The Biblical Nimrod, then, is not out of place
among them.  The following particulars are culled from Gen. x,
8-10, and xi, 2-9, also vi, 4: `And Cush begat Nimrod: he began
to be a mighty one in the earth.  He was a mighty hunter before
the Lord....And the beginning of his kingdom was Babel, and
Erech, and Accad, in the land of Shinar... .And it came to pass
...that they found a plain in the land of Shinar; and they dwelt
there. ...And they said, Go to, let us build us a city and a
tower, whose top may reach unto heaven....So the Lord scattered
them abroad from thence upon the face of all the earth: and they
left off to build the city.  Therefore is the name of it called
Babel.'  Nimrod, then, was held responsible for the audacious
enterprise; and as early as Orosius and St. Augustine he was
regarded by Christians as a giant.  According to ^De Vulgari
Eloquentia~, I, vii, 24-31, it was' sub persuasione gigantis'
that man presumed to surpass his maker.  In the confusion of
tongues Dante's Nimrod has suffered more than his misguided
fellows, for he speaks a language understood by no one else and
can comprehend no other soul.  His mind, too, is as dazed as his
words are senseless.  He can vent his feelings only by blowing
the big horn with which, as a `mighty hunter,' he is equipped.

	The manifold crimes of the Lower Hell are due to pride
and envy, and these sins are personified in Satan and the giants.
Embedded in the central point of his kingdom, the arch-sinner,
surrounded by a ring of fellow-rebels, holds his eternal court.
The spirits that thought to rise so high are sunken at the bottom
of the universe; their monstrous forms are fixed and impotent
forevermore.  This rather than the Circle of Violence, is
artistically their fit place; and here, no doubt, Dante would
have put them, even if Virgil had not pointed the way (^Aen.~,
VI, 580-1):

     `Hic genus antiquum terrae, Titania pubes,
     Fulmine dejecti, fundo volvuntur in imo.'

	For the ^pozzo~, cf. G. Agnelli in ^Giorn. dant.~, VIII,
