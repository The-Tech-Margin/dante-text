PROEMIO ALL' ILLUSTRISSIMA ET ECCELLENTISSIMA REPUBLICA FIORENTINA DI
CHRISTOFORO LANDINO.

LLL  ANCORA, che non sia specie alcuna d'Eccellenti Scrittori,
Illustrissimi Signori nostri, laquale la humana generatione non
debba sommamente lodare, attento che essi habbiano con ogni loro
industria, e fatica messo in comune cioche essi hanno saputo, non
solo per giovare a lor medesimi, & suoi coetanei, ma anco a tutti
coloro, che verranno ne futuri secoli: nondimeno poiche spesse
volte ho meco medesimo discorso una quasi infinita copia di
coloro, iquali hanno lasciato memoria di loro, @o nella lingua
Greca, @o nella Latina, non trovo niuno, che si possa agguagliare
a quel Poeta: ilqual certamente, & per profondit@a di stile,
possano pur dimostrarci le cose grandi alla vita nostra, & alla
salute molto appartenenti, ma ancora sotto maraviglioso velame
ricoprendole, l'adorni di modo, che oltre ad una incomparabile
utilit@a, laqual si trahe di tanta, e cosi varia dottrina,
