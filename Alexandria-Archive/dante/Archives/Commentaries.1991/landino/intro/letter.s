AL SERENISSIMO PRENCIPE, IL SIGNOR GUGLIELMO GONZAGA, Duca di Mantova, &
Monferrato.

SI come @e cosa propria, & conveniente, che le pi@u ricche gemme
ne i piu gran Tesori risplendano, & le pi@u vive glorie fra i
pi@u chiari, & gloriosi nomi risuonino; cos@i havendosi di nuovo
con ogni perfettione ad illucidar la bellezza, & @a ravivare lo
splendore del vero preggio della lingua Toscana (che il presente
Volume) era ben degno, & ragionevole di consacrarlo @a persona
tale, che (da quello ricevendo fama) rendesse @a lui gloria, & lo
facesse co 'l divino suo nome pi@u illustre, & preggiato.  Ilche
non s@i tosto ricominciai meco stesso @a pensare, che mi si
rappresent@o nell'Idea di doverlo mandare @a V.  Serenit@a,
perche si come questo per gli alti suoi, concetti viver @a per
tutti i secoli futuri, cos@i V. Serenit@a per l'eccellenti sue
parti, sar@a sempre ammirabile, & @a tutti i posteri veneranda.
Et tanto pi@u mi mossi @a farlo, perche venivo in uno istesso
tempo con ci@o @a sodisfare, parte dell'intenso desiderio, che ho
sempre havuto di farmi conoscere da Vostra Serenit@a, per suo
fedelissimo.  E chi sia, chi non gli desideri ogni bene?  havendo
origine da quel cos@i glorioso Ceppo, dalquale sono continuamente
usciti, & dati al mondo invittissimi Prencipi, & Heroi
famosissimi? de' quali (per non dilatarmi in quello, che @a tutti
@e chiarissimo) nominer@o solamente il felicissimo Federico suo
Progenitore, ilquale, allhora che accett@o in Mantova Paolo Terzo
Pontefice, & Carlo Quinto Imperatore (di memoria sempiterna)
dimostr@o con i Trionfi, & spese eccessive per ci@o fatte la
magnanimit@a dell'animo suo, & col rettamente governare, &
reggere il popolo a se sottoposto (onde merit@o di esser in ogni
suo negocio adoperato, come consigliero dal sudetto invittissimo
Imperatore) diede saggio del maturo, & perfetto giudicio suo.
L'Illustrissimo suo Zio parimente per le sue eccelse operationi,
si fe degno, che dalla Maest@a Catolica fosse creato in tutte le
sue imprese memorabili, Capitano Generale.  Della grandezza poi,
meriti, & special doti dell'animo di V. Serenit@a, che dir@o io?
se non ch'ella @e il vero Tesoro dela virt@u, & proprio obietto
della gloria, & che per ci@o con giubilo, & allegrezza universale
@e stata giustamente (di Marchese ch'era di Monferrato) assonto
al grado di Serenissimo Duca.  Ma di tutte l'altre @e maggiore la
gloria dell'esser V. Serenit@a Auttore dell'Illustrissimo
Prencipe suo Primogenito, poiche si scorgono di gi@a in lui a
pullulare tutte le doti, & virt@u unite de' suoi Illustrissimi
Antecessori, con somma contentezza non pur de' sudditi suoi, ma
de gli esterni ancora.  A voi donque Serenissimo Duca, & al
Trofeo della vostra gloria, dedicata humilmente porgo la presente
opera, laqual come cortesissimo si degner@a d'accettare, &
reputarla quasi principio, & caparra del grand'animo, ch'io ho di
servirla, & di essaltare il divino suo nome, si come di Prencipe,
non pur di mondana dignit@a, ma d'interno valore, & prudenza al
par di chiunque si sia dotato, & che tutto il d@i si vede ir
dispensando le gratie sue, non solamente a grandezza, e utilit@a
de' popoli suoi, ma anco per beneficio commune; Laqual' opera
Christiana si degni colui, che di niente cre@o il tutto, far che
per longo tempo si possi continuare da V. Serenit@a; ilche sia
con il concederle longa, & felice vita.

     Di Vinegia alli 10. Giungo 1578.
     Di V. Serenit@a
     Humilissimo Servitore
     Gio. Ant. Rampazetto.

