principio del c. II <Gi@a era il sole all'orizzonte giunto>.  Il
secondo nel c. IX <Nell'ora che comincia i tristi lai>.  Il terzo al
principio del canto XIX <Nell'ora cher non pu@o il calor diurno>.  Il
quarto quasi al termine del canto XXVII. <Vedi il sole che infronte
ti riluce>.  Al mezzod@i di questo quarto giorno giunge alla fonte
di Lete ed Euno@e.  Il rimanente del giorno si suppone passato
nell'immersione di Euno@e.  La cantica terza comincia col risorger
del sole: <Fatto avea di l@a mane e di qua sera>.  Onde si suppone
che il resto della notte siasi passata da Dante e Beatrice e forse
in compagnia di Stazio e Matelda con le sette donne, nel Paradiso
Terrestre.
