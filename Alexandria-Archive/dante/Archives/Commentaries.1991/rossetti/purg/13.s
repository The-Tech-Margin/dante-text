	Scalda il cuor dell'uomo; illuminagli l'intelletto, e
l'amore e la ragione ne scacceranno l'invidia.  Senza il sol
della ragione l'uomo erra nelle tenebre dell'errore, spinto da
sensi come un bruto.  ^Ut quemadmodum si sol non sit per reliqua
