PURGATORIO I

CHIOSE MARGINALI I.

CHIOSE SINCRONE

	Hec secunda pars totius comedie Dantis que purgatorium
appillatur dividitur in tres partes principales in prima quarum
auctor tractat de quodam exordinario loco in quo anime illorum
qui distulirunt venire ad penitentiam et confessionem usque ad
finem vite et tunc contriti mortui sunt extra verum purgatorium
per eum fingitur expectare tanto tempore quanto vixerunt in hoc
mundo in tali mora et negligentia ut dicam singulariter de
talibus in suis locis per quinque partes distinctis et hoc durat
a principio hujus secunde partis usque ad decimum ejus capitulum.
Ibi incipit secunda in qua auctor de vero tali purgatorio tractat
et VII. ejus circulis distinctis secundum septem mortalia peccata
purganda circa operis satisfactionem usque ad XXVII. capitulum.
Ibi tertia pars in qua dicit et tractat de paradiso terrestri et
durat usque ad finem purgatorii et dicitur purgatorium fictum et
venrum fictum dicitur in hoc mundo cum homines discedunt a vitiis
et tendunt ad virtutes verun cum anima exit de hoc seculo sine
peccato mortale non tamen sine veniali et vadit se ad purgandum
in alio mundo.

	Auctor in hac secunda parte sue comedie intendit tractare
de statu animarum separatarum a suis corporibus liberarum tamen a
perpetua dampnatione non tamem absolutarum ab aliquibus penis
propter culpas per eas commissas a quibas antequam possint ire ad
vitam eternam oportet purgari ad hoc ut nullum malum remaneat
inpunitum. circa quod est notandum quod postest commicti peccatum
dupliciter. sc. mortale et veniale quod est quod quando homo
comictit se arumpit a virtute divina et in ea non sperat nec
credit quod possit nec velit ei parcere et sic in totum se
separat a virtutibus et committentibus taliter peccatum data est
eis pena infernalis.  Aliud peccatum est veniale quod est quod
quando homo commictit ei tamen remanet aliquia ordo virtuosus et
remorsio conscientieJquibus data est pena purgatorii ad tempus et
quia presens comedia tractat solummodo de animabus liberis ab
eternali perditione ideo dicit etiam de penis non eternalibus sed
ad tempus ut in processu apparebit et ut melius intelligatur
postquam auctor exivit de inferno ad revidendum. hujus mundi
immaginatur vel fingit se esse in quadam ampla planitie comfini
cuidam marine.  Item fingit in hac planitie non esse arbores nec
plantas aliquas nisi vincos palustres.  Item fingit esse in illo
emisperio quemdam maximum mum montem ascendentem usque ad ethera.
Item fingit istum montem esse circa medium ipsius circumdatum
muris.  Item fingit dictum montem esse circulatum gradibus ut ita
ad apicem ipsius gradibus ascendatur quemadmodum in centrum
inferni per gradus discenditur.  Item pon^  quod in predicta
planitie usque ad muros extendente purgentur anime illorum qui
fuerunt negligentes in penitendo.

