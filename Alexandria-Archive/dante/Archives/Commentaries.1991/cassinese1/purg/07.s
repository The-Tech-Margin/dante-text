PURGATORIO VII

CHIOSE MARGINALI I.

CHIOSE SINCRONE

	|53.  questa riga.~  Spiritualiter hoc potest intelligi.
videlicet. ut homo in homo in hoc mundo ascendens ad altitudinem
humane felicitatis pro qua hic mons accipitur non debet hic velle
facere si non vult deviare nise sole.  idest. lumine sibi gratie
et caritatis radiante.

	|72.  il lembo.~ idest. durat iste callis ultra quam ad
medium dicti montis. et postmodum deficit.

	|73.  biacca.~ quia existens in hoc loco quamquam
distulerint venire ad penitentiam usque ad finem dierum suorum
tamen quia probitas eorum memoranda viriditate fame eorum in hoc
mundo viget stant sic in herbis et floribus coloratis ut hic
dicitur.

	|94.  Rodolfo.~ qui post electionem de eo factam
neglessit venire in ytaliam et rome coronari de quo dixi in
proximo precedenti capitulo qui fuit primus qui recepto imperio
recusaverit in ytaliam venire.

	|97.  Laitro.~  Iste fuit octacherus rex bohemie per quam
bohemiam labitur aqua que vocatur molta que molta postmodum
intrat albiam que albia dictam Moltam portat in mari in quo regno
successit in probitate et virtutibus. de qua albia ait Lucanus in
|II.~  Fundit ab extremo flavos aquilone svevos albis etc.

	|103.  quel nasetto.~  Iste rex phylippus filius lodoyci
regis francie canonizati olim in santum secundum quod fertur
habuit jparvum nasum et ideo vocabatur nasettus et ivit cum
potentissimo exercitu contra regem Aragonem tandem ibi ab
Aragonensi rege fuit positus in conflictu cum tota sua gente et
mortuus apud terram perpignani districtus sive territorii
catalonie et hoc vult tangere hic auctor dicendo dictum phyippum
mortuum fore fugiendo et disflorando lilium signum ejus armature.

	|104.  cun colui.~  Supradictus rex phulippus habuit
quemdam pulcerimum filium nomine phylippus habuit quemdam
pulcerimum filium nomine phylippum pulcredutudinem suam vocabatur
bellus et successit in regno francie et habuit in uxorem filiam
tebaldi regis navarre qui bellus quot et quanta enormia fecerit
dicam infra in XX. capitulo ideo fingit auctor hoc duos ita
contristari. scilicet. tebaldum et regem phylippum patrem dicti
belli.

	|112.  Quel.~  Iste fuit domnus jpetrus rex aragonum
virtute et probitate clarissimus qui erat magnus et membrutus et
cantat cum carolo rege apulie qui habuit nasum magnum et hauit
tres filios. scilicet.  Anfusium sibi similem in virtute si
vivisset nam in juvenili etate mortuus est et iste est ille
juvenculus qui sedet post dictum ejus patrem.

	|126.  si dole.~ quia reliquid filios ingnominiosos et
viles respectu ipsius patris et dicit auctor quod illud evenit
dicto carolo de suis filiis quod dompno petro de suis.

	|132.  Guigliemo Marchese.~  Iste guillelmus Marchio
montisferrati qui habita longa guerra cum illis de Alexandria
demum captus est ab eis et in eorum carceribus mortuus. ex quo
homines de monteferrato predicto et ille de canevese teritorii
dicti Marchionis adhuc plorant. idest. dolent de hoc
reminiscendo.

