|CANTO 11~

	|1.  nei cieli stai.~ et sic non debemus orare deos
terrenos -- |2. Non circumscripto.~ dicitur enim aliquid
circumscriptum quando potest definitione assignari in loco ut sic
sit in illo quod non alibi. quod de deo dici non potest nam bene
habitat in celis propter veros amores quos ibi habet non tamen
circumscriptus sed ut omnia circumscribens. ut dicit infra in
paradiso capitulo XIIII.  Nam scribitur ysaye celum sedes mea
terra autem scabellum pedum meorum et sic deus est ubiquc et non
potest dici esse in uno loco solo necessario sed ubique
actionibus et non loco.

	|4.  Laudato sia.~ i. sancitificetur nomen tuum.

	|7.  Vegna.~ i. adveniat regnum tuum -- ^tuo regno.~ i.
nos filii tui petimus regnum et hereditatem paternam nobis
promissam per ipsum ut patrem -- |8. da noi.~ subaudi etiam cum
toto nostro ingenio quod est.  idest. nisi ipsa gratia predicta
ex se veniat.

	|10.  Come.~ fiat voluntas tua sicut in celo et in terra
-- ^angeli tuoi.~ istam quartam particulam. augustinus de sermone
dei volens exponere ait per celum hic angeli intelliguntur per
terram sancti homines et est dicere sicut in angelis voluntas dei
fit ut eo perfruantur ita fiat hic in terra a sanctis hominibus.

	|13.  Da oggi.~ panem nostrum cotidianum da nobis hodie
-- |15. safanna.~ ut contingit ebreis in deserto que gratia dei
in manna pluente eis ut exodi XV. capitulo antecederunt.

	|16.  E come.~ dimicte nobis debita nostra sicut etc.

	|19.  Nostra.~ et ne nos in tentationem etc. -- ^sadona.~
est notandum quod deus non ducit in hoc mundo in temptationem nos
sed patitur nos teptari tamen hic petitur ne deserti divino
auxilio alicui temptationi vel consentiamus decepti vel sibi
credamus afflicti.

	|27.  si sogna.~ quod dicitur incobus de quo virgilius
dicit in XII.  comparative ac velud in somnis occulos ubi
languida pressit.

	|53.  la cervice.~ in qua parte corporis superbia plus
discernitur quam in aliqua alia parte quando homo cum capite
elevato incedit et ideo hic deprimitur.

	|58.  Io fui.~  Modo auctor volens ostendere quomodo
vitium superbie ut genus dividitur in tres species. scilicet.
arrogantiam appetitum excellentie et presumptionem fingit inter
alios reperisse hos tres spiritus et primo istum domnum Umbertum
comitem de sancta flore de Maritima valde superbum in prima
specie dicitur superbus quasi qui proprie appetit esse super
alios.

	|67.  Io sono.~ iste fuit dominus Umbertus comes de
sancta flore de Maritima qui propter suam arrogantiam fuit
mortuus in campagnatico terra maritime ab illis de dicta terra.

	|79.  lui.~ iste fuit domnus odoringius de eugubio
magister optimus miniator librorum qui domnus odorisius cessit
postea in dicta arte dicto magistro franco de bononia --
^Oderisi.~ optimus miniator librorum qui expertus fuit in dicta
arte omnes alios magistros excedere et in isto est secunda
species superbie que dicitur appetitus excellentie.

	|97.  Cosi atolto.~ videlicet. domnus Guido de
Guinizellis de bononia optimus rimator in lingua materna cessit
domno Guidoni de cavalcantibus de florentia qui eum in fama dicte
locutionis superavit.

	|108.  piu tardo.~ dicitur enim quod octava spera movetur
tardior aliqua alia nam movetur in XXXVI. millibus annis.

	|109.  colui.~ qui fuit domnus provenzanus de salvanis de
senis qui dominatus fuit dicte civitati senarum tempore quo
florentini fuerunt positi in conflictu penes montem apertum a
senensibus anno Domini MCCLX.  ut dixi ibi in inferni capitulo X.
et fuit iste domnus provenzanus in tertia specie superbie que
dicitur presumptio.

	|137.  Carlo.~ regis apulie qui fuit incarceratus quando
Corradinus fuit debellatus a dicto Carulo.

	|141.  chiosarlo.~ quia expellent te et ibis per mundum
mendicando quod valde grave est.

	|142.  li tolse.~ videlicet. quod nonstetit extra
purgatorium.

