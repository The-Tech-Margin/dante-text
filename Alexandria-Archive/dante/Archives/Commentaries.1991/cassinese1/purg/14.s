<Purgatorio> Canto 14
Chiose Marginali
I.
Chiose Sincrone

	|6. parli a colo.~  Ait ysiderus. ethymologicon II. omnis
oratio componitur verbis. scilicet. coma. colo. et periodo coma
particula est sententie colus membrum periodus ambitus seu
circuitus fit autem ex conjunctione verborum coma ex comate colus
ex colo periodus coma est juncture finitio colus membrum quod
intellectum et sensum prestat sed adhuc pendet oratio periodus
est extrema sententie clausula quod quidam sic exemplificant et
putant disponens deus humanum genus de manibus eripere. inimici.
ecce coma per Angelum magni consilii filium de virgine statuit
incarnari ecce colus qui sui sanguinis. effuxione. nos ad gloriam
revocavit. et ecce periodus ita quod nil aluid vult auctor dicere
de colo nisi quod eum interrogat ita dulciter ut respondeat eum
ad colum. idest.  quod tantum respondeat auctor eis quod animus
eorum remaneat in quiete et non in suspenso licet etiam post
colum aliqua circuentia possent dici.

	|7. duo spiriti.~ sc. domnus Guido del duca de
bretenorio. et domnus Ra[?n]erius de calbulo de romandiola.

	|17. fal[?t]erona.~ alpes quedam in casentino in
territorio comitum Guidonum de casentino qui dicuntur comites de
porciano.

	|31. si pregno.~ quia multis aliis montibus alligatur
dictus appenninus et ideo vocat eum pregnantem idest. grossum et
amplum propter annexionem aliorum montium -- |32. monte.~ se.
appennini. de quo dixi supra |capitulo 16.~

	|42.  Cyrce.~ que suis[?] poculis incantatis convertit
sotios Ulixis in diversas feras ut plene dixi supra in |inferno
capitulo 26.~

	|43. brutti.~ qui sunt comites Guidones de Casentino qui
nominantur comites de porciano qui ratione eorum libidinose et
immunde vite merito possunt vocari porci.

	|46.  Botoli.~ sc.  Aretinos. qui ratione procacitatis et
rabide eorum audacie sine Magno posse. dici possunt Botoli qui
grippi in grammatica appellantur et sunt canes parvi et latraces.

	|50. lupi.~ sc. florentinos qui ratione eorum
insatiabilis cupiditatis quasi lupi dici possunt.

	|53. volpi.~ sc. pisanos qui eorum astutia et sagacitate
dici possunt quasi vulpes.

	|58. nepote.~ qui fuit domnus Fulcerius de calbulo nepos
dicit domni ranerii electus potestas florentie qui trucidavit
multos florentinos sub pretextu partis blancorum expulsorum tunc
de dicta civitate.

	|87. consorte.~ et participem de quo aperitus in sequenti
capitulo dicam.

	|94. ripieno.~ quasi dicat quod intra istos terminos
inter quos est romandiola solebant esse homines veridici et
solatiosi. hodie secus quia omnes intra istos terminos habitantes
sunt mendaces invidiosi et iracundi et vitiosi.

	|97. lytio.~ de valbona de cesena.

	|100. fabbro.~ iste fuit domnus faber de lambertaccis de
bononia -- |gentil.~ quasi dicat fuit nobilis virtutibus et animo
non tamen sanguine.

	|118. pagani.~ de qua casa est domnus Maghinardus de
susinana ultimus de domo sua dicta pagani quem vocat demonem
ratione sue astutie et sagacitatis.

	|133.  Anciderami.~  Post hec auctor volens tangere que
debent homines in hoc mundo refrenare. ne in hoc vitium invidie
incidant et qui in eo suut mundentur ab eo viso pernicioso ejus
effectu dicit exemplum cayni qui postquam invidia occidit fratrem
suum Abel. ibat dicendo ut legitur genesi capitulo IIII.
quicumque invenerit me occidet me ut dicit hic textus ad
significandum quod bona et mala proximi occidunt invidum bona
affligendo mala vero dando et causam letandi et occaxionem
peccandi.  secundo per exemplum agluari converse in saxum ut
scribit ovidius in secundo. videlicet. cecrops. rex atenarum.
tres habuit filias. scilicet.  hanc aglaurum. herseu. et
pandrasen. que tres sorores tres cameras habebant contiguas et
media erat dicte herse ad quam hersen volens semel Mercurius ire
ut ejus procus ad jacendum secum dicta Aglauros invidia dicte
ejus sororis opposuit se in limine camere dicte ejus sororis
dicto Mercurio unde ibi ab eo conversa est in saxum ubi Mercurius
pro facundia accipitur que facit invidium quasi insensatum ut
saxum tali virtute eloquentie invidendo que due vos. sc.
|Ancideraimi ec.~ et |io sono ec.~ dicit auctor quod deberent
esse [?|]circa talle vitium invidie camum nobis.  idest. frenum
nobis.

