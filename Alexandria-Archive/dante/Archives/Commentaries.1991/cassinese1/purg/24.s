<Purgatorio> CANTO 24
Chiose Marginali
1.
Chiose Sincrone

	Continuat se auctor ad proxime dica.

	|7. mio sermone.~  Ad illum quo dixit proxime supra.  |e
questaliro e quel ombra percui scosse~ etc.

	|13. bella a buona.~ i. que non habuit parem in bonitate
et pulcritudine -- |15. corona.~ que dicitur aureola debita
virginibus in celo nam ipsa virgo mortua est quamquam nupserit in
hoc mundo et ducta fuerit ad virum tamen mortua est intacta nam
dum per vim esset extracta de monasterio. ut dicit auctor infra
in Paradiso capitulo III.[ -- ?] a domno Cursio de donatis de
florentia ejus fratre et nupta fuisset et tradita domno Roselino
delatosa implorata et obtenta gratia ab eo suo viro ut ne illo
primo sero non concuberet secum illa nocte superveniente febre
infirmata. miraculose est ad mortem et in brevi mortua est virgo.

	|19.  Buonag[ran?]ta.~  Oribisani de lucca tempore
auctoris. inventor maximus in materna lingua.

	|24.  Bonifatio.~  Filium dicti domni ubaldini olim
Archiepiscopi Ravenatis cujus pastorale frustrum babet in
summitate quoddam signum ad modum unius Rocchi et hoc est quod
dicit de rocco.

	|38. la ovel.~ i. in gutture. sive gula in quo membro est
gulosorum excessus.

	|42. tuo parler.~  Iste bonagiunta murmurando prenumptiat
auctori exilium suum et quomodo sic exul veniet luce et ibi in
quadam juvenem dominam precabitur.

	|45. la riprenda.~ quasi diceret quocumque modo ipsa
civitas regarguatur de suis vitiis ipsa tamen tibi placebit.
propter istam dominam.

	|49.  Ma di.~  Hic auctor volens ostendere quomodo
quilibet volens scribere poetice latine vel materne debet primo
moveri non simpliciter a seipso sed ab illa spirituaali
instigatione interius nos ad talia incitante quam Hieronimus in
prohemio bibie vocat energiam.  Statius in principio thebay.
vocat destrum.  Gualfredus in ejus poetria intrinsecam lineam
cordis. auctor vero hic pro amore quodam ejus interiori eum
accipit secundum cujus subjestionem dicit hic quomodo ejus
ingenium extra scribendo significat et promit.  Modo quia iste
bonagiunta et frater Guitonus de Aritio. et Jacobus notarius de
tol[e?]ntino antiqui dictatores in lingua materua non ita
scripserunt sed magis a casu et suo motu et improvise contra
poeticam doctrinam traditam per Gualfredum. ut si quis habet
fundare domum non currat ad actum Impetuosa manus et per alios
multos poetas. hic ut dicit textus dicendo novum stilum auctoris
et aliorum modernorum taliter scribentium dolce et novum clavum
in hoc alludens vendentibus cartam bononie et quarternos cum
lineis habentibus certos clavos in se secundum majorem et minorem
commensurationem et formam librorum seu voluminum veterum vel
novorum.

	|56. ritenne.~  Eo quia non ita ardenter expressimus unum
quid sicut tu -- |57. nuovo chiodo.~ quasi diceret si modo
deberem meas rimas compillare modus quem tenes esset clavus. mee
menti. videlicet.  numquem scriberem nisi incitatus a corde et in
scribendo premeditatus quod secus fecimus. notarius.  Guitonus et
ego.

	|60. non avvenne.~ quia scribebamus non premeditati et a
corde non incitati sed ad libitum et sine ratione.

	|82. or va.~ dicit hic ista umbra quod domnus Cursius
ejus frater de proximo tractus erit a quadam bestia ad mortem
violentam corporalem et cum anima ad vallem inferni et ita fuit
nam inde post non modicum tempus aspirando una die ad dominium
civitatis florentie sua ferina et bestiali arrogantia et
presumptione et superbia. que hic pro dicta bestia ponitur
occisus est in fuga prope dictam civitatem.

	|103. rami gravidi. e vivaci.~ cujusdam alterius arboris
quam allegorice pro cibo corporali et ejus attractione summit a
quo depenet vitium gule. quod in matre nostra Eva occasio fuit
omnium malorum de qua Genesi III.[ -- ?] dicitur. dicens videns
mulierem lignum quod esset pulcrum occulis et ad vescendum suave
tulit de fructa illius et comedit ubi glossa non licet intueri
quod non licet concupisci et hoc notat hic dum finxit vocem illam
in hac arbore monuisse eos. ut se proximarent sibi que arbor
scientie boni et mali in esu vetitia Eve et ade. ut Genesi II.[
-- ?] materialis fuit licet spiritualiter liberum nostrum
arbitrium figuret nam et materialis fuit petra percussa a Moyse
in deserto fundens aquam ut Numeri 20 capitulo et tamen
prefigurata est in christo non enim habuit a natura arbor
paradisi ut esset lignum scientie boni et mali sed ex occasione
quam presentit mala fuit propter inobedientiam. ut de hoc plenius
dicam infra capitulo fi. ad propositium igitur quia paucissimi in
hoc mundo ad supradictam primam arborem intendunt spiritualiter
per virtutem sobrietatis. sed ad hanc secundum omnes quasi
tendunt corporaliter vitio gule inobedientes illis verbis Luce
21.  Actendite ne graventur corda vestra in crapula et ebrietate
ex quo dicit secundario hec vox.  Quomodo hec secunda talis arbor
elevata fuit a dicta arbore Eve.  Ideo fingit auctor sub dicta
prima arbore nullas animus elevare manus sed tantum sub ista
secunda quasi ut sint ille anime gulosorum purgantes se ibi
taliter cupientes et carentes cupitis. ut divina justitia ita eas
puniat ut punitur anima tantali. ut in Ovidio IIII.[ -- ?]
dicitur.  Inde auctor inducit dictam vocem ad dicendum de pravo
effectu hujus vitii gule. ut refrenet homines ab eo.  Et primo
tangit quid et quantum in commensationibus contingat mali per
exemplum illorum centaurorum creatorum in nubibus poetice
loquendo. ut plene dixi supra in inferno capitulo 12. ut dicit
etiam textus hic qui semmel convitate per periteum sponsum
ypodamen ad suas nuptias egerunt [n?]t dicitur Jeremie V.[ -- ?]
et Iosue VIII.[ -- ?] capitulo nam post prandium dicti Centauri
saturi et ebriosi causa luxuriandi capere voluerunt dictam
sponsam cum aliis dominabus nuptialibus et rapuissent nisi Deus
et alii Comites dicti peritei eos fugassent bellando. et
occidendo. ut Ovidius in 12.  Tertio fingit auctor hic dictam
vocem clamasse et dixisse contra illos Ebreos molles de quibus
legitur Iudicum capitulo 7.[ -- ?] nam dum Gedion parasset se ad
pugnam contra Medianos cum decem milibus ebreis habuit a Deo quod
ante talem pugnam duceret dictam gentem ad flumen auoddam et
omnes bibentes resupine cum ore in aqua rejceret et bibentes cum
manu duceret secum ad bellandum et ita facto solum 300 fuerunt
bibentes cum manu ut dicit Glossa quod genuflexo bibit qui avide
bibit et cum illis 300 octinuit.

	|128. colpe de lagola.~ ut fuit de divite epulone illo
qui splendide convivabat ut luce capitulo 16.

	|136.  Driczai.~  Hic venit ad finem VI. circuli et
principium VII. in quo puniuntur anime luxuriosorum.

	|150. ambroxia.~ erbe quas equi soli pascuntur valde
odorifere de qua ait Ovidius in II.[ -- ?] Ambroxie suco etc.

	|151. senti dir beati .~ dicit auctor quod ista vox VI.
Angeli dixit beatos illos quos gratia Dei removet ab hoc vitio
gule exuriendo semper prout justum est in hoc mundo ut impleatur
in alio juxta illud Salmisticum canticum Virginis exurientes
implevit bonis et divites dimisit inanes.  Et sic iste angelns
complet illam partem quam supra capitulo XXII.[ -- ?] ubi non
dixit et qui exuriunt ut est in textu Mathei V.[ -- ?]

