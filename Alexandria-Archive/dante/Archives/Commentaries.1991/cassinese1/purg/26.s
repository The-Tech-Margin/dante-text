	|29.  venne gente.~  Volendo hic auctor tangere de illis
duabus speciebus luxurie de quibus plene scripsi supra in inferno
capitulo 15. et 16. scilicet. de sodomiticis et luxuriantibus
bestialiter et contra naturalem legem fingit has duas qualitates
animarum ita diverso calle sibi obviam venisse per hunc ignam
quod totum tangit hic in eo quod dicit de Cesare qudiente iam
contra se. idest. in ejus infamiam dum semel triumpharet obici
sibi quomodo fuit regina. idest. quasi uxor Nicomidis regis
bictinie a quo sodomitus fuerat sua pueritia unde ob hoc hii
Rictimi facti fuerunt.  Galliam Cesar subegit Nicomides Cesarem
Ecce Cesar nunc triumphabat qui subegit Galliam.  Nicomedes non
triumphat qui subegit Cesarem et hoc de sodomitis de
luxuriantibus contra naturalem legem tangit dum dicit de phasife
de qua plene scripsi supra inferno capitulo 12.

	|76.  la gente.~  Construe sic. gens que non venit
nobiscum fuit offensa ^decio.~ idest. de illo peccato ^perche.~
idest. propter quod iam Cesar in triumpho suo intellexit se
nominari regina.^contra se.~ idest. in reprensionem et obrobrium
