	|78.  Regina.~  Cesar in pueritia pulcerimus stetit cum
quodam rege bittinie nomine licomedes adeo ei gratiosus et
dilectus qu9od publice dicebatur dictum regem sudumitare eum modo
tractu temporis Cesar adultus romam rediit et factus tribunus et
in Galliam missus eam vicit et populo romano suegit.  Erat tunc
rome consuetudo quod quando tales vitoriosi romam revertebantur
ponebantur super quodam curru aureo et per romam cum maximo
triumpho ducebantur et supra eapud ejus in curru sedebat unus
ribaldus malidicus qui omnia vitia que sciebat de dicto
triumphante elamabat in populo in reprensionem dicti triumphantis
et eum aliquando alapis cedebat. ut se non extolleret in
superbiam.  Modo Cesari redeunti victa Gallia et deducto per
romam super dicto curru dictus ribaldus in ejus reprensionem
elamabat in populo ostendendo Cesarem dicens ecce qui se
supponebat licomedi tamquam ejus uxor in hec verba.  videlicet.
ecce Regina que subjacebat licomedi et tunc facti fuerunt
infrascripti rittimi Cesar subegit Galliam licomedes Cesarem etc.

	|79.  si parton.~  Isti cum quibus non erat Dominus Guido
fuerunt mere sodomite. sed isti cum quibus erat fuerunt partim
sodomite et partim egerunt cum mulieribus contra naturam et ideo
dicit quod eorum peccatum fuit hermafroditum eo quia egerunt cum
mulieribus tamquam si ipse fuissent masculi hermafronditus est
qui habet utrumque sexum et ideo mere sodomite elamant sogdomam
etc.  Isti alii quia egerunt contra naualem legem elamant illud
quod scripsi de phasife supra in inferno capitulo 12.

	|91.  tristitia di Ligurgo.~  Scribit Statius in 5
thebay. quod dum ysifilis filia toantis et uxor olim Iasonis ex
quo habuit duos filios.  scilicet.  Toantem et Enicum cum Adstro
et aliis regibus grecis iverunt in obsedione thebarum de quibus
plene scripi in inferno capitulo 14. foret exp;usa de insula
lemni a feminis dicte insule io quod evadere fecerat dictum ejus
patrem contra promissionem de qua ystoria plene scripsi supra in
inferno capitulo 18 et navigasset versus insulam Schirii ubi erat
dictus ejus pater capta est a pirratis et vendita in Enea
civitate certis ministris liguri. regis tracie et sic inde facta
est nutrix Archemoris unici filii dicti ligurgi ignota ejus ragli
notione que manens inrure cum dicto suo alumpno et requisita a
dicto Adstro et ab ejus exercitu sitiente ut doceret eos aliquem
fontem ivit eis ostensum langiam fontem ut scripsi supra capitulo
22. et reversa reperit dictum Archimerum puerum occisum a
serpentibus unde condempnata ad mortem cognita a dictis duobus
suis filiis evasit a morte eorum amore et gratia de quibus ipse
Statius ait Inierant matremque avidis etc.

