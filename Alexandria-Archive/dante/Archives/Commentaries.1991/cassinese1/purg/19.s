|PURGATORIO CANTO 19~

CANTO XIX

CHIOSE MARGINALI I.

CHIOSE SINCRONE

	Modo ad cognitionem contemporum in dicto presenti
capitulo premictendum est quod secundum figmenta poetarum
Achelous fluvius habuit. tres filias ex calliope musa que ab eo
inde Acheloides vocate sunt.  que rapta proserpina a plutone ut
ejus consozie converse sunt in monstrua marina et dicte sunt
syrenes iste vere loquendo puelle fuerunt optime canentes et ideo
finguntur filie Acheloy fluminis propter ejus sonoritatem sed
quia sonoritas sine artificio non valet fingunt cas poete filias
fuisse dicte Calliope muse que sonat consonantiam et quia dicte
puelle sua dulcitudine cantus attrahebant sibi homines dicte sunt
syrenes a siren.  quod est attractio modo quia in hoc mundo quasi
ut mari quodam tales terrene et caruales delectationes nos
attrahunt ut plurimum et submergunt in peccatis et vitiis
finguntur ita ut syrencs in mari canendo homines in navigia ad se
trahere.  Igitur ad propositum Auctor habito usque huc tractatu
de quadruplici nostro amore vitioso et corrupto faciente nos
nostro primo motu et malitia cadere ut incontinentes in
superbiam.  Invidiam Iram et accidiam. ut superius dixit volens
nunc incipere tractatum alterius triplici nostri amoris corrupti
et concupiscibilis non ita nostro motu et malitia principaliter
set attractione potius quadam ab extrinseco veniente in nobis que
tripliciter accedit primo a bonis terrenis attrahentibus nos ad
avaritiam et cupiditatem secundo a mulieribus attrahentibus nos
ad luxuriam tertio a cibis et poculis attrahentibus nos ad vitium
gule. quo respectu puto aniquum usum pingendi dictas syrenas
habuisse hic. scilicet. eas pingere cum vultu virgineo in quo
attractio predicta luxurie denotatur.  Item cum manibus strictis
in quo attractio avaritie figuratur.  Item cum caudis piscium in
quo attractio gule denotatur fingit se cogitando ad id quod
superius dixit sibi Virgilius dicendo quomodo dictus triplex amor
superius plorabatur.  Secundum quomodo tripartitus sumatur
relinquebat ei discernere obdormisse ut dixit in fine precedentis
capituli et sompniasse ut hic dicit textus. scilicet. se vidisse
contemplando dictam triplicem attractionem in suo primo et
proprio esse ut mulierem quamdam ita turpem et informem prout est
recto judicio rationis.

	|2.  intepidar.~  Eo quia victus est a frigore terre.
tunc ut hic dicit et interdum a saturno. idest. per aspectum
dicti saturni. ad solem in quo alba gromantes parant se ad
videndum solem surgere in Oriente. idest. per spatium breve inter
lucem et obscurum quem solem vocant majorem fortunam ex eo quod
inter XVI. figuras gromantie duas ex eis attribuunt ipsi soli qui
per eos vocatur fortuna major et fortuna minor et tunc dicit se
vidisse. idest. contemplative percepisse feminam. que hic
describitur.

	|15.  Come amor vuol.~ quasi dicat quod nos ipsi moti sic
ab attractione predicta vitiosa in se turpi reddimus eam pulchram
videndo eam ceco visu concupiscentie et acceptabilem.

	|18.  averi mio intento.~ quasi dicat quod nedum propter
dictam triplicem attractionem principalem homines attrahuntur ad
dicta vitia sed etiam propter dulcitudinem soni et cantus
mundani.

	|22.  Io volsi Ulixe.~  Hoc dicit solum ut atractio
muliebris que in persona circis meretricis eum attrassit ut plene
scripsi supra in inferno capitulo 26. de qua hac muliebri
attractione tali etiam loquens Augustinus super quper quadam
epistola Petri ait pellices dicuntur Meretrices a pollutione vel
a pellis formositate que incautos homines pellicit. idest.
allicit et attrahit.  Modo quia per dictum dudicium rationis
figuratum in persona Virgilii ut sepe dictum est respiciens ad
virtutem temperantie presertim confundimus has vitiosas
attractiones et refrenamus et abjuicimus ut putridam et femdam
rem respicientes eas cum oculis linceis. idest. rationabilibus et
recto judicio considerativo et intellectu fingit auctor hic ita
super vinisse. hanc secundam aliam dominam honestam et Virgilium
ita in ea respicientem et per eam ut per virtutem temperantie ita
aperientem hanc muierem aliam premissam et ostendentem sibi ejus
ventrem putridum et abominabilem ut hic dicitur quam secundam
dominam sumit pro dicta virtute temperantie in quantum frugalitas
que sic definitur per Valerium frugalitas inimica est luxuriosis
epulis et vini abundantie ab immoderato veneris usu adversa.

	|50.  Qui iugent.~ ista sunt verba Matthei e. capitulo
dicens. scilicet.  beati qui lugent quoniam ipsi consolabuntur
que faciunt ad purgationem premissi vitii accidie.

	|70.  Comio.~ hic fingit auctor intrare quintum circulum
in quo puniuntur anime avarorum et prodigorum ita jacentes
resopinas et plorantes.

	|73.  Adhesit pavimento anima mea.~ vivifica me secundum
verbum tuum que verba psalmiste potest dicere in isto mundo
quilibet corrigens se de hoc vitio et debet considereari ejus
psalmi CXVIII. sententia ad quam non dubito auctor respesisse.

	|99.  fui successor petri.~  Iste fuit Papa Adrianus
quintus de domo illorum del Fiesco qui hodie dicuntur comites de
la Vania fiumine labente in territorio Januensi inter has duas
terras. scilicet.  Chisverei et Siestri qui Papa Adrianus sedit
in papatu uno mense et VIIII. diebus et mortuus est.

	|105.  Piume sembran.~ quasi dicat omne aliud pondus
vester esse pluma respectu ponderis papalis manti et maxime
volenti eum bene custodire in quo vult auctor ostendere quomodo
difficilier regimen papale geritur sine peccato et reprehensione.

	|121.  avaritia spense.~ quia ligati et intenti eramus
solummodo ad avaritiam et non ad aliud et ideo sic hic ligati
sumus -- |123.  ne tene.~ per supradicta verba vult auctor
ostendere naturam et pravitatem hujus vitii avaritie ut homines
viventes in hoc mundo avaros magis retrahat visa pena ab ipso
vitio avaritie.

	|124.  lingati epresi.~  Avaritia enim est radix omnium
malorum.

	|137.  neque nubent.~ scribitur Matthei XXII. quod
Sadducei negabant in futurum resurrectionem nostram ita a Christo
querentes Moyses scripsit si uxor fratris alicujus vidua
remanserit sine filiis alter frater eam in uxorem accipiat modo
quedam mulier fuit uxor septem fratrum sussessive in
resurrectione cujus erit uxor[?] quibus Christus respondit cum
resurgerint nec nubent nec nubentur. set erunt sicut Angeli in
celo idest.  vivent angelica vita et nulla discretio erit inter
eos.

	|142.  Alasgia.~ que fuit uxor domini Maro[belli?] Ma.
laspine Marchionis de Lunisana.

