<Purgatorio> CANTO 23
Chiose Marginali
1.
Chiose Sincrone

	Premisso prohemio suo quod per se satis patet auctor
continuando se ad proxime supradicta describendo penam quam dicit
animam pati in purgatorio propter peccatum gule in fame et siti.
fingit se nunc hic vidisse has umbras ita macilentas et occulis
obscuris et canas. ut dicit textus. quod forte possit reduci
allegorice etiam ad illos homines qui in hoc mundo viventes in
satisfactionem hujus vitii gule cum abstinentiis et jeiuniis
quasi se purgando. simili modo extenuati apparent.

	|25.  Non credo.~  Scribit Ovidus in VIII. quod dum
Eresiton in contemptum cereris dee bladi quamdam quercum illi
consecratam incidisset extenuatus est ita per famen ad instantiam
dicit cereris quod consumptus est fame unde describendo quod eum
fames annichilavit inquid quesitan que famem etc.  Qui Eriston
consumptis omnibus suis opibus propter famem ultimo vendidit
filiam propriam.

	|28.  Io dicea.~  Quando Tytus Vesp[a?]xianus obsedit
Ierusalem gens Judaica ibi habita erat propter famem ita effecta
macilenta sicut apparebant iste anime nam ad tantam penuriam
devenerant ultimo quod quedam mulier nomine Maria proprium filium
quem lactaverat reperta fuit in dicta Civitate sic obsessa assare
causa ipsum comedendi ut ait Josephus de cujus obsedione dixi
supra capitluo eodem proximo.

	|48. forese.~ de Donatis de florentia filii domni Symonis
et consotii magni dicti auctoris.

	|72. dovria dir sollaczo.~  Dicit hic auctor quod iste
anime plorant et canunt ibi moti illa duplici voluntate quo
dominus noster letus ivit ad crucem et tristis in qua clamavit
ely ely. lamazabatani nam ait Atanasius scribens contra
Pollinarum quando christus dixit si possibile est transeat a me
calix iste sed tamen non mea sed tua fiat voluntas et dum dixit
Spiritus quidem prontus est caro vero infirma duas voluntates
ostendit humanam. scilicet. que propter infirmitatem carnis
fugiebat pati et divinam qua pati volebat et rationalem.

	|94. labarbasgia.~  Est quedam contrata insule sardinee
in qua mulieres vadunt seminude.

	|120. suora di colui.~ sc. solis. nam luna. dicitur soror
solis qui sequitur modo.

END OF CANTO: Anne Marie Booye 12/2/86

