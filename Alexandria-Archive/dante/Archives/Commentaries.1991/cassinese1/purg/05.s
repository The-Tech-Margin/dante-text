	|88.  Buonconte.  Iste fuit de comitibus de montefeltro
de urbino et fuit mortuus inbello in casentino loco dicto
cammpaldino districtus florentie optento a florentins contra
aretinos cum quibus aretinis erat ipse et numquam relantum fuit
ubi moriretur et quomodo. modo auctor componit talem
immaginationem sue mortis ut dicit textus.

	|95.  larchiano.~ que nascitur in alpe montis pruni supra
