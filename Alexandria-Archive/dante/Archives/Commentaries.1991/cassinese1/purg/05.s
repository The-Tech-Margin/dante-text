CANTO V.

CHIOSE MARGINALI I.

CHIOSE SINCRONE

	|6.  gli occhi rivolsi.~  Hic ostenditur fragilitas
humana quia homo existens in penitentia multotiens vacillat.

	|10.  tanto simpiglia?~ quasi dicat. quod volens
ascendere ad virtutes non debet propter murmura personarum vel
alia accidentia sibi tale iter lentum facere.

	|67.  Ondio.~  Est sciendum quod Marchio aczo estensis.
Dominus Ferrarie probabat juxta posse esse dominum bononie et in
ea habuit multos tractatus populus bononie de hoc perpendens
elegit in potestatem domnum Jacobum Dalcassaro de civitate Fani
qui cum intravit in regimen multos amicos dicti domni aczonis
cives bononie qui istis tractatibus asenserant cepit et aliquod
decapitavit aliquod expulit semper utendo verbis ampullosis et
injuriosis contra dictum domnum Aczonem et dicebat specialiter
quod jacuit milies cum noverca sua et quod descenderat ex quadam
lavatrice et multa alia oprobriosa propter quod dictus marchio
semper postea procuravit facere eum occidi ab assisinis et tandem
dum ipse domnus Jacobus esset electus per domnum Maffeum
vicecomitem dominum mediolani in potestate ipsius civitatis
mediolani et acceptaverit causa eundi ad dictum officium secessit
de fano et venit per Mare usque venetias inde volens venire
paduam fuit mortuus ab asisinis juxta quamdam villam que dicitur
oriacum in districtu paduano et quomodo fuerit mortuus dicit
textus. nam Marconus de mestre comitatus trivisii asisinavit eum
et cum quodam rongone ammputavit sibi cosciam cum totu sexu et
ideo forte quod vidit sanquinem suuper quo sedebat. scilicet.
sanguinem coscie et sexi. vel super quo sedebat dicit ideo quia
anima dicitur sedere super sanguinem.  Vel dicas et forte verius
quod dum bononienses facerent guerram cum marchione Aczone domino
ferarie et mutine occaxione castri bazani et sangnani comitatus
mutine occupatorum per dictum comitem bononie domnus Jacobus
reperiit se esse potestatem dicte Bononie in quo officio oportuit
eum multa dicere et facere que dictus marchio injuriosa sibi
reputavit.  Quo officio completo tractu temporis ibi mediolanum
in potestariam et sibi contingit quod supra dicitur.

