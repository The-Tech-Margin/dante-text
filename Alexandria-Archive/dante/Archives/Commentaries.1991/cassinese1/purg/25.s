	|50.  coagulando prima.~ i. in VII. horas usque ad quod
spatium aut adheret matrici aut refunditur ^aviva.~ subaudi vita
vegetabili cujus vite tres sunt potentie. scilicet. nutritiva
augumentativa et generativa -- |51. costare.~ i. cohagulare vel
