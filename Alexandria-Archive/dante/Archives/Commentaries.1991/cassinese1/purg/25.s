	|79.  lachesi.~ i. fatalis dispositio temporalis de qua
dixi supra capitulo XXI. -- |81.  lumano el divino.~ i. ejus esse
corporale et spirituale ac aliis sensualibus potentiis. quasi
omnibus mutis. idest. sopitis per mortem corporis ducit secum
virtutes animales ut est memoria intelligentia et voluntas
acutiores solito.

	|84.  agute.~  Augustinus in libro de spiritu et anima
ait anima recedens a corpore secum trahit solum imaginationem
rationem intellectum concupiscibilitatem et irrascibilitatem et
secundum merita afficitur et fortius hiis potentiis utitur.  Item
dicit in libro de Trinitate quod memoria intelligentia et
voluntas sunt una vita una mens una essentia et sunt in anima
substantialiter.

