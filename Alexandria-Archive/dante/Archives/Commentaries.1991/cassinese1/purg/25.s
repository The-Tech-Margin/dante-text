PURGATORIO XXV

CHIOSE MARGINALI I.

CHIOSE SINCRONE

	In hoc capitulo auctor principaliter 4. facit nam in
prima premisso exordio summit querendo materiam scribendi de
generatione humana nostra et de natura et qualitate nostre anime
coniunte et separate et hec durat usque ibi.  ^Sangue perfecto.~
etc. ibi 2[a?] in qua dicit de corporali nostra formatione et hec
usque ibi.  ^Ma come danimal.~ etc. ibi 3.[a?] in qua de esse
nostre anime tractat et hec usque ibi. ^et gia venuto atultima.~
etc. ibi. 4.[a.?] et ultima in qua incipit tractatum VII. circuli
et durat dum fingit se venire ad ultimum Angelum infra.

	|3.  atoscorpio.~ quasi diceret quod de XII. oris diei in
illo emisperio jam VIII.[?] precesserant et IIII. restabant adhuc
et nox in isto erat in medio.

	|21.  non tocca.~ cum sint dicte anime spiritus
incorporei et sic non est eis opus alimentatione nec nutrimento.

	|22.  Meleagro.~  Scribit Ovidius in VIII. quod dyana dea
indignata contra Cencum regem callidonie ejus contemmptorem misit
in dictam ejus contratam aprum quemdam vastantem omnia quem demum
dictus meleager occidit et capud ejus tradidit Atalanti virgini
quam dilligebat ut procus ejus tamen Flexipatra et Toxipus
fratres et avunculi dicti Melagri dictum capud apri abstulerunt
per vim inscio dicto Meleager ambos occidit quo scito Altea mater
dicti Meleagri et soror predicotum fratrum quemdam stipitem
fatatum. ut quantum duraret duraret vita dicti Meleagri misit in
ignem et secundum quod comburebatur ita dictus meleager
consumebatur et sic demum consupto dicto stipite. mortuus est
iste Meleager. ut ait Ovidius. stipes erat. qui cum etc.  Ad
propositum dicit Virgilius auctori quod si dicte cloto lachesis
et Antropos sorores de quibus dixi supra in XX.  capitulo
demonica potentia hec facere potuerunt et Circes homines in
bestias convertere arte magica qua etiam Apulegius asinus diu
fuit quanto magis divina potentia facere potest animas predictas
in purgatorio macrefieri cum talem potentiam contulit demonibus.
Item secundo arguit Virgilius et dict de motu nostro in speculo
apparente quod tamen in se speculum non movet vere sed
representative in quo promictit quod inferius magis aperit de
virtute informativa et aere vicino dictarum animarum.

	|40.  Sangue.~  Hec est secunda pars hujus capituli in
qua Statius explicite solvendo dubium auctoris de anima nostra
separata incipit primo a remotis dicere de creatione nostra
corporali et ut de conjuncta copulatione ipsius anime et ejus
origine melius premictere possit que expediunt ad propositam
materiam sequendo Augustinum dicentem in suo libro quomodo animam
Ade Deus potuit amiscere limo terre que formato corpore primo
animam accepit ut Genesi capitulo II. sed ratione carebat quia
prius debebat domus compaginari inde induci habitatorem inquid
sic.  ^Sangue.~ etc. ad cujus principii evidentiam notandum est
quod in nostro virili stomaco de cibo et potu digesto natura
duplicem sanguinem ellicit rubeum. scilicet. qui cruor dicitur
quem epar de stomaco surgit et post trasmictit ad venas ad
augmentationem et substentationem membrorum corporalium.  Item
alium dealbatum eo quod magis decoctum et magis per consequens
perfectum. ut hic dicit textus qui licet vocetur talis sanguis
secundum ysiderum virus. unde ad eo viri dicimus secundum eum et
per philosophum.  Aqua viri coitus tamem sperma voctur et ad
procreandum institutum est qui quidem non bibitur. idest.
atrahitur a venis. ut primus sed remanet post digistionem quasi
alimentum leve quod elevatur ultimo de mensa. ut fragmentana
panis et sic ultimo dictum sperma levatur ab omnibus nostris
principalibus membris capiens a corde specialiter ut a
principaliori membro habitum et virtutem informativam ad
compositionem corporis humani producendam a quibus principalibus
membris dicit Statius dictum sperma descendere ad testiculos inde
per coitum in vulvam et matricem mulieris et super ejus mulieris
sanguinem habentem specialiter etiam a corde virtutem
informativam ad humanum fetum producendum [??????] subaudi et ex
hoc subdit Statius hic statim quomodo sperma predictum
dispostitum est virtualiter ad agendum propter suam majorem
perfectionem et dictus sanguit mulieris ad patiendum.

	|43.  Prenile dal core.~ quasi diceret sperma habet
