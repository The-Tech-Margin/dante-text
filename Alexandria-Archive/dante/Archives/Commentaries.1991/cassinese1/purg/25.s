gustari a mestuo scilicet ipsum sperma.

	55.^e sente.~ sc. ipsa virtus influens que est activa.
scilicet. sperma56 ^fungo marino.~ qui est informis viscositas
que in certis lictoribus maris reperitur ut scribit phylosopus in
suo libro de animalibus Quam viscositatem vocat ibi fungum
marinum nam si incutitur ab aliquo movetur quasi ut testudo
quedam et sic secundario factus est dictus embrio anima sentitiva
elapsis septem diebus secundum Macrobium.

	|58.  se distende.~ i. membra figurat quod est in quinta
edomoda secundum dictum Macrobium. -- |60.  intende.~ et sic
concludendo in hac secunda parte postquam per virtutem principii
activi quod erat in femina producta est anima sensitiva in
generato quantum ad aliquam partem ejus principalem tunc incipit
operari dicta anima sensitiva ad complementum proprii corporis
per modum nutritionis et augmenti.

	|61.  Ma come.~ IIiis dictis veniamus ad tertiam partem
quam ut levius valeam explicite exponere et ut perspicatius
intelligatur per hunc modum procedam primo querendo quid sit
anima intellectiva et rationalis de qua hic dicitur.  Secundo
unde in nos veniat.  Tertio quando in nobis infunitur Quarto
quomodo se habet cum vegetativa anima et sensitiva.  Quinto ubi
in nobis localiter residet.  Sexto que sunt ejus virtutes et
potentie.  Septimo quomodo se habet separata a corpore.  Ad
primum dico quod multi multas diffinitiones dederunt et hoc non
insisto. que anima secundum ysiderum dum vivficat corpus dicitur
anima dum vult dictitur animus dum scit dicitur mens dum recolit
dicitur memoria dum recte judicat dicitur ratio dum spirat
dicitur spiritus dum sentit dicitur sensus.  Ad secundum dico
quod a deo.  Ad tertium dico quod numquam anima infunditur nisi
oranato embrione et plene habilitato ad ipsam animam recipiendam
quam infusionem anime dicunt fieri in masculo in quadraginta
diebus in femina vero in octuaginta diebus.  Ad
quartum.............63.  ^errante.~  Nam phylosophus in 3. de
anima ponit intellectum separatum ab anima eo quod non videbat
organum assumptum que opinio ut falsa improbat Tomas in prima
parte questione 76. ubi dicit intellectus qui est intellectualis
operationionis principium. idest. anima forma est humani corporis
et licet intelligere sit actus qui non possit exerceri per organa
corporalia sicut virtus visivaper occulum tamen in materia est in
quantum ipsa anima cujus est hec virtus est hominis forma et
terminus generationis humane.

	|65.  intelletto.~ i. animam intellectivam que dirigit
sensitivam eo quod anima est quid aprensum per ipsum intellectum.

	|69.  perfecto.~ quod est in masculo in quadraginta
