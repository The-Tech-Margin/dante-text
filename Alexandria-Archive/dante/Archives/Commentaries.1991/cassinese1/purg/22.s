	|115.  Taccevansi.~  Expedita ista prima auctor procedit
ad secundum in qua dicturus est de V.[ -- ?] circulo purgatorii
in quo dicit purgari animas gulusorum ut homines in hoc mundo
viventes se retrahant a vitio gule propter virtuositatem
oppisitam et constrariam dicto vitio gule capescendam. ut
superius in aliis circulis fecit fingit se in principio ejus hanc
primam arborem invenire cum pomis et aspersione aque ita
conversam radicitus ad celum. ut dicit textus ad cujus
allegoricum integumentum veniendum prenotandum est. quod secundum
quod colligi potest ex scriptura sacra nobis hominibus duplex in
hoc mundo cibus paratur spiritualis. scilicet. et corporalis ex
alimento quidem primi consequitur. homo in hoc mundo vitam
eternam ex alimento vero secundi vitam corporalem et temporalem
unde christus qui est ipse cibus spiritualis de se dicit.
Joannis V.[ -- ?] quod est panis de quo comedentes vivent in
eternum secus in illis qui comedunt de corporali ut comederunt
ysraelitici de manna figurato in tali cibo corporali et tandem
omnes in deserto mortui sunt. ut exodi capitulo XV.[ -- ?] Item
dicit panis. quem ego dabo caro mea est pro mundi vita.  Itam est
aqua illa de qua bibens non sitiet in eternum ut Joannis IIII.[
-- ?] capitulo quo quidem christo ut cibo spirituali frui non
possimus nisi contempnendo cibum corporalem unde deuteronomii.
VIII capitulo frequens sumptio cibi spiritualis inducit
contemptum cibi corporalis et gregorius quanto corpus impletur
tanto anima minoratur hic igitur auctor hanc arborem ponit pro
alimento et saporatione dei ut cibi spiritualis verum quod ad
dictam saporationem talem non possumus a nobis nec illam ut
arborem inascensibilem adscendere nisi gratia nos desuper elevet
ad eam ideo fingit auctor eam arborem ita hic conversam ad celum
ad quod forte ait gregorius per arborem palme significatur vita
justorum que in terris debilis est et in celo fortis et per hoc
docuit nos christus orando implorare panem cotidianum qui pro
tali gratia ponitur. secundum unum intellectum.  Item quia
mediante tali gratia dei per scientam sacre scripture gustamus ut
plurimum. talem spiritualem cibum.  Itam fingit auctor hanc vocem
quam hic accipit pro auctoritate famosa sacre pagine allegorice
loquendo inter frondes dicte arboris dicere quomodo daniel
spre[v?]it cibum corporalem ut danielis capitulo I. et capitulo
X.[ -- ?] et acquisivit talem scientam ut cibum spiritualem.
Item quia mediante etiam tali gratia dei venit ad hunc
spiritualem cibum per virtutem temperantie et sobrietatis ut ad
saporationem dei cum jejunio et abstinentia cibi corporalis ut
faciunt santi Heremite contenti pane pomis et aqua et ut
faciebant discipuli domini comedendo spicas ut Matthey secundo
capitulo.  Ideo fingit auctor dictam vocem suasive dicere hic
quomodo domina nostra non petiit vinum in nuptiis ut scripsi
supra in capitulo XIII.[ -- ?] pro ejus gustu. sed pro aliis et
quomodo antiquis mulieribus romanis erat incognitus usus vini. ut
ait Valerius in secundo et quomodo illi de prima etate aurea
egerunt ut ait ovidius in primo. aurea prima sata etc. et quia
dulciter talis cupiditas explebatur aquas illas mel appellavere
et nectar.  Item quomodo Joannes batista in deserto procibo
habuit mel silvestre et locustas nec vinum novit vel sicceram quo
non surrexit major inter natos mulierum. ut Matthey 3.[ -- ?]
capitulo et luce et marci capitulo primo.

