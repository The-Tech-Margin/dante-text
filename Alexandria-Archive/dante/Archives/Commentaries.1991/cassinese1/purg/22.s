	Auctor principaliter duo facit primo se continuando ad
proxime dicta complet tractatum quinti circuli usque ibi --
|taceranse a[le?]ndui [g?]ia poeti.~  Ibi incipit tractare de
sexto circulo et hec secunda pars durat usque ad 25 capitulum in
eodum ibi -- |c gia [venu?]to al[u?]ltima tortura.~

	|5.[be?]ati.~ sc. qui esuriunt et sitiunt justitiam
quoniam ipsi saturabuntur tamen meditatione dixit iste angelus
se. qui sitiunt justitiam. idest. qui cupiunt esse in virtute
frugalitatis contraria vitio cupiditatis ut mathey 5.[ -- ?]
capitulo nam de illis verbis mathey. beati qui exuriunt et
sitiunt justitiam quoniam saturabuntur presens angelus non dixit
nisi beati qui sitiun[t?] justitiam. alia vero. scilicet. beati
qui exuriunt quoniam saturabuntur dicet alter angelus anfra in
capitulo 24 in tractatu gule.

	|10.  Quando.~ dicit hic auctor illud quod dicit tullius
in suo libro de amicitia dicens propter virtutem et probitatem
etiam eos quos numquam vidimus quodammodo dilligimus tangendo
etiam de benivolentia que ut ait phylosophus in VIIII.[ -- ?]
ethycorum est virtus differens ab amicitia nam benevolentia est
quantum ad ignotos et presentiam concupiscit.  Amicitia vero est
inter cognitos et simul conversantes.

	|40. sacra fame.~ que verba sunt in Virgilio III.[ -- ?]
ibi. fas omne obrumpit ec. exclamando contra Polinestorem. de
ejus avaritia et cupiditate -- |42. giostre grame.~  In qua pena
puniuntur supra in inferno prodigi capitulo VII.[ -- ?]

	|6.  Quandi.~ quasi diceret quot prodigi resurgent in
di[c?] judicii cum crinibus incisis ut dixit supra in inferno
capitulo VII.[ -- ?] propter ignorantiam tollentem eis sensum et
conscientiam penitendi et confitendi tale peccatum. ut mortale
vivendo in extremis. idest. in prodigalitate.  que est ita
extremitas a medio virtutis liberalitatis. ut est avaritia.

	|56. tristi[l?]ia de locasta.~  Et est sciendum quod
dicta locasta in vita sua multa infortunia tulit nam prima ejus
tristitia fuit quia vidit layum ejus virum regem thebarum primo
occidi in bello ab Edipo filio ipsius lay et ipsius locaste
ignorando se fore filium predictorum et quod Forbas rex quidam
alius fuisset ejus pater non verus sed putativus sive adoptivus
nam dictus Forbas. carens filiis adoptaverat hunc Edipum
transmissum a dicta ejus matre de precepto dicti ejus viri habito
per presagia quod debebat eum occidere ad qouddam nemus ubi eum
occidi facere debebat tamen pi[c?]tate materna ibi vivus remansit
qui ut pulcerimus infans sic repertus ibi presentatus per ejus
gentem fuit dicto Forbe.  Secunda ejus tristitia fuit dum postea
ignoranter accepto dicto Edipo in virum et ex eo habitis pluribus
filiis. ipsa recognovit una dic ipsum esse ejus filium per
stimata que impressit dicta locasta dicto Edipo dum transmisit
eum ut dictum est in plantis pedum.  Tertia ejus tristitia fuit
quanto dictus Edipus ejus vir et filius cognito patricidio quod
facerat et tali nephario suo conjugio dolore se ceccavit.  Quarta
et ultima tristitia fuit dum vidit una dic Etheoclem et
pollinicem suos filios et dicti Edipi se ad invicem occidi in
bello singulari propter quam se gladio peremit dicta locasta. que
omnia recitat seneca in tragedi[a?] ejus Thebay. et quia statius
in ejus Thebay. prosecutus est tantum hoc ultimum membrum ideo
auctor in persona Virgilii de crudis armis duplicis tristitia
dicte locaste ita loquitur ut hic in textu.

