PURGATORIO XVIII

CHIOSE MARGINALI I.

CHIOSE SINCRONE

	Continuando se auctor ad proxime precedentia adhuc
premisso suo exordio querit a Virgilio primo quid sit iste noster
amor animi a quo omnis nostra operatio bona et mala procedit.  Et
repondendo dicit sic a remotis.  videlicet. quod animus noster
cresatus est promptus ad amandum et sic inclinabilis et mobilis
est ad omne placibile nam ut ait phylosophus in libro de natura
aminalium omnia naturaliter appetunt delectationem et in quarto
ethicorum intendere opertet ad que facile mobiles sumus et in X.
dicit quod primum per desideria inclinata est mens nostra statim
veniens de potentia ad actum ut subditur hic in textu dictus
talis amor noster.  Item quod etiam quomodo nostra virtus
apprensiva intentionaliter ad id quod estimat bonum subito animum
volvere facit et si inclinatur ad illud talis inclinatio prima
est et dicitur talis noster amor animi ut dicit hic textus qui
amor ut motus spiritualis semper ascendit in altum. idest. in
augmentum...................cum desiderio donec res amata
contentet eum unde ex hoc diffiniendo Cassidorus talem amorem ait
amor noster est quedam vis appetitva rerum quas dissiderio
eligimus et amplectimur nam cum quis amat aliquid quasi illud
concupiscens apprehendit ut quid boni licet interdum non sit
dicit hic auctor.  Ac per consequens fallunt illi qui verificant
omnem nostrum amorem laudabilem ex quibus auctor adhuc sic infert
ergo si talis noster amor de foris evenit in nobis et anima
nostra non ambulat cum alio pede quam cum pede dicti amoris
secundum Augustinum dicentem amor pes anime est qui si rectus est
dicitur charitas si curvus dicitur cupidhas non debet merere vel
demerere. bene vel male agendo ad quod respondet auctor sub
representatione rationis nostre naturalis phylosophice dicendo
omnis forma substantialis. septa. idest.  circumdata a materia et
cum ea unita specificam virtutem habet in se etc.  forma
substantialis dicit frater Albertus est illa que adjunctione sui
ad materiam facit quoddam esse essentiale ut ignitas.  Forma vero
accidentalis est illa que adjunctione sui ad materiam non facit
quid sed quale ut albedo.  Alii dicunt formam substantialem esse
que dat esse simpliciter et per ejus adventum dicitur aliquid
generari et per ejus recessum corrumpi.  Accidentalis forma autem
non dat esse simpliciter sed esse tale ut sic sub illa forma
substantialis septa. idest. circumclusa a materia ut a sepe
comprehendit non solum nos homines corporaliter viventes sed
etiam cetera alia animata et inanimata corporalia et elcmenta tam
habentia virtutem specificam. idest. propriam et singluarem in se
collectam. idest. innatam que specifica virtus non sentitur in
nobis nec in actu operationis nec etiam demonstratur nisi per
effectum et per frondem viridem in arborem vejetabilis vita nam
sicut primeva magnes sive calamita numquam specificam ejus
virtutem attrahendi ferrum ostendisset nisi appropinquatum
fuisset sibi ferrum ita talis specifica virtus in nobis non
panditur nisi adveniat objectum aliquod movens habitus

	nostros et affectus ad diversa ut diversi sumus in facie
juxta illud perssi mille hominum species et rerum discolor unus
velle suum cuique est nec vota vivtur uno.  Unde nequimus
propendere unde intellectus. idest.  intellectualis primus motus
noster et primarum notitiarum veniat ymo provenit appetibilitas
cum semper in nobis antequam adveratmus que prime notitiarum
veniat ymo provenit appetibilitas cum semper in nobis antequam
advertamus que prime notitie et primi motus nostri et amores sunt
ita in nobis et resultant deficiente nostro judicio ut studium
mellificandi in apibus nam nesimus dicere quare amor potius eas
inducat ad id faciendum quam muscas vel vespes nisi quod ita
innatum est eis.  Reassumendo auctor hic in textu. scilicet.
quomodo prima talis nostra voluntas non meret bona nec mala sed
si in dicta mala prima voluntate oblectatur quis venialiter
peccat si procedit ad acutm mortaliter et e contra si in bona
quis procedit et ad oblectamentum venit incipit merere et ecce
principium merendi hujus secunde nostre voluntatis de quo
subditur hic in textu.  Et concludendo Virgilius inde dicit quod
licet ad dictam istam nostram secundam oblectatoriam voluntatem
quelibet alia subaudi voluntas. bona vel non aggregetur non tamen
necessitamur appetitive ad sequendum eas cum innata sit nobis
consiliaria virtus. idest.  ratio humana que habet nostram
voluntatem si bona est suadere si mala fugare Ideo quod liberum
nostrum arbitrium quantum ad rationem dicitur et liberum quantum
ad voluntatem ad quod phylosophus in |3.~ de anima sic ait
reprehendens male utentem hoc libero arbitrio in qualibet dicta
ejus parte amplius extendente se intellectu et dicente
intelligentia. idest. dicta nostra ratione fugere aliquid vel
mutari non movetur sed secundum desiderium agit ut inastinens
voluntas enim appetitus est omnis enim inellectus rectus est
appectitus et fautasia recta et non recta sunt nam semper movet
ad appetitum aut bonum aut quod bonum videtur fuerit et contrarii
appetitus ad invicem quod accidit cum ratio et desideria
contraria sunt. vel fuerint nam ait phylosophus. bonum
intellectus est.  ultima beautitudo. idest.  Deus quod bonum
proprie est obiectum nostre voluntatis unde quelibet recta
voluntas ab ipso Deo movetur et sicut color obiectum est visui et
illum movet absque necessitate ulla ita ipse Deus voluntatem
nostram ut ejus objuectum movet sine ulla necessitate et sic
liberum arbitrium remanet in esse suo et sicut si visiva virtus
nostra corrupta esset ratione organi vel alterius accidentis non
moveretur a calore sic et quando intellectus noster et voluntas
corrupta sit aliquo accidente non movetur a deo cum ipse non
possit causa perverse voluntatis.

