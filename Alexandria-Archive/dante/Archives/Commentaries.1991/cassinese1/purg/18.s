	|113.  Io fuoi.~  Ad ostendendum quomodo hoc vitium
accidie multum regnat in religiosis et claustralibus qui deberent
esse quasi aves spirituales et Bernardus dicit quod incedunt
lentissime ad modum testudinum -- ^Abate.~  Iste fuit Abbas
sancti zenonis tempore imperatoris Federici Barbarosse primi de
svenia qui cepit regnare anno Domini MCXL et qui inde elapsis
XXII. annis de Mense Martii civitatem Mediolani funditus
destruxit sibi rebellem hujus tempare Imperatoris Albanum
concrematum est a Romanis Hic Imperator Spoletum construxit.
hajus tempore sepulchrum Christi et crux a Soldano capta est hic
imperator Crucis stigmate signatus per mare cum multa multitudine
gentium ad liberationem terre sancte procedens cum esset in
Armenia in parvo flumine est submersus filius autem ejus quem
secum duxerat corpus ejus detulit usque Tyrum ibique sepultus
est. hujus tempore Abbas Johachinus fuit in calabria qui plures
libros super yeremiam et apocalipsim et de omnium prophetarum
libris seripsit. hujus etiam tempore Rodolphus coloniensis
archiepiscopus trium magorum corpora de perside olim
constantinopolim translata et inde a predicto Federico Mediolanum
transvecta Mediolano postea ab eodem Federico destructo Coloniam
transportavit. hujus etiam tempore petrus commestor in francia
qui utriusque testamenti ystoriam que dicitur scholasticam
composuit.

