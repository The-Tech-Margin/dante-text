|PURGATORIO CANTO 21~
<Purgatorio> Canto 21
Chiose Marginali
1.
Chiose Sincrone

	Sitis sciendi numquam ab homine discedit cum naturaliter
secundum phylsophum omnes s[?c]ire desiderent. nisi aqua. idest.
gratia dei talis sitis sedetur et ejus scientia infusa modo quia
christus mistice pro hac tali sua gratia ejus sciente accipit
aquam illam quam samaritana mulier illa ab eo petiit de qua
scribitur Ioannis capitulo IIII.[ -- ?] scilicet.  quomodo de
civitate sicar. que est in samaria provincia veniens auritura
aquam ad puteum quemdam positum extra dictam civitatem. ubi
christus ipse solus requiescens penes dictum puteum dixit ei. da
mihi bibere. cui illa ait quomodo tu.  Iudeus hic a me petis cum
samaritana sim cui christus ait si scires donum dei et quis est
qui petit a te potum tu petisses ab eo aquam vivam de qua bibens
non sitiet in eternum sed bibens de aqua hujus putei sitiet adhuc
et illa domine da mihi aquam hujus putei sitiet adhuc et illa
domine da mihi aquam illam ut non sitiam amplius super quibus
verbis ait Augustinus mistice hic aqua hujus putei que mortua pro
voluntate seculi ponitur et desiderabilitate nostra et scientia
que ut aqua in profunditate putei tenebrosa est ydra auriens eam
pro cupiditate nostra mundana ponitur et desiderio quod numquam
tali aqua satiatur.  Aqua vero viva de qua hic dictur pro gratia
spiritus sancti ponitur que caritate habet nos conjungere christo
fonti salienti de quo gustans nil vulterius desiderat.

	|1.  La sete natural.~ i. humana nam homines naturaliter
scire desiderant.

	|6. nescrive luca.~  Scribitur Luce capitulo ultimo.
scilicet. quod dum post passionem et resurrectionem christi
cleophas et Almeon secundum Ambroxium licet non nominetur in
evangelio dictus Almeon. nam gregorious dicit. quod iste non
nominatus fuit ipse lucas. sed quadam humilitate noluit se
nominare irent tamquam discipuli christi per Ierosolimam
loquentes soli ambo ad invicem et dolentes de ipso Jeshu.
apparuit eis christus dicens eis qui sunt sermones vestri qui non
cognoscentes eum dixerunt sibi et tu peregrinus solus es in
civitate Ierusalem etc. sic etiam fingunt umbra statii apparuisse
eis.

	|25.  Ma per colei.~  Sciendum est quod secundum quod
scribet Fulgentius greci vates antiqui sic finxerunt tres sorores
ut tres furias infernales deservire plutoni regi infernali.
scilicet.  Megeram. tesifonem et aleto. de quibus plene dixi
supra in inferno capitulo VIIII.[ -- ?] ita atribuunt ei tres
alias sorores ut tria fata quarum prima dicitur Cloto de qua hic
auctor facit mentionem. secunda lachesis. de qua mentionem facit
auctor in eodem capitulo XXV.[ -- ?] tertia Antropos de qua supra
in inferno capitulo penultimo.  Cloto enim dicitur evocatio
lachesis sors Antropos sine ordine hoc sentire volentes quod
prima sit nativitatis nostre evocatio.  secunda vite sors
quemadmodum quis vivere possit tertia mortis conditio que sine
lege venit et ex hoc a[?t]ribuitur dicte Cloto colum deferre et
in eo ponere debitam quantitatem lini que florentie dicitur
conocchia lachesis filare. idest. producere vitam nostram
Antropos rumpere quod fi[?l]atum est. idest. finem imponere vite
unde versus Cloto colum bajulat lachesis net Antropos occat quasi
includatur forte tres dispositiones nostri animati corporis ut
prima ducat nos de non esse ad esse. secunda ut per illud esse
temporale nos trahat tertia ut nos ducat de tali esse ad non
esse. et secundum hoc loquitur hic textus.

	|48. tre gradi.~ quasi diceret quod a porta purgatorii.
supra talia non fiunt cum fingant illam superiorem partem dicti
ejus montis esse in dicta prima etherea regione aeris ubi talia
evenire non possunt.

	|50. figlia di Taumante.~ que est ille semicirculus aeris
quem vocamus Yridem quam poete fingunt fuisse filiam Taumantis.
Iste semicirculus est refractio visus ad solem qui quia interdum
apparet. in parte meridiana et tunc pluviam magnam significat
interdum in orientali et tunc serenitatem. interdum in
occidentali et tunc pluviam parvam et ideo dicit quod dicta Yris
mutat sepe contratas.

	|61. fa prova.~  Nam tunc dicitur velle liberum cum
congruunt simul voluntas et appetitus.

	|64.  Prima vuol ben.~  Et est sciendum quod secundum
phylosophum 3.[ -- ?] de anima voluntas est appetitus cum
ratione.  Unde etiam in VI.[ -- ?] ethicorum ait.  Electio autem
appetitus consiliarius propter hoc rationem veram esse et
appetitum rectum appetitus vero quem auctor vocat. hic talentum
dicitur affectus sine ratione ut supra in inferno. ibi -- |che la
rasgione sommettono al talento.~ itaque cum peccamus non
voluntate sed appetitu delinquimus et ideo dicit dicta umbra quod
anima existens in purgatorio ante tempus implete rei
satisfationis bene vellet celum adscendere voluntate absoluta
predicta sed justitia divina facit quod dictus appetitus sicut
fuit in hoc mundo contrarius dicte voluntati in peccando ita in
purgatorio contrarius est voluntati in relevatione pene.

	|82. buon Tito.~  Iste thytus vespasianus imperator
imperari cepit anno Domini LXXXII.[ -- ?] et tota iudea victa.
ultimo cepit Ierusalem cum cede et strage Iudeorum immensa nam ut
refert Josephus ultra centum milia Iudeorum mortui sunt ibi et
eam submisit populo romano. judicio dei summi regis in vindictam
ejus filii ab ipsis crucifixi de quo dicam in proximo capitulo.

END OF CANTO: Anne Marie Booye 10/20/86

