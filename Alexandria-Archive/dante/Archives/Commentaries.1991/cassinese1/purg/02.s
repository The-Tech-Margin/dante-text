|PURGATORIO CANTO 02~

PURGATORIO II

CHIOSE MARGINALI I.

CHIOSE SINCRONE

	Fingit auctor quod iste mons cum dicta ejus planitie est
directe oppositus illi regioni ubi est yerusalem civitas que
communiter dicitur esse in medio terre habitabilis orizon enim
est ille circulus qui dividit celum in duas partes quarum una
tota videtur et appellatur emisperio desuper altera pars latet
nos et appellatur emisperium desuptus itaque si predicta loca que
sub opposita habent eumdem orizontem de necessitate sequitur quod
quando in uno apparet sol in altero oritur nox. et so. tendit ad
occasum.  Item est sciendum quod emisperium supernum dividitur
per unam

	semicircullum in duas partes una totaliter est versus
orientem altera versus occidentem qui semicirculus appellatur
meridianus eo quia vadit sol. vel alte vel depresse semper quando
tangit circulum illum est meridies.  Item est sciendum quod sol
et nox ei opposita tunc sunt in libra quando tantus est dies
quanta est nox. sed cum dies crescit. ut est de mense martii nox
exit de libra quando vero non crescit. nox exit de libra. ut est
de mense Septembris.

	|1.  orizonte.~ orientalem et erat in ortu alterius
emisperii.

	|17.  un lume.~ idest.  Angelus dei quem describendo
ponit ipsum in facie rubicundum. ut aparet stella martis. hic ei
simulata. et cum alis albis.

	|31.  sdegnan.~ eo quia rationes humane non sunt
sufficientes ad celestia investiganda.

	|34.  Vedi.~  Hic denotat fragfilitatem humanarum artium
que mutantur ad tempus sicut in nobis mutantur pili. nam primo
habemus capillos modicos deinde habemus multos et coloratos
deinde habemus canos et paucos et demum totaliter resolvuntur
quod non est sic in pennis et pilis celestibus. idest. in
scientiis et artis divinis.

	|40.  venne a riva.~ et per hoc videtur quod sensus
humanus non potest comprehendere ultra suam proportionem.

	|44.  per iscripto.~ nedum verus et vivus videbatur res
santa et beata set etiam si fuisset descriptus talis qualis erat
apparuisset beatus.

	|46.  In exitu.~ quem salmum cantavit populus dei quando
exivit de manibus pharaonis et ivit in terram promissionis qui
egiptus interpretur hic status peccatorum deducens homines ad
Infernum.  Et in exitu interpretatur liberatio animarum a statu
predicto que sunt in via salvationis. idest. venture ad
salvationem. idest. promissionem.

	|91.  Casella mio.~  Quasi dicat propter compostionem
poematis presentis Deus miserebitur mei et liberabor a perpetua
dampnatione et revertar huc hic dicit quod negligens fuit in
mundo et tandem in fine suorum dierum opus composuit meritorium.
Iste Cassella fuit florentinus et optimus intonator Cantilenarum
qui pluriens intonavit cantillenas auctoris et fuit optimus
cantator -- |93. tolta.~ quasi dicat quomodo est.  quod jam est
tantum tempus quod tu decessisti. et nunc venis ad te purgandum
quomodo potest esse hoc quasi ammirative.

	|96.  negato.~  Eo quia tanto tempore quanto fecisti in
vana delectatione tanto stetisti extra purgatorium.

	|100.  volto.~ i. ut ego perpendi de mea vanitate volvi
me versus romam. idest. versus precepta ecclesie ipsis me submisi
et sic fui liber ab eterna perditione. -- |102. da lui.~ auctor
hic primo ipsum Angelum pro motu angelico accipit qui trahit
homines de egipto. idest. de servitute peccatorum ad statum
penitentie et purgantionis per mare. idest. per fluctus et
amaritudines terrene concupiscentie et volumptatis. ut tractus
fuit populus ysrael de servitute de servitute pharaonis de egipto
in qua steterat. per |LXX.~ annos per mare rubrum. ut in libro
exter dicitur secundo accipit pro vero Angelo quasi velit sentire
quod Angeli ut ministri dei animas purgantes. se in hoc mundo in
abditis. idest. remotis receptaculis ut ait Augustinus de anima
pasquasii ducant ad verum et universale Purgatorium cito et tarde
secundum exigentie divine justitie.

	|103.  A quella.~ includit quod sine directione sancte
matris ecclesie. et ejus mandatorum nemo possit ire ad savationem
cum dicatur mater fidei. et relegionis.

	|112.  Amor.~  Fuit quedam cantillena auctoris intonata
sive notata per dictum Casellam.

	|119.  veglio honesto.~ sc. cato qui interpretatur
sollecitudo opposita negligentie. vel virtus honestatis
abominantis talia adire.

	|122.  monte.~ i. ad penitentiam. nam sicut mons ducit
nos ad celum ita penitentia animas ad paradisum.

	|133.  tosta.~ per dicta duo capitula potest comprehendi
quod existentem in peccatis et volentem ab illis discedere
oportet primo quod sit humilis et se supponat penitentie deinde
quod penitentiam solicite exerceat semper fidelis christianus
existens.

