|PURGATORIO CANTO 10~
|CANTO X~

	|7.  pietra fessa.~ pro quibus lapidibus sic se
moventibus sentit allegorice. scilicet. quomodo diversis motibus
teptationum in principio statu penitentie homo in hoc mundo
diversimode fluctuatus temptationibus debet eis cedere.

	|22.  Da lasua.~  Hic incipit tractaare de primo circulo
purgatorii in quo fingit auctor puniri animas superborum.

	|34.  scorno.~  Erant ibi picte sive sculpte iste
ymagines importantes bonum virtutis humilitatis effectum qui
superbi hoc videntes magis afficiebantur et dolebant dictam
virtutem non fuisse secutos quam fuisse secutus ymagines superbie
scultas in dicto pavimento quod moraliter et spiritualiter potest
accipi.

	|52.  Unaltro storia.~  Legitur secundo Regum VI.[ -- ?]
capitulo quod dum semel arca sancta reduceretur de civitate gabaa
ad yerusalem cum thure et. VII. choris sacerdotum ac sinistrasset
in via currus ducens eam substentata fuisset a quodam nomine oza
dicta arca mortuo ibi statim dicto oza ex eo quod non erat sibi
tale officium commissum ut dicit hic auctor.

	|68.  Micol. Uxor david et filia saul regis despexit
ut superba dictum david ejus virum tam humilem dicens ei o quam
gloriosus fuit hodie rex ysrael discoperiens se ante ancillas
suas cui tunc david ita respondit ludam autem eum qi elegit me et
vilior fiam plusquam factus sim et ero humilis in occulis meis.

	|73.  Quivera.~ scrivit Elinandus ystoricus cum trajanus
cum suo exercitu ad bellum pergeret vidua quedam illi occurrit
plorando dicens obsecro ut sanguinem filii mei innocentem occisi
vendices cumque ipse imperator si incolumis reverteretur se ei
vindictam facturum sponderet vidua respondit et quis mihi hec
prestabit si tu in prelio moriaris et alia verba que hic sunt in
textu scribit ibi ille Elinandus quomodo aute anima hujus trajani
de inferno reversa est ad corpus precibus sancti gregorii. ut hic
dicit dicam in paradiso capitulo XX. et omnes supradicte ymagines
et actus habent representaare maximam humilitatem.

	|124.  vermi~ et informes remota anima sicut vermes
sirici remoto papilione qui dicitur florentie farfalla qui etiam
nascitur ex eis.  Item quomodo etiam sic remanemus attoma. illa.
scilicet. corpuscula informia que agitatur per radium solis
transeuntem per aliquam umbram in defectu.  idest. deficiente
nobis spirtu.

End of Canto: Joyce Han 1/13/87

