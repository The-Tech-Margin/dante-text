|PURGATORIO CANTO 15~
<Inferno> Canto 15
Chiose Marginali
I.
Chiose Sincrone

	Auctor volens ostendere. quomodo in illo emisperio in quo
fingit se esse sol distabat ab ejus occasu per tres horas dicit
quod tantum restabat ibi de cursu solis quantum apareret de spera
ipsius ab ortu suo usque ad finem tertie hore in quo spatio
temporis dicta spera solis semper aparet in terra sive in pariete
moveri et tremula quasi ut est puer ludens ut hic dicit unde
poete finxerunt solem habere quadrigam eo qoud quadruplici limite
diei metitur spatium. unde ejus equus primus dicitur cous.idest
rubeus cum sol. primo rubicundus oriatur. secundus dicitur acron
quod omterpretatur splendens eo qoud terne hore momentis
insistens lucidior fulget tertius lampas eo quod in meridie magis
flagrat quartus phylogeus quod grece terram amans dicitur quod
hore none proclivior urgens occasionibus pronus incumbit et sic
ad propositum dicit auctor quod ibi in illo emisperio erat hora
vespertina et in ytalia subaudi erat media nox et in ierusalem
erat hora matutina unde bene sentit quod dicit auctor. quod tunc
in illo monte purgatorii respiciens versus occidentem in dicta
hora verspertina percutiebatur a radiis solis in naso. idest. in
facie et per consequens reflexio solis radiantis in vultum
lucidum ut speculum illius alterius angeli quem fingit auctor se
ibi reperiisse debebat ipsum auctorem repercutere in facie et hoc
affirmat tali ratione naturali sol cum directo jacet deorsum
lumen suum et sic cadit directo ut cadit lapis si proiciatur de
supra orso equali ictu ut comparative dicit hic quod puntum
dicitur cenit nullam umbram recipit et per consequens nulla
reflexio tunc ab eo procedit sed cum a dicto punto cenitico
meridiano universali recedit ex parte occidentali vel distat a
parte orientali percutiens in aliquod corpus lucidum ut aqua et
speculum facit suam reflexionem qui radius secundus ita reflexus
tantum ascendit potentialiter quantum primus descendit ut etiam
patet in aliqua aqua descendente per aliquod conductum vel meatum
que tantum ascendit quantum descendit ex quibus satis possunt
intelligi que hic tangit auctor in textu.

	|34. angil beneditto.~ qui erat in fine secundi circuli
et in principio tertii -- |35. intrate quinci.~ nam exibant de
secundo circulo et intrabant in tertio in quo purgantur anime
iracundorum.

	|38. beati misericordes.~ qouniam ipsi misericordiam
consecuntur.  ut Mathey V.[ -- ?] capitulo -- |39. gode tu che
vinci.~ que verba scribit apostulos ad romanos[.?] |12.~ capitulo
ibi noli vinci a malo sed vince in bono malum vincitur a malo
dicit ibi glossa qui malus efficitur et malitie se subicit per
que verba ita prolata auctor nil aliud vult tangere nisi quod
homo mundatus a premisso vitio invidie et deductus ad virtutem
caritatis proximi. et factus misericors ejus potest vere dicere
hec verba in mente sua.

	|51. sospiri.~ quasi velit auctor dicere quod quia
homines ponunt eorum affectum ad res mundanas que minuuntur
consorte. idest.  participante dictarum rerum evenit postea quod
invidia in hominibus regnat quod secus est in celestibus rebus
que augentur consorte. idest.  participe et non minuuntur.

	|79. sieno spenti.~ dicit enim quod sicut plage mortales
habent corpus hominis interimere ita peccata mortalia habent
interimere animam nisi solidentur cum medecina penitentie et
dolore conscientie.

	|83. laltro girone.~ sc. tertio in quo fingit puniri
animas iracundorum et volendo se referri sub sensu spirituali et
morali ad iracundos in hoc mundo viventes ad se purgandum in hoc
mundo a tali vitio iracundie quomodo primo debent se dare ad
contemplandum bonum illius virtutis que contrariatur dicto vitio
ire. que est mansuetudo secundum phylosophum dicentem in quarto
ethycorum Mansuetudo autem est medietas contra iras fingit in
extatica visione in qua est ille qui contemplando a statu suo
astraitur nam dicitur extasis ab ex quod est extra et stasis
status ut erat demaritus in ortulo suo semel qui capras
depascentes caulos ante se non videbat sibi apparuisse in
principio hujus tertii circuli ymaginem virginis marie in illo
actu mansueto in quo fuit dum reperuit filium puerum exeuntem de
templo ei dicendo filii quid fecisti nobis ecce pater tuus et ego
dolentes querebamus te. ut luce capitulo II.[ -- ?] Item ymaginem
ephysistrati olim ducis civitatis athenarum qui ut ait valerius
in titulo de humanitate et clementia libro 5.[?] dum ejus uxor
coram eo fecissit duci quemdam Juvenem qui osculatus fuerat quam
eorum filiam puellam amore provocativo ut confessus fuit dictus
iuventis credens mori absolutus est ab ipso duce dicente contra
dictam ejus uxorem si eos qui nos amant occidamus quid illis
faciemus quibus odio sumus.  Item ymagninem sancti stefani
protomartiris christi qui dum lapidaretur dixit verba que in
textu continentur ut in actibus apostolorum capitulo VIII.

	|98. tanta lite.~ nam magna lis fuit inter palladam et
netumpnum de nomine imponendo civitati athenarum. nam neptupnus
volebat nominari eam suo nomine et pallas suo tandem sancitum est
quod nominari deberet ab illo istorum deorum qui felicius
prodigium ibi monstraret ex quo netumpnus cum suo tridente
percussit terram et emicuit equus ut ait virgilius in primo
qeorgicorum munera vestra cano etc. pallas etiam percussit ibi
terram et emicuit oliva quo prodigio magis fausto censito.
vocata fuit dicta terra athene ab ipsa pallade que etiam athena
nominabatur quod sonat scientiam et ideo dicit auctor quod inde
emanavit omnis scientia et doctrina ut ait priscianus.

	|117. falsi errori.~ quasi dicat quod usque tunc non
cognoverat ita per effectum bonitatem dicte virtutis
mansuetudinis sicut nunc.

	|131. pace.~ idest. christi qui pacem tantum dilexit quod
omnis ejus salutatio erat pax vobis pacem meam do vobis.

END OF CANTO: Anne Marie Booye: 10/07/86

