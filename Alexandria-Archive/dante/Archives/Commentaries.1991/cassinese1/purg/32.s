|PURGATORIO CANTO 32~

PURGATORIO XXXII

CHIOSE MARGINALI I.

CHIOSE SINCRONE

	|2.  decenne sete.~ que pro deccenali vacatione a studio
pratice theologie allegorice ponitur. quibus X. annis auctor in
dicta pratica theologie non studuerat.

	|8.  quelle dee.~ sc. virtutibus theologicis. et ideo
volvi visum quia audivi ab eis virtutibus totum istud. videlicet.
^Untroppo fixo.~ quasi dicat quod iste virtutes videntes auctorem
ita fixe respicere beatricem clamaverunt alta voce nimis fixe
respice beatricem que clamatio fecit me volvere versus sinistram
meam et desteram currus. ubi erant iste tres vitutes sic
clamantes.

	|16.  esser rivolto.~ quasi de veteri ad novam ecclesiam
previsam a Joanne in apocalipsi. III. capitulo. quasi dicat quod
auctor tamquam homo purgatus a vittis in hoc mundo in statu
virtuoso existens mediante vita activa debet agere et meditari ut
prudens et iustus et debet erigere mentem suam visa origine
nostre ecclesie militantis prenuptiata a prophetis. ut supra
dictum est ad videndum et contemplandum qualiter iam existens per
christum auctorem disposita sit ante et post ejus passionem --
|17.  et tornarsi.~ revolverunt se contra cursum fluminis versus
ortum solis.

	|36.  beatrice scese.~ de curru post passionem christi et
venit in hunc mundum.

	|41.  fora dalindi.~ qui habent arbores mire altitudinis
ut ait Virgilius in georgica. aut quos occeano etc.

	|43.  Beato se.~  Quasi allegorice voluerit auctor dicere
in hoc passu quod christus renovata dicta sua ecclesia militante
per ejus redemptionem ad statum restitute virtutis obedientie eam
reparavit sua nova lege evangelica quoam arborem sic siccam et
mox factam frondiferam auctor accipit hic moraliter pro ligno
scientie boni et mali in qua arbore sicut prevaricatio primorum
parentum fuit inobedientia et superbia ex qua desiccata potuit
dici fuisse ita obedeintia christi et humilitas reparavit in
nostram vitam et salutem -- ^non discindi.~ excedendo in appetitu
iusti et comodi ut primi parentes fecerunt de quo tali appetitu
et cur arbor sit ita excelsa dicam infra in sequenti capitulo.

	|53.  mischiata cum quella.~ luce. idest. cum signo
arietis quod signum sequitur signum piscium quod est in medio
mensis martii.

	|55.  si rinnovella.~  Actribuunt enim poete ipsi soli et
ejus currui quatuor equos primus Eous. idest. robicundus cum tali
colore sol primo surgat.  Secundus Acreon. idest. splendens. ut
est in tertia hora lucidior suis momentis.  Tertius lampas eo
quod ardentior in meridie fervet.  Quartus phylogeus. idest.
amans terram quod in ultima parte diei contingit.

	|65.  Gli occhi spietati.~  Scribit ovidius in primo
quomodo Jupiter iacuit cum quadam nomine Yo. ex quo Juno
transformavit in vaccam et eam dedit custodiendam Argo habenti
centum occulos quam volens ab eo liberare Jupiter. de mandato
ipsius Jupiter mercurius adivit dictum Argum et coram eo cepit
sonare dulcissime cum quadam eius fistula et querente dicto Argo
unde illam habuerat dictus Mercurius cepit dicere quomodo quedam
nimpha de ortigia nomine siringa iam adamata fuit a quodam deo
nomine pan. quem fugiens dicta siringa veniens ad flumen ladonis
non valens illud transire conversa est ibi in arundinem de cujus
calamis dixit Mercurius habuisse dictam fistulam in qua tali
recitatione dictus Argus cepit soporare et elaudere occulos in
sompnum tunc Mercurius occidit eum et dictam Yo liberavit.

	|62.  surgi che fai.~  Auctor facit comparationem hic de
se excitato a Matelda sicut fuerunt a voce christi petrus Jacobus
et johannes quod ut Matthey 18. capitulo. ipse dominus duxit eos
in montem excelsum et transfiguratus est ante eos et resplenduit
facies eius sicut sol. et vestimenta eius sicut nix facta fuit et
apparuerunt ibi Moyses et elya. et nubes obumbravit dictos
discipulos de qua vox quedam exvit dicens hic est filius meus
dilectus in quo bene mihi complacui quam discipuli ita audientes
ceciderunt in faciem eius quibus inde christus dixit surgite ut
laczaro in morte dormienti quod tangit hic auctor dicendo quod
dicta vox christi maiores sompnos excitavit et levantes occulos
non viderunt nisi yeshum. quod totum fuit ostendere. flores meli.
idest. arboris mali pomiferi celi. idest. signa deitatis que in
ipso christo ut dicit textus.

