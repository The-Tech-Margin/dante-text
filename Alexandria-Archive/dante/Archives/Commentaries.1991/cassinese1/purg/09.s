|PURGATORIO CANTO 09~
|CANTO IX~

	|1.  La concubina.~  Notandum est quod ystorice dicitur
quod laumedon troianus tres habuit filias scilicet.  Assaracum
tytonum et priamum quem tytonium poete fingunt auroram solis
habuisse in uxorem, et ideo ab eo vocata est tytonia unde ovidius
de faustis.  Tu frigis Assarici tytonia fratre relicto et subdit.
Jam tua laumedon nurus ortaque noctem pelli, etc.  Ac etiam
fingunt dictum tyton ex ea habuisse filium nomine Menonem quem
tyton etiam fingunt dicti poete fortiter senuisse et ideo auctor
hic vocat eum antiquum qui tyton tenebat in concubinam auroram
lune quem tyton poete ponunt pro illo vapore qui colorat tramque
auroram licet non ita continue auroram lune sicut solis et ideo
vocatur istius maritus et illius amicus et sic auctor non vult
aliud dicere nisi quod luna oriebatur et erat in signo scorpionis
et ubi dicit.  ^Et la nocte dei passi etc.~ vult dicere quod nox
infecerat duos passus tunc et erat in tertio nox enim dicitur
facere VII passus. idest. habere. VII partes nam habet
crepusculum conticinium gallicinium intempestum gallitium
Matutinum et dilluculum quorum iam transiverat duos. scilicet.
crepusculum et conticinium et erat in tertio.  scilicet. in
gallicinio.

	|12.  cinque.~ scilicet. virgilius Sordellus Judex ninus
Marchio corradus et auctor.

	|13.  tristi lai.~ Scribit ovidins in VI. quomodo Terreus
olim rex tracie accepta in uxorem progne filia pandionis ducis
Athenarum carnaliter jacuit et per vim cognovit phylomenam ejus
congnatam et sororem dicte progene et filia dicti pandionis in
quodam nemor dum semel duceret eam de Athenis ad dictam ejus
conjugem et ne tale scelus sciretur linguam dicte phylomene
amputavit et ipsam ligatam dimisit in dicto luco ad quam dam
arborem ut a feris bestiis devoraretur breviter tamen hoc scito
recepta dicta phylomena secum occulte dicta prognes tali dolore
mota occidit ythym filium suum et dicti Terrei infantem et eum
coquid et secrete eum dedit ad comedendum dicto ejus patri uno
mane ituro venatum quo recepto insequente dicto terreo dictas
sorores dicta prognes conversa est iin irrundinem et dicta
filomena in avem sui nominis et dictus Terreus in avem nomine
uppupam cristatam et hoc quod dicit auctor de primis guais dicte
hirrundinis.

	|18.  sue vision.~ iuxta illud Ovidii.  Namque sub aurora
jam dormitante lucerna somnia quo cerni tempore vera solent --
|19. una aquila.~ quam aquilam nominatam luciam auctor hic et
supra in inferno, II, capitulo allegorice accipit gratiam
cooperantem que subsequitur gratie operanti et de hoc vide quod
dixi supra in dicto capitulo, II.

	|23.  Ganimede.~ Servius scribit super primo Virgilii ubi
dicit rapti ganimedis honores etc.  quod hebes filia Junonis diu
fit in officio propinandi vinum sive potum giovi in celo tandem
visum est sibi Jovi non fore decens quod talia per feminam
fierent unde disposuit se ad illud officium habere hunc ganimedem
tamquam nobiliorem et pulchriorem iuvenem de mundo unde cum una
die dictus ganimedes venatum ivisset cum suis multis comitibus
super montem yde juxta trojam.  Jupiter in forma aquile illum
rapuit ad celum et eum ad dictum officium constituit.  Veritas
est quod Jupiter lascivus et luxuriosus dum semel cum exercitu
suo discurreret contra trojam tunc parvam civitatem cum alia
rapina quam fecit rapuit istum ganimedem et qua sibi placuit eo
quia pulcerimus puer erat duxit secum in creta et eum fecit suum
pincernam. idest. propinatorem vini quod bibebat et eum tenebat
in lecto sicut uxorem modo dicitur quod eum transtuplit in celum
veritas est quod post mortem ganimedes fuit consacratus et
appropriatus. VIII. de. XII. signis celestibus. scilicet.
acquario et ideo acquarius qui semper pluvit dicitur pincerna
celi. idest.  Jovis.

	|30.  me rapisse.~ Auctor visis conditionibus illorum qui
per contritionem extremam salvati sunt de quibus huc usque dixit
in hoc secundo libro volens nunc contemplative ascendere ad
tractatum illorum qui ad veram confessiiinem in hoc mundo
viventes veniunt ad se purgandum ut ad verum purgatorium quodam
fingit dormiendo. idest. contemplando ita se nunc elevaari hic
per hanc talem figuratam aquilam ut per gratiam dei cooperante
predictam.

	|37.  Schiro.~ insula in qua habitabat licomedes qui
multas filias habuit inter quas thetis posuit achil. lem -- |39.~
^dipartiro.~ et quomodo illum inde duxerunt dixi supra in inferni
capitulo 26.

	|58.  e laltre genti.~ sc. domnus Corradus malaspina et
Judex ninus -- ^genti forme~ -- alias. ^gentil forme.~ -- vel.
^for me.~ idest omnes remanserunt in circulo inferiori me excepto
qui veni tecum.

	|76.  tre gradi.~ fingit auctor quod per tres gradus nos
oportet venire ad istam porta primo per primum gradum quem fingit
esse nitidum et albissimum ad denotandum quod peccator volens
confiteri primo ante dictam confessionem debet reminisci omnium
que peccando commisit et introspicere ante confessiionem et ideo
auctor fingit eum tam lucidum ut speculum in quo se totaliter
videbat.  Item per secundum gradum quem fingit lividum et nigrum
et crepitus ad denotandum compunctionem scindentem cor nostrum in
tali casu.  Item per tertium quem fingit esse rubissmum ad
denotandum erubescentiam quam habere debet peccator in
confessione.  Vel possunt accipi tres gradus sicut accipiuntur in
decretis de penitentiis nam ibi dicit tribus modis deum
offendimus. scilicet. corde ore et opere ita per cordis
contritionem oris confessionem et operis satisfactionem ut per
tres gradus venire debemus ad veram penitentiam -- ^porta.~ quam
allegorice ponit pro introitu status confessionis et penitentie
ad quem in hoc mundo viventes actu et proposito primo dirigit
mihi a gratia dei predicta figurata in predicta aquila et lucia
et quia ad hanc talem portam itur per gradus cum nemo repente fit
summus ut ait Juvenalis ideo dicit de eis ut infra -- |78.~
^portier.~ quem Angelum auctor accipit hic pro sacerdote.

	|82.  spada nuda.~ que pro sacerdotali offitio accipitur
sive Judicio in quo sacerdos debet ferire peccatorem cum Justitia
et misericordia.

	|112.  discripse.~ i. memorem eum fecit ipsum examinando
et in hac tangitur doctrina alios confessantium.  |116.~
^vestimento.~ quod erat vile et per hoc redarguit sacerdotes
indutos de pretiosis vestibus.

	|119.  Luna era doro.~ pro clave aurea accipit hic auctor
auctoritatem sacerdotalem sine qua non potest homo in hoc mundo
absolvi. nam sacerdotibus remictentibus deus remictit pro
argentea accipit prudentiam et districtionem sacerdotalem nam
oportet eum cognoscere quicquid iudicat sit diligens inquisitor
sapienter interroget a peccatore quod forsan ignorat vel
verecundia tacet et factis diligentibus interrogatinibus sit
postea benivolus.

	|132.  di fuor torna.~  Hic ponit aliud documentum vel
perdicta quod debet facere sacerdos videlicet quod homo
perseveret in bene operando alias talis confessio nil sibi
prodesset.

	|136.  Metello.~ ut ait lucanus in 3.[ -- ?] dicitur eim
quod cesar remoto Metello consule a dicta porta tarpeje traxit de
dicta tarpeja 7125. pondera auri et DCCCC. milia argenti.

