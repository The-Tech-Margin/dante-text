|PURGATORIO CANTO 12~
|CANTO 12~

	|28.  Briareo.~ qui briareus gigans fuit fulminatus a
giove dum in grecia in contrata flegre bellare voluit contra deos
cum aliis gigantibus ibi occisis a dicto iove.

	|34.  Timbreo.~  Apollo dicitur tymbreus a timbrea erba
nascente iuxta templum ejus qui simul cum pallade et Marte ejus
fratribus et filiis dicit iovis fuerunt ad fulminandum dictum
briareum cum aliis gigantibus ut dixi in inferno capitulo 14.

	|34.  Nembroth.~  Legitur genesis capitulo X.+0~ et
XI.+0~ quod Noe habuit tres filios scilicet. clam.  Sem et Japhet
de quo cham. descendit iste Nembroth. qui ut recitat magister
ystoriarum sua superbia cepit esse potens in terra et robustus
venator hominum coram domino. idest. opressor hominum affectu
dominandi ita quod dominator fuit super omnibus descendentibus
dicti sui patris et idem fecit Jethan filius dicti sem super ejus
descendentibus et etiam suphen filius dicti Japhet qui
descendentes Noe erant adhuc eo vivente. numero XXIIII. milium.
qui dicti tres fratres ut duces eorum convenerunt dictam gentem
in quadam contrata dicta sanar et ibi dictus nembroth. cepit
facere turrim quamdam ascensuram usque ad celum. ne iterum
deluvium offenderet ex quo deus descendit ibi confundens linguam
eorum ita quod nullus alium intelligebat.

	|37.  Nyobe.~ ista Nyobes filia tantali et uxor amphyonis
regis thebarum dum semel fieret festum quoddam latone sua
superbit egit ut scribit ovidius in VI. de ea dicens constitit
atque occulos circumtulit alta superbos inquit querite nunc
habeat quam nostram superbia causam quoquo modo audetis genitam
titanida ceo latonam proferre mihi etc. ex quo apollo et diana
missi a dicta latona eorum matre suis sagiptis occiderunt omnes

VII. filios masculos dicte Niobe ludentes equester una die in
quodam campo et VII ejus filias etiam unde dolore tali dictus
amphyon se occidit et dicta Niobe effecta est saxum.

	|40.  Saul.~ habetur ad licteram in primo regum capitulo
XV dictum est ei nonne cum parvulus esses in occulis tuis capud
in tribu israel factus es qui superbus factus a regno dejectus
est nam lege primo regum capitulo ultimo quod dictus saul victus
a philisthin et occisis in bello ab eis ionatan et aminadab et
melchisue suis filiis dolore irruit in proprium ensem super
montem gaboe et ibi mortuus est quem montem david postea
maledixit umquam ibi caderet pluvia vel ros et ita evenit et fuit
ut legitur secundo regum capitulo primo.

	|43.  Aragne.~ que conversa fuit in araneam per dictam
palladem cum qua certare voluit texendo cujus ystoriam ponit
Ovidius in sexto de ejus tela lamata -- |44. Roboan.~ iste
roboan filius salamonis dum esset creatus rex ierusalem mortuo
ejus partre populo roganti eum ut exoneraret eum collectis
respondit superbe minimus digitus meus grossior est dorso patris
mei pater meus aggravavit jugum vestrum ego vero addam post quam
responsionem misit uriam tributorum exactorem suum ad exigendum a
dicto suo populo collectas et lapidatus est ab ipso quo viso
dictus roboan ascendit festinus quem dam currum et fugit in
yerusalem et recessit israel a domo david ut III.  Regum XII.
capitulo.

	|50.  Almeon a sua Madre.~ herifilis uxor amphyarai et
mater dicti almeonis quadam superbia pro quodam ornamento
infausto recepto ab Argia uxore Polinicis indicavit dictum ejus
virum latitantem ne iret ad exercitum thebanum cum aliis regibus
grecis de quo dixi in inferno capitulo XIIII.+0~ nam presciebat
se ibi moriturum ut mortuus est et etiam dixi in capitulo XX. ex
quo almeon eorum congiugalium filius dictam ejus matrem occidit
de precepto dicti sui patris ut scribit ovidius in VIIII. et
statius in IV. et in secundo de infortunato tali jocali sive
ornamento dixit supra quomodo venus uxor vulcani jacuit cum marte
et ex eo genuit quamdam filiam nomine hermionem que maritata est
cadmo. primo regi thebarum dum pararetur ire ad nuptias dicta
venus rogavit vulcanum ut ei fabricaret aliquod ornamentum qui
memor dicti adulterii quoddam ornamentum taliter infaustrum quod
dicta hermione et quecumque alia que illa habuit de domo dicti
cadmi infortunata fuit ut hic dicitur.

	|53.  Sennacherib.~ lege ad licteram de superbia
senacherib ysaye

XXXVI. et VII. cui dictum est obsidenti ierusalem capta alia
terra iudea superbia tua ascendit in aures meas ponam circulum in
naribus suis sive tuis et reducam te per viam qua venisti et
percussit inde angelus domini ejus castra et octuaginta quinque
assyrii mortui sunt ibi et ipse senacherib rex assiriorum
reversus est et dum in civitate ninive adoraret ydola sua in
templo filii sui eum ibi occiderunt.

	|56.  Thamir.~  Cyro rex persarum fuit victus in bello a
thamira regina scitharum ut scribit solinus in primo et valerius
in VIIII. in titulo de ultione et scripsi superius in inferno
capitulo 17. et dicta thamiris fecit sibi truncari capud et poni
in utre sanguinis humani dicendo sanguinem ististi et sanguinem
bibe.

	|59.  Heliferne.~ holifernus dux nabuchodonosoris regis
assyrorum 14. ut legitur iudit capitulo VI. dum civitatem
bettulie obsideret et ad extremum dusisset decapitatus astute a
dicta iudith probissima muliere sub proprio temptorio sub
simulatione concubitus et cum ejus capite super hasta posito ipsa
cum aliis civibus dicte terre bettulie egressa in conflictu
posuit exercitum dictorum assiriorum quod autem fuerit superbus
scribitur etiam ibi quod contra achironem prophetam hec verba
protulit dicens quoniam prophetasti nobis quod gens ysrael
defensabitur a summo deo ut ostendam tibi quia non est deus nisi
nabuchodonosor cum percusserimus eos quasi hominem unum.

	|70.  Or superbite.~  Modo est videndum quid allegorice
auctor sentit pro supradicta pena purgatorii hujus vitii superbie
que purgatur sub primo pondere saxorum summissa et curvata
cervice et decet eum hic tali respectu dicere quod sicut homo in
hoc mundo levata cervice in superbia huc usque fuit ita nunc
volens a tali vitio expiari debet illam penitudinem infirmare cum
honore et dolore conscientie considerando quod illos quibus
superesse voluit superponere debet.

	|79.  Angel.~ qui pro motu angelico allegorice accipitur
qui semper de uno circulo ad alium animas deicit. idest. dimisso
vitio uno per penitentiam movet hominem ad dimictendum aliud et
ire ad virtutem illi vitio oppositam ut ire a vitio superbie ad
virtutem umilitatis.

	|100.  al monte.~ in isto monte in costa ejus gradus
lapidei qui adscendunt usque ad summitatem montis in cujus
summitate est ecclesia sancti Miniati fuerunt facti ut hic
dicitur bono antiquo tempore. scilicet.  quando quaterni. idest.
libri communis florentie et doga. idest. mensura dogata canipe
salis securi erant ubi abind[e?] antea vitiati fuerunt ut dicit
auctor infra in paradiso capitulo XVI.

	|110.  Beati pauperes spiritu.~ dicit glossa fore homines
humiles quasi velit auctor allegorice dicere congratulationem
quam homo sibi ipsi facit et sentit mundato animo ab aliquo
vitio.

End of Canto: Joyce Han 1/26/87

