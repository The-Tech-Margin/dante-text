|PURGATORIO CANTO 31~

PURGATORIO XXXI

CHIOSE MARGINALI I.

CHIOSE SINCRONE

	|1.  per punta.~ idest. rejprendendo me reprensione
directa -- |2.  per taglio.~ idest reprensione obliqua.

	|5.  se questo e vero.~ scilicet. quod me destitisti. qui
hominem in hoc mundo facio felicem in alio animam beatam per
alias affectiones mundanarum rerum.

	|40.  Ma quando.~  Dixit quod bene erat ei[?] notum
quicquid querebat sed cum proprio ore quis confitetur rota
voluntatis volvit se ad contrarium ut contingit beato gregorio se
confitenti ab Alexandro episcopo de quo idem gregorius inquid in
quadam epistola.  Omne in tuis auribus quod mihi de me
displicebat exposui.

	|51.  in terra sparte.~ quia temporalium rerum
benedictionem promictebat secus spiritus eius. idest. novum
testamentum.

	|54.  tuo disio.~ quod saltem non sequeris me in his que
temporalia erant. quasi dicat nulla.

	|55.  lo primo strale.~ quasi dicat post testamentum
vetus. bene debebas sequi testamentnm novum.

	|59.  o pargoletta.~ i. que proca fuit auctoris que pro
ar te poesis ponitur moraliter dicendo in quadam cantillena sua.
^Io mi son pargoletta bella et nova et son venuta etc.~ que
reprensio hic facta per Beatricem allegorice postest dici in
persona multorum teologorum dissuadentium ipsam poesiam et alias
mundanas scientias que reprensio fit contra illos qui ad
voluntatem non ad eruditionem errores gentilium tolant legunt
poetas.

	|63.  si spiega indarno.~  Juxta illud Salamonis dicentis
frustra iacitur rete ante occulos pennatorum et sic homines
barbati possunt in hoc.

	|70.  si dibarba.~ idest. vento affrico spirante de terra
et contrata ietulie in occidentali parte afriee ubi. iam Jarbas
rex regnavit.

	|84.  quandella cera.~ quasi velit dicere. quod ista.
Beatrix. cum fuit viva in mundo erat pulcerima omnium dominarum.
et mortum. idest. anima sua erat in alio mundo multo plus
pulcerima aliarum animarum. modo. fac sic constructum. videbatur
mihi beatricem anticam. idest. senem. sub suo velo et ultra
flumen letheum plus vincere in pulcritudine. scilicet.  metipsam
respectu pulcritudinis quam habebat dum vivebat quam vincere hic
alias dominas.

	|91.  Poi quandot cor.~  Auctor volens ostendere quomodo
in hoc mundo homo perfecte purgatus et correctus ita mediante
vita activa ut possit dicere verba que secuntur. sc. asperges me
domine mundabor et lavabis me et super nivem dealbabor ac etiam
ut oblitus omnium dolendorum dicat ultra quam apostolus ad
phylippenses. capitulo. III. dicens non ut iam perfectus sim me
arbitror aprendisse aut que retro sunt obliviscens moraliter
coniungitur virtutibus cardinalibus supra dictis teologicis et
per eas amori sacre teologie datur fingit se hic post dictam
reprensionem trahi per letheum fluvium oblivionis offerri dictis
virtutibus moralibus prius.

	|106.  Noi sian qui nimphe et nel ciel semo stelle.~  In
quantum hec virtutes considerentur in suo astracto et a mente et
a gratia divina mediantibus corporibus cellestibus infundi in
nobis possunt quasi ut stelle dici fore in celo in quantum vero
considerentur pro effectibus suis hic infernis possunt quasi dici
nimphe idest. virgines in virtute ita superantes humanitatem ut
quasi dee quedam in hoc mundo reputate sunt.

	|109.  occhi suoi.~ ut ad eius propriam pulcritudinem.
idest. ad licteralem intellectum sacre scripure ut ad ejus
tericam sed ad ejus spiritualem intellectum dict quod alie tres
acuent ejus occulos -- |110. aguczeranno ituoi.~ occulos
evangeliis et epistolis apostolicis tamquam profundius
discernentes in quo intellectu spirituali teologie fingit se
auctor vidisse grifonem. idest. christum deum nostrum et hominem.
ut viderunt apostoli in monte eum in transfigurantione. de qua
dicam in sequenti capitulo. ut sol. videtur in speculo.

	|116.  smeraldi.~ idest. coruam evangelistis. aliter
coram occulis beatricis.

	|122.  vi raggiava.~ et sic in occulis beatricis ut
perintellectum spiritualem teologie fingit se vidisse dictum
grifonem. idest deum verum et hominem.

	|136.  disvele a lui la bocca sua.~ idest. rideas. qui
risus est. secunda pulcritudo que allegorice potest accipi pro
illa secunda litera p. quod boetius vidit in veste philosophi.
que litera praticam sonat que pratica teologie per lectionem
sermones disputationes et predicationes.  Viva voce a doctoribus
teologicis tota die precipitur orentenus ut risus ab ore ex que
teorica dicta teologia magis elucescit.

