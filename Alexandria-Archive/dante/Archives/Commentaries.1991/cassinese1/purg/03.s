CANTO III.

CHIOSE MARGINALI I.

CHIOSE SINCRONE

	|3.  fida Compagna.~ i. ego redii ad meum consilium quo
instigante disposueram purgari a vitiis.

	|6.  la montagna.~ nisi voluntas mee rationis adfuisset.

	|7.  rimorso. eo quia cognoscebat se propter delitias
mundanas distulisse purgari.

	|25.  sepolto.~ nam virgilius obiit branditii et inde
fuit translatum ejus corpus neapoli -- |27. Branditio.~
civitate calbrie unde pro epitaphio scriptum est in sepulchro.
Mantua me genuit calabri rapuere tenet nunc parthenope cecini
pascua rura duces.

	|28.  ora.~ nam.  |VII.~ celi planctarum sive spere
posite inter nos et firmamemtum celi sive.  |VIII. spere non
tollunt nobis aspectum nec impediunt dicte |VIII.~ spere seu
stellati celi.

	|31.  Assoferir.~ respondet hic virgilius cuidam tacite
questioni que fieri posset videlicet si anime sunt incorporales
ut res aeree et impalpabiles quomodo ergo puniuntur et
cruciantur.  Ad quod respondet quod ad pationdum supplicia calida
et gelida similia corpora ut sunt auime et eis similia ut demones
illa virtus disponit que non vult hominibus aparere quomodo hec
agat. scilicet. virtus divina.

	|35.  lanfinita.~ i. viam infinitam divine sapientie que
substantialiter est una in tribus personis quantum ad trinitatem
divianam ex quibus auctor concludit hic. quod nos gens humana
simus i. esse debeamus contenti huic coniuntioni causali quia
circa predicta talia qui dicit quia. importat secundum
phylosophum in primo metaphysice scire effectum ignota causa sed
scire propter quod est scire effectum et causam quasi dicat simus
contenti videre effectum in talibus sine impossibili visione
cause nam ypocras et pythagoras induxerunt hanc legem discipulis
suis ut non essent ausi in sententiis suis interrogare propter
quod vult sed esset eis pro ratione dicentis auctoritas quanto
magis auctoritas divina pro ratione habenda est et subdit ergo si
humana ratio hic scruptari potuisset primi nostri parentes non
fuissent prevaricati precepta dei et per consequens non fuisset
opus incarnationis verbi divini in maria ut dicit hic in textu.
Ac etiam Aristotiles et plato et alii magni phylosophi et poete
dampnati in limbo inferni prevedissent adventum filii dei et
credidissent in ehristum venturum et sic salvati forent.

	|40.  E desiar.~ probat quod ratio humana non sit
sufficiens ad comprehendendum divinam potentiam nam si hoc esset
possibile aristotiles plato et virgilius comprehendissent aut
aliquis eorum aut omnes tres simul qui procuraverunt hec scire et
frustra tentaverunt eo quia erant humani.

	|43.  Io dico. phylosophi per unum modum omnia
considerant.  videlicet. per modum naturalem et ea que per
predictum modum eveniebant posse esse et aliter non et velle
considerare divina eo modo quo humana non est bona consideratio
quia non procedunt eodem calle et ideo isti philosophi in hoc
stulti fuerunt quia credebant eo modo argumentare in divinis quo
in humanis quod esse non potest et si tenuissent quod humanus non
fuisset capax ad divina comprehendenda eorum animus remansisset
in quiete et eorum desiderium est quietum quod externaliter erit
eis luctuosum.

	|49.  lerice.~  Erici. est quedam terra Januensium supra
marinam in lunisgiana -- ^Turbia.~  Turbia est alia terra in
principio provincie. versus Januensem districtum etiam supra mare
et inter istas duas terras sunt montes inascensibiles et asperi.

	|60.  venivan lente.~ per hoc notat hic auctor tarditatem
et negligentiam quam habuerunt isti in hac vita in veniendo ad
penitentiam suorum peccatorum.

	|66.  dolce figlio.~ i. non sit in te talis tarditas et
credas quod propter peccatum negligentie sic planeJrocedunt.

	|112.  io son Manfredi.~  Iste manfredus fuit filius
naturalis secundi federici imperatoris et fuit factus per eundem
ejus patrem. rex cicilie et apulie et hacbuit quamdam filiam
nomine gostantiam uxorem dommpni petri de ragona et matrem
dommpni federici de siciliia et dommpni Jacobi de aragona
filiorum dieti petri et fuit in vita sua quasi usque ad finem
discors cum ecclesia tantum quod excommunicatus fuit per dictam
Ecclesiam et missus fuit contra eum quidam legatus episcopus
consentie qui juravit quod oportebat eum pellere de regno cum
anxilio regis caroli qui erat comes provincie et fecit eum regem
sicilie et Manfredum privavit ut dicitur in capitulo |28.~
inferni et fuit in conflucitu cum tota gente sua ad ceperanum et
ibi vulneratus et dicit textus nepote di Gostantia eo quia
naturalis erat non sumpsit pronomen a patre federico sed a
Gonstantia ejus noverca dicendo se nepotem dicte contantie.

	|117.  si dice.~ i. dicas sibi quod ego non sum damnatus
et quod oret pro me.

	|118.  rotta. sc. quando fuit positus cum tota sua gente
in conflictu per carolum prope ceperanum ut dixti supra in
inferno capitulo |28.~

	|125.  Glemente.~ iste Manfredus fuit excommunicatus
tempore Urbani pape |IIII.~ et privatus per eumdem de dicto regno
et mortuus et sepultus in capite pontis juxta civitatem beneventi
et iterum excommunicatus a clememnte papa quarto successore dicti
Urbani et desepultus mandato dicti pape per episcopum consentie.
et dejectus extra regnum apulum et projectus prope flumen viridis
dirimentis apuleam a campanea et a terris patrimonii.

	|135.  verde.~ i. donec homo est in hac vita in qua licet
penitere de peccatis suis.

	|145.  Che qui.~ et sic nota per presentem capitulum quod
homo numquam debet desperare a gratia del et si coningit quod
cadat in contumaciam ecclesie quam citius potest reconfederet se
cum ea et qluod viventes in hoc mundo debent orare pro defunctis.
et maxime pro propinquis et amicis quia eis multum prodest. si
non sunt dampnati.

