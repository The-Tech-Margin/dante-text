CHIOSE MARGINALI I.

CHIOSE SINCRONE

	Auctor continuando se volendo dicere quomodo quasi in
occasu solis in illo alio emisperio apparuit ei iste ultimus
Angelus purgatorii per quasdam circumlucutiones hic talem horam
ita discribit ut dicit textus ad quod magis aperiendum notandum
est quod per mediam yndiam currit contra orientem quoddam flumen
famosum et magnum adeo quod dicit orosius quod ejus latitudo
trahitur per XX. miliaria. vocatum cujus fauce intrando occeanum
mare. sol. in equinoctiali tempore videtur oriri unde lucanus in

III. de ipso dicit.  Qua colitur ganges etc.  Item est notandum
quod per mediam ysaniam in nostro occidente currit aliud famosum
flumen versus occeanum predictum vocatum yberum super quo flumine
etiam equinoctiali tempore sol nobis occidit unde Ovidius in Vii.
Ter iuga phebus equis etc.  Modo ad propsitum si tunc sol in
signo arietis existens in hoc nostro oriente erat in circulo
meridiano. sequitur per consequens quod signum alterius libre
sibi oppositum erat in nostro oiccidente in media nocte et sic
dictus yberus fluvius cadebat idest. fluebat versus dictum
occeanum sub dicto signo alterius libre hoc dicit ad dexteram
arietis cujus signum dicitur etiam libra ratione eiusdem
adequantionis diei et noctis.  Item per consequens principium
diei esse debeat in Jerusalem existentem in medio hujus nostre
terre ut plene dictum est supra capitulo. 2. et 4. purgatorii.
Unde etiam principium noctis sive sero debebat incipere inillo
alio emisperio in illo monte opposito dicte Civitati Jerusalem
secundum fictionem hujus auctoris.

