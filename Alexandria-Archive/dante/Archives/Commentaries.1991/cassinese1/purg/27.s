vitiis ulterius procedendo habeat facere cum dicat Augustinus ad
perfectionem duo pertinent. scilicet. nos purgari prius a noxiis
inde impleri bonis nom non sufficit abstinere a malo nisi faciat
quod bonum est et dicit quod habet levare occulos ad stellas ut
fecit auctor hic et ruminari in eis. idest. debet mentis occulos
erigere ad virtutes que ut stelle nobis de celo radiant et
lucent. idest. a divina gratia veniunt et dirigere earum fructum
in nutrimentum. idest ineffectum ut per eas ad vitam activam
venire possit inde ad contemplativam in quibus duabus vitis tota
nostra felicitas et beatitudo consistit que activa vita in veteri
testamento prefigurata est in persona lie. predicte filie laban
et uxoris prime Jacob. et contemplativa in Rachele ejus lie
sororis et secunde uxoris dicti Jacob.  Est enim vite active
rejuectis vitiosis operibus et virtuosa per nos exerceri unde
Augustinus in vita activa homo existere debet virtutibus
serviendo ut per justitiam subveniant miseris per providentiam
precaveat sibi ab insidiis per foritudinem sciat ferre molestia
et per temperantiam sciat fugare delectationes.  Item ejus active
vite etiam est ut operando homo velit proximo prodesse.  Et si
vis clarius circa hanc allegoriam lie predicte intimare quid
auctor sensit vide quod scripsit circa finem sue monarchie inter
alia dicnedo duos fines deus homini proposuit intendendos
beatitudinem. scilicet. hujus vite que in operatione proprie
virtutis consistit jus vite que in operatione proprie virtutis
