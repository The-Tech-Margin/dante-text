	|50.  gittato mi sare.~  In quo nichil aliud sub
allegoria vult sentire nisi quod ipse auctor et alii hacetnus in
luxurie vito maculati veniendo virtualiter in hoc mundo vivendo
ad penitudinem et purgationem hujus vitii recongnoscendo quodmodo
in eo jam turpiter exarserunt.  Unde Osee. VII.  capitluo omnes
adulterantes quasi clibanus succensus a coquente non possunt hoc
facere sine adustione quadam conscientie et verecundie quod patet
per experientiam non nemo turpiter fornicatus cum aliqua persona
raro videbit illam quin non succendatur quodam quasi igne
inverecundia et erubescentia si correptus fuerit a tali vito
dolendo et purgando.  Item alludit forte auctor verbis Apostoli
dicentis Omne peccatum quodeumque fecerit homo extra corpusest
qui vero fornicatur in corpus suum peccat ubi dicit glossa cetera
peccata tantum animam maculant fornicatio non tantum animam sed
etiam corpus ibi enim non solum est verecundia spiritualis sed
etiam corporalis.  Ideo ipse auctor fingit in hoc circulo ultimo
purgatorii tantum et non in aliis corporaliter se passionatum
fuisse. vel potius auctor inelligit loqui tropolice. scilicet. ut
ad moralitatem viventium in hoc mundo ut ab eo evertant incutat
hunc tantum timorem ejus pene.

