PURGATORIO XXX

CHIOSE MARGINALI I.

CHIOSE SINCRONE

	|1.  Quandol septentrion.~  Aucotr figurative loquendo
comperat illa VII.  dona spiritus sancti que sunt in omnibus
aliis corporibus celestibus in illo celo impyrreo. manentibus
illis VII. stellis. que faciunt plaustrum in celo septemtrionali
que stelle sunt lucidiores omnibus aliis in illo celo
existentibus.  Modo contrue sic. quando setemtrion. idest. VII
candelabra que pro. VII. donis spiritus sancti ponuntur ^primi
celi.~ idest. impyrrei in quo dicta cnadelabra stant circa
deitatem tamquam stelle ^quod.~ sc.  septemtrion. primi celi
^numquam scivit occasum neque ortum nec velum alterius nebule.~
quasi dicat quod istud setemtrion primi celi. idest. VII dona
numquam oriuntur nec tendunt ad occasunti sicut alie stelle nec
umquam fuit obumbratum ab aliqua nube preter quam a nube culpe
quod fuit quando lucifer peccavit quia tunc illud celum fuit
aliqualiter obumbratum propter illud peccatum.

	|4.  Et che faceva.~  Et quod sc. septemtrion ^faciebat
illic quemlibet acortum eius quod ad eos pertinebat sicut ille.
qui girat timonem causa venieni ad portum facit infirmiorem.~
videlicet. respectur offitii. sc.  acortum eius quod ad ipsum
pertinet. nam omnes isti. qui precedebant currum vel erant cum
curru vel post faciebant solummodo id quod illa. VII.  candelabra
demostrabant. sicut omnes naute faciunt solummodo id quod
magister navis imperat et non aliud. ^firmum stetit.~ sc.
septemtrion. quod est in principio ^la gente verace.~
videlicet.XXIIII. senes. ^venuta.~ idest.  qui primo venerat
^inter grifonem et ipsum.~ sc. septemtrionem ^volvit se ad currum
tamquam ad suam pacem.~

	|10.  Et un dilor.~  Est advertendum quod inter priscos
prophetas et sanctos scriptores saloman fuit ille qui magis
prenumptiavit christum.  Ecclesiam militantem per eius passionem
et mortem in hoc mundo instituturum.  In canticis capitulo. IIII.
dum dicit loquendo in persona christi ad ecclesiam tota pulera
es. amica mea et macula non est in te Veni de libano sponsa mea
Veni de Libano Veni et hoc tangit hic auctor. ut de eventu dicte
ecclesie et stabilitate eius. dum fingit quomodo firmatis dictis
septem candelabris velut illis septem stellis septemtrionalibus
dicti seniores volverunt se ad dictum currum. idest. prophetias
suas retulerunt ad christum et ad dictam ejus ecclesiam
militantem illam jam prefigurantes. -- |11.  veni sponsa de
libano.~ monte. scilicet. iudee vel senitie qui candor
interpetratur ut ostendat ecclesiam christi per eius mortem
habitam in iudea venisse in mundum et candida columba sine ruga
et macula.

	|16.  basterna.~ que est quilibet currus pannis decoratus
secundum uguicionem.

	|21.  Manibus o date lilia plenis.~  Unde in Virgilio in
VI.  Tu Marcellus eris manibus date lilia plenis .       |22.  Io
vidi.~  Et est notandum quod quedam sacra scriptura fuit ante
ecclesiam orta. ut vetus et novum testamentum quod in eternum
mandatum est. quedam cum ipsa ecclesia ut sunt illa IIII.
concilia principalia. sc.  Nicenum constantinopolitanum Ephesinum
et calcedonense quibus christum interfuisse nemo fidelis dubitat.
scripta. 4. doctorum ecclesie ac aliorum sanctorum ecclesie.
quibus standum est usque ad ultimum quiddam post ecclesiam ut
traditiones ecclesie. decretales epistole.  Item opusculoa
sanctorum patrum que in sancta Ecclesia reperiuntur qui in nullo
ab ecclesia devirarunt.  Igitur. ad propsitum volens auctor
ostendere quomodo theologica scriptura mediantibus institutis
dictorum conciliorum et scriptorum prephatorum sanctorum
instituta et firmata dicta nova ecclesia in eam venit et cum ea
instituta est fingit hanc beatricem figurantem theologiam in
dictum misticum currum discendisse de celo in nube quadam
floribus stipatam et dictos Angelos proicientes flores pro
cherubinorum virtutibus qui precellunt alios sapientia et
scientia qui plenitudo scientie interpretantur.

	|31.  Sopra candido velo cinta dolivo donna.~  Contrue
sic literam.  ^donna cinta doliva sovra candido velo.~que habebat
sertum de oliva apparuit mihi -- ^oliva.~ arbor consecrata olim
minerve de sapientie.

	|42.  fuor di pueritia.~ videlicet. usque ad XV. annum
nam in ista sua pueritia forte de facto dedit se ad studendum in
veteri tesamento. quod est prima pars teologie. quod dicit infra
de prima parte teologie.

	|54.  non tornasser adre.~ cogitando quod Virgilius
recesserat. quasi non adhuc explicitus ab amore mundane scientie
rationalis pro quo in hoc passu.  Virgilium. accipit et beatricem
pro scientia divina.

	|70.  ancor proterva.~ quasi dicat quod protervitas non
est in verbis sed in actibus. unde in grecismo immprobus est
aliquis verbis factisque protervus.

	|83.  In te domine speravi.~ non confundar in eternum in
iustitia tua libera me inclina ad me aurem tuam accelera ut eruas
me est michi in deum protectorem et in domum refugii ut salvum me
facias quoniam refugium meum et fortitudo mea es tu et propter
nomen tuum deduces me et nutries me.  Educes me laqueo hoc quem
absonderunt quoniam tu es protector meus.  In manus tuas commendo
spiritum meum redimisti me domine aeus veritatis.  Odisti
servantes vanitates supervacue.  Ego autem in domino speravi.
Exultabor et letabor in misericordia tua quoniam respesisti
humilitatem meam salvasti de necessitatibus animam meam nec
conclusisti me in manibus inimici statuisti in loco spetioso
pedes meos que verba hucusque si david. fuisset in hoc ficto
cantu auctor non prius protulisset ad mictigationem
impetran[?]lam a beatrice.

	|88.  trapela.~ quasi dicat illa talis nix liquefit
dumodo meridiana terra spiret suos tepidos ventos in qua regione
est quedam terra vocata cyena ita sub tropico cancri et sub sole
ibi eminente quod nullam umbram facit ibi aliquod corpus ad solem
vel arbor de qua lucanus in secundo Egipto atque umbras nusquam
flectente cyene.

	|104.  non fura.~ quasi dicat non est opus quod loquar
vobis qui in conspectu divino videtis omnia preterita presentia
et futura. sed loquor isti. se auctori.

	|109.  rote magne.~ i. VII. sperarum. vel celorum VII.
planetarum stellarum vagarum que dant nobis nostra ascendentia
cum 12. signis stellarum fixarum octave spere. secundum. quod
assotiatur ut dicit hic simul. quod fuit cum planeta mercurii
assotiato cum signo geminorum quarum stellarum ita sotietarum
effectus est reddere hominem ingeniosum et licteratum.

	|112.  gratie divine.~ que plus habent operari circa
hominem et omnem effectum quam coniunctio planetarum.

	|121.  mio volto.~ idest in prima mea parte veteris
testamenti.

	|124.  su la soglia fui.~ idest. in interitu mee secunde
etatis. idest.  secude partis novi testamenti in qua virtuosior
et carior in pulchritudine facta erat -- |126.  e diesse altrui.~
sc. studio poesi et oblectamentis vanis poetarum et aliis
mundanis et infructuosis scientiis fugiendo in aliena castra.

	|127.  Quando di carne aspirito.~ nam testamentum novum
carius et pretiosius est veteri nam vetus promictit suis
observatoribus benedictionem temporalis prosperitatis et
transgressoribus temporalis aversitatis maledictionem. ut
deutronomii capitulo 28 lex novi testamenti promictit suis
observatoribus vitam eternam et transgressoribus gehennam et hoc
vult auctor dicere. ibi. ^quondo di carne.~

	|130.  via non vera.~ idest. ad petitas mundanas
scientias -- |131. ymagine...false.~ quam ostendunt iste
poetice scientie.

	|141.  Li prieghi miei.~  Et est sciendum quod 4. sunt
genera virtutum secundum Macrobium. scilicet pollitice et
purgatorie de quibus potest dici sermo fuisse hucusque in hoc
libro purgatorii. tertie dicuntur animi iam prugati et ab omni
aspergine vitii detrusi quarum est cupiditates non reprimere sed
penitus oblivisci ex quod patet cur auctor hic flumen letheum hic
fingit. sunt quarte exemmplarie de quibus dicam in capitulo
sequenti.  Modo auctor in sua persona. ut in persona cuiuslibet
alterius iam pergati a peccatis in hoc mundo mediantibus dictis
virtutibus purgatoriis volens ad dictas tres virtutes ulterius
modo venire non sufficiendo sibi hucusque plorasse eius male acta
commissa per euin in faciendo sed etiam ea que commisit in
omictendo fingit se hic supra proximo ita reprendi de omissione
studii dicte theologie.

