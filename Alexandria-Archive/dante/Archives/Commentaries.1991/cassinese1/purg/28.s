	Auctor fingit se ingredi locum terrestris paradisi. qui
ebraice ortus dicitur et Eden quod delitie interpretatur secundum
ysiderum.  Et primo describendo eum dicit ipsum esse supra locum
purgatorii in summitate dicti ejus montis in medio alterius
partis mundi et emisperii ut superius dictum est licet magister
in secundo sententiarum ponet ipsum esse in oriente nostro et
idem videtur sentire ysiderus.  Augustinus super genesim dicit
eredendum esse locum hujus paradisi a cognitione hominum forte
remotum fore et seclusum a nostra habitaione aliquibus
impedimentis montium vel marium ideoque scriptores locorum de eo
minime scripserunt que opynio non modicum consonat opynioni
auctoris premisse.

