<Purgatorio> CANTO 13
Chiose Marginali
1.
Chiose Sincrone

	Auctor in principio hujus capituli prohemialiter tangit
sub allegoria quadam in sua persona quomodo homo ingnarus nature
et qualitatis alicujus vitii eo quod illud actualiter numquam
sive vix commiserat ut de se auctor nunc hic dicit circa hoc
vitium in fine presentis capituli non potest solum per virgilium
idest per rationem hic intimare sed expedit quod volvat sinistram
partem sui. idest. cor suum ad solem. idest. ad scientiam
speculativam que talia naturaliter nobis habet judicare quod
totum auctor tangit hic in eo quod fingit ita virgilium solem
respesisse et eum ita allocutum fuisse. ut dicit textus.  Modo
quia unaqueque res melius apprenhenditur per respectum ejus
oppositi contrarii ut ait phylosophus.  oppositi juxta se posita
etc. fingit etiam hic auctor per talem scientiam speculativam se
primo cognovisse virtutem caritatis et amoris proximi fore
oppositum et contrarium hujus vitii invidie quod tangit hic in eo
quod fingit oviam sibi has tres voces spirituales volitando
occurrisse et protulisse verba ita facientia ad caritatem proximi
primo per exemplum virginis Marie. ut Jo. cap. II. et in nuptiis
vino deficiente non dixit filio non habeo sed dixit vinum non
habent quod ad dilectionem proximi fuit per ipsam prolatum.
secundo per exemplum horestis filii Agamenonis regis et
clitemestre qua ejus matre accisa per eum propter adulterium
commissum per eam cum quodam nomine egisto dictus horestes multum
per mundum vagando erravit numquam tamen derelictus a quodam suo
comite nomine pillade tamen his ambobus applicantibus contrate
cujusdam regis habentis ex presagio quodam se mori debere manibus
dicti horestis et habentis etiam quod unus istorum duorum erat
horestes licet nexiret quis interogantis etiam sepe ipsos duos
quis eorum horestes dictus pillades dicebat ego sum horestes idem
dicebat ne ille moriretur ita se ardenter amabant.  Tertio per
exemplum domini nostri qui ut ait Math. VI.[ -- ?] capitulo dixit
dilligite inimicos vestros et bene facite his qui oderunt vos.
Post hec auctor volens allegorice ostendere quomodo homines in
hoc mundo viventes ac volentes se in statu penitentie ab hoc
vitio invidie purgare et in dicta virtute caritativa persistere
debent plorare in corde suo se jam ita fuisse in tali vitio
propter ejus circumstantias pessimas quas modo cognoscunt subdens
de aglauro invida sic ibi conversa in saxum fingit has animas ita
plorare in loco saxoso livore colorato in quo denotat statum
invidorum in hoc mundo livore plenum in eo quod dicit habere
sutos denotat cecitatem invidorum unde dicit sunt invidi quasi
non videntes nam non sunt et vere dici possunt esse ceci mente
qui dolorem preponunt gaudio eterno.  In eo vero quod fingit
dictas animas coopertas dicto cilicio vult denotare quod sicut
cilicium carnem intus ferentis habet macerare ita invidia afficit
invidum intus et macrefacit.

	|37. sferza.~ augustinas dicit quod invidia est dolor
aliene felicitatis licet alii dicunt quod est exultatio in
adversis proximi et tristitia in proximis ita virtus caritatis e
contrario in nobis operatur.

	|153. chentrovar diana.~ dicunt enim senenses quod sub
civitate senarum latenter labuitur quidam fluvius nomine dyana
valde immensus et cotidie sperant ipsum scaturiri et postea per
dictum flumen navigare et plures fodi fecerunt ut eum reperirent
et nondum reperierunt et ideo dicit auctor quod iste senenses
plus spei perdent in volendo facere quod portus thalamonis sit
portus nominatus nam omni anno mictunt amiraglies qui armate
galearum habent assistere et cum sunt ibi propter malum aerem ut
plurimum moriuntur quam reperire dictum flumen diane set plus
perdent ammiragli eo quia dixi ut plurimum moriuntur.

END OF CANTO: Anne Marie Booye 10/03/86

