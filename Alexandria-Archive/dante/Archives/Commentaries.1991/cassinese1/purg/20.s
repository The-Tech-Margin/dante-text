|PURGATORIO CANTO 20~

CANTO XX.

CHIOSE MARGINALI I.

CHIOSE SINCRONE

	|1.  Contra miglior voler voler mal pugna.~  Aucotr
premissa hac moralitate. scilicet. ut omne velle nostrum
quantumque bonum sit debet cedere meliori. dicit de vitio
avaritie et tacite de lupo et veltro. de quibus supra dixi in
Inferno capitulo primo.

	|19.  dolce Maria.~  Inter istas animas avarorum audivit
quamdam sic clamantem.

	|26.  virtute.~  Est enim virtus frugalitatis. qua homo
vivit secundum naturam spernendo delicata et sordida medium
tenendo in qua virtute fuit etiam ut ait seneca ad lucillum
Seramis civis ille romanus. qui ad agrum colendum rediit functo
offico Senatorie et dictature. de quo Fabritio ait Servius
dicens.  Fabritius paupertate gloriosus consul romanorum existens
respondit legatis sannituum offerentibus ei immensum aurum sic
romani mallunt imperare habentibus aurum quam aurum habere.
Idemque fecit de auro sibi oblato a pirro rege epirrotharum.

	|31.  larghecza.~  Congratulando etiam eidem saucto
Nicolao de largitate quam fecit illis tribus virginibus quibus
largitus est tres massas auri proiciendo eas diversis vicibus de
nocte in domo patris earum qui prius aurum hoc propter
paupertatem intendebat eas questu meretricio et sic omnes tres
nuxerunt ut habetur in ejus legenda.

	|34.  tanto ben favelle.~  Que omnia tria exempla ponit
auctor sic recitari hic ut pateat gloriosus effectus virtutis
predicte paupertatis spiritu quam habuit dicta domina nostra et
virtutis frugalitatis quam habuit dictus Fabritius et virtutis
largitatis quam habuit dictus sanctus Nicolaus contrarius dicto
vito avaritie et oppositus illis in hoc mundo qui a tali vitio
purgari conantur.

	|43.  Io fui radice.~  Et est sciendum quod regnum
Francie primo cepit in persona Faramundi comitis a quo
descenderunt. XXVII.  Reges. et dicta est prima genoalogia
francorum regum predictorum qua defficiente venit secunda que
incipit in persona Pipini patris Caroli Magni in qua fuerunt. XV.
reges quorum ultimus fuit Loduicus filius Loctarii. quo Ludovico
mortuo.  Franchi volentes eligere in regem quemdam fratrem dicti
Lottarii. deditum in vita religiosa et solitaria ut tangit textus
de pannis bisgis quia differebat et distulit astutia et potentia
hujus Ugonis Ciapette existentis Marescalichi parisgienis in anno
domini 990. eligerunt in eorum regem Hobertum filium majorem
dicti Ugonis Zapette et sic in eo orta est tertia genoalogia
dictorum Regum quorum prava opera in processu facta recitando
maxime post magnam dotem provenzalem de qua dicam inferius in
Paradiso Capitulo VI. in fine dicit dicta umbra quomodo dicit
ejus descendentes usurpaverunt territorium ponti Normandie et
Guasconie et alia fecerunt ut patet in textu -- ^mala pianta.~
idest presentis tertie genoalogie regum Francorum.

	|61.  dote provenzale.~ que dedit filia Ramondi
Berlengherii comitis Provincie.

	|67.  Carlo.~ sc. rex Sicilie creatus ab Urbano Papa
IIII. qui venit in Italiam et decapitari fecit Corradinum ut
plene scripsi in XXVIII.  Capitulo Inferni. -- |69.  ripinse
alciel Tomasso.~ item fecit venerari Sanctum Tomasium de Aquino
in Habatia Fosse nove in Campanea ubi hodie ejus corpus jacet et
hoc fecit timendo ne ad Papatum veniret.

	|71.  tragge un altro carlo.~ qui in 1301. venit in
Italiam et de Florentia expulit partem blancorum inductu domini
Musciatti de Frenzesis et domni Cursii de Donatis. de Florentia.

	|79.  laltra.~  Iste fuit Carolus Zottus olim rex Apulie.
qui in maritimo bello victus a domno Rogerio de loria amiraglio
regis Sicilie captus et in carecere positus fuit in civitate
Missine qui tradidit Beatricem filiam suam in uxorem Marchioni
Aczoni de Este pro tringinta millibus florenis quos habuit
paciscendo a dicto Marchione.

	|86.  fior daliso.~  Idest Rex Phylippus dictus Bellus de
dicta domo Francie. qui fecit capi Papam Bonifatium VIII. in
civitate Alanie per domnum Guillelmum de Nugareto et Colonenses
putando dictum Papam favisse Comiti flandrie ad ipsum vincendum
in campo ut vicit ex qua captura mortuus est dictus Papa dolore.
Item dictus rex Felippus dedit operam ut Ordo Templariorum
deponeretur per Clementem Papam V. ut ejus bona occuparet ut
postea occupavit et factum fuit.

	|92.  cio.~ sc. fecisse eum capi et mori -- |93.  cupide
vele.~ idest. contra dictos fratres exercere suam cupidam
avaritiam.

	|100.  nostre prece.~  Quasi diceret non solum ego conto
istas laudes dictorum virginis Marie Fabritii et Sancti Nicolai.
sed omnes qui hic sunt similiter cantant et quia predictorum
talia virtuosa opera vigent in lucida fama et gloria diurna dicit
quomodo in die ibi talia memorant et contraria opera illi facta
circa vitium avaritie in nocte qui ut per infamiam in hoc mundo
sint obscura et denigrata et etiam in frenum nobis et ut cessemus
tali vito inducit hic auctor has animas sundario dicere in
abomnationem dicti vitii avaritie. quomodo primo memorant ibi
avaritiam pigmalionis filii Beli regis syrie qui mortuo dicto
ejus patre. siccheum qui acerbas dicebatur sacerdotem herculis
opibus ditissimum ejus congnatum nam maritus erat didonis sororis
ipsius pigmalionis in actu sacrificium facientem proditorie
occidit et ejus opes rapinavit et ex hoc vocat eum hic in textu
proditor latro et parricida ex eo quod sacerdotem qui pater
omnium potest censeri occidit et etiam sibi affinem.  Secundo
memorant avaritiam Myde olim regis frigie qui ut scribit ovidius
in. XI. resituto per ipsum silleno sacerdote capto per ejus
pastores baco cujus sacerdos erat petiit a dicto baco deo
thebanorum in remunerationem talis servitii ut quicquid tangeret
aurum efficeretur et ita ei concesso dictus Myda fame moriebatur
cum cibaria sua ab ipso tacta aurum statim fiebant nisi
restitutus fuisset a dicto baco in pristionum statum.

	|109.  Del folle Acan.~  Tertio memorat avaritiam Achan
qui ut scribitur Iosue capitulo VLL. furatus fuit capta civitate
Jerico per Iosuem quedam spolia contra precepta Dei.  Unde hoc
scito et reperto sorte facta lapidatus fuit a populo.

	|112.  col marito Saffira.~  Quarto memorat avaritiam
Annanie et Soffire ejus uxoris qui ut legitur Actuum Apostolorum
capitulo V. volentes ostendere se velle sequi Apostolos Chrisi et
eorum mandata et vitam vendito quodam suo agro medietatem pretii
obtulerunt ad pedes Apostolorum et aliam medietatem occultarunt
ex quo Petrus dixit eis cur tentavit Sactanas cor vestrum et
statim ibi ad pedes Petri mortui ceciderunt -- |113.  Eliodoro.~
Quinto memorant quod causaliter contingit avaritia mediante in
templo verusolime ut Machabeorum capitulo III.[?] videlicet. quod
dum inter summum Sacerdotem et simonem quemdam thesaurarium dicti
templi discordia orta esset dictus Simon ivit ad Apolonium ducem
Sirie et Phenitie. pro Seleuco tunc rege Asic et retulit quomodo
in dicto Templo reposita erat infinita pecunia quo audito dictus
Apollonius retulit dicto regi Seleuco hoc qui Rex motus avaritia
statim misit quemdam suum marescalcum nomine Eliodorum
spoliaturum dictum templum de dicta pecunia cum magna comitiva
qui eliodurus miraculose in dicto Templo calcibus cujusdam equi
habentis sessorem quemdam terribilem Iacessitus fuit adeo quod
mortuus fuisset ibi nisi onia sacerdos dicti templi tunc iuvisset
eum suis orationibus.

	|115.  Polinestor.~ 6.[?] avaritiam Polinestoris olim
regis tracie qui dum haberet Polidorum puerum filium Priami in
recomendatione a dicto ejus patre tempore ossesse Troje cum magno
thesauro occidit eum et ejus corpus prohici fecit in mari capta
Troia ut dicto thesauro potiretur -- |116. Crasso~ Septimo
memorant avaritiam Crassi consuli Romani qui existens in
obsidione Carre civitatis Sirie corruptus pecunia confidens de
civibus dicte civitatis ingresus est eam ubi dimisso exercitu
extra fusum est aurum per os ejus bulliens et dictum est ei.
aurum sitisti et aurum bibe.

	|130.  delo.~  Insula posita in mari Egeo in medio
Cicladum sundum Ysiderum que sic tremavit antequam Latona
peperisset Solem et Lunam secundum fictionem ovidii in V.[?] ibi
quoquo modo audetis etc. nam Iuno fecerat ei denegari omnem alium
locum preter hanc insulam.

	|140.  canto.~ ut Luce capitulo II. qui pastores
vigliantes in nocte nativitatis Domini circa Bettelem audiverunt
in aere canere Angelos.  Gloria in excelsis ec.

