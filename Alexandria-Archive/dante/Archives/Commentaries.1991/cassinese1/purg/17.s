|PURGATORIO CANTO 17~

PURGATORIO XVII

CHIOSE MARGINALI I.

CHIOSE SINCRONE

	Auctor duo in hoc capitulo facit primo continuando se
perficit tractatum tertii circuli usque ibi ^noi eravamo ove piu
non saliva.~ ibi incipit tractatum quarti circuli et prosequitur.

	4.^Come.~  Acutor narrat.

	|11.  uscio fuor.~ idest. de contemplatione status et
vite iracundorum in hoc mundo existentium Allegorice subaudi --
|12.  bassi lidi.~ idest. ad immaginandum pravum exitum iracundie
et effectum ut frenum debens nos a tali vitio compescere et
refrenare apostrophando ad virtutem ymaginativam nostram dicendo
quomodo eripit interdum nos subdendo quod si non venit a visu
nostro vel auditu vel ab aliquo alio nostro corporali sensu ut
non venit unde ergo manat et subdit quod a lumine. idest. corpore
aliquo celesti informante seipsum ad hoc per se vel per
ispirationem divinam porrigentem eam ut dicit hic textus.

	|19.  empiecza.~  Et quod abuminabile sit hoc vitium
iracundieob ejus exitum et effectum exemplum dat hic auctor de
progne. de infrascripto crucifixo ut hic dicit auctor -- ^muto
forma.~ que prognes mutata fuit in irrundinem sua iracundia ut
plene dixi supra eodem libro capitulo non.

	25.^Poi piove.~  Scribitur in libro hester. quod de aman
summus mareschalcus Assueri regis syrie iratus quod dictus rex
spreta guaschi regina ejus prima uxore iu uxorem accepisset
hester ebream quamdam mulierem ac neptem mardocei probissimi viri
ebraici fecit capi omnes Judeos existentes in reguo dicti regis
una cum dictor Mardoceo et cum dictum Mardoceum parasset poni in
cruce super quadam jam erecta antenna in eadem antenna crucifigi
fecit eum postea dictus Rex prece dicte hester et dictum
Mardoceum cum aliis Judeis liberavit.

	34.^fanciulla.~ dicitur quod Amata regina uxor regis
latini in tantam iracundiam currit propter laviniam ejus filiam
et dicti regis traditam in uxorem enee et non Turno suo nepoti.
quod se laqueo suspendit. ut ait Virgilius in |XII.~  Et nondum
informis leti etc.

	47.^una voce.~ pro isto angelo Allegorice intelligas quod
scripsi supra in exitu primi circuli de superbia.

	60.^si mette al niego.~ quasi dicat ironice iste angelus
sine rogamine ostendit nobis viam sicut faciunt homines ad
invicem in hoc mundo qui vident aliquem aliquod egere de quod
possunt subvenire et non subveniunt qui quasi negare maligniter
videntur eo ipso quod non subveniunt.

	69.^senzira amara.~ que verba angelica sunt Matheiquinto
et sunt repugnantia huic vitio irancundie.

	76.^Noi.~  Incipit hic auctor tractare de quarto circulo
in quo fingit puniri animas accidiosorum -- ^Noi eravam.~  Est
primo premictendum quod licet vitium accidie ut genus dividatur
in plures species. scilicet. in tepiditatem in mollitiem et
somnolentiam in otiositatem in dillationem in tarditatem in
negligentiam in ignnaviam in tristitam et tedium vite tamen dicta
tepiditas prima species radix dicitur accidie et ex ea nascuntur
omnia premissa vitia et in hoc auctor vocat eam diffiniendo
amorem scemum. idest. diminutum in suo debere amare subanudi deum
ut summum bonum unde diffinitur tepiditas est amor parvus boni
magni.

	88.^intendi.~ hoc dicit auctor ad ostendendum quodmodo a
dictor nostro amore defectivo causatur vitium accidie predictum.

	93.^O natural.~ de naturali amore patet in angelica
creatura que naturali illo amore dilligit deum quomodo
naturaliter quisque dilectus diligit se dilligentem.  Item
creatura etiam humana eodem naturali amore diligit deum eadem
ratione et diligere debet tamquam bonum nostri intellectus et sic
per consequens illum odire non possimus ut hic inferius dicitur
nec etiam homo seipsum ut similiter hic dicitur in textu.  Item
ejus parentes et filios et benefactores ejus et talis amor semper
est sine errore ut dicit textus nom non potest quis errare
sequendo quod naturale est.  Item et cetere alie creature brute
et insensales tali naturali amore dilligunt de brutis patet in
filiis suis et in specie eadem sibi coniunctis de amore animi
patet in nobis dum preter naturam movemur ad aliqua amanda solum
animi motu.

	|97.  ben diretto.~ idest. dam in primo scilicet.
naturali bene dirigimur.

	|105.  merta pene. merita pene.~ cum amamus malum proximi
nostri quod triplici modo fit ut hic dicitur ad quod et ad que
sequuntur hic usque ad finem hujus capituli ita ait Augustinus
quem auctor hic ad literam sequitur dicens sicut virus est amor
ordinatus ita vitium est amor inordinatus.  Amor vero duplex est
inordinatus primo si sit amor mali et licet sit amor boni est
tamen inordinatus si sit minus vel nimis parvus et hoc secundum
duas species bonorum quedam enim bona sunt parva ut temporalia et
corporalia quedam magna ut bona gratie et glorie intelligendo
bona gratie ipsam gratiam et opera meritoria.  Amor ergo boni
magni inordinatus est si sit parvus et sit parvus et talis amor
videtur esse radix in vitio accidie et tepiditaltis Amor vero
parvi boni inordinatus est si sit minus et iste amor videtur esse
radix gule luxurie et avaritie et diversificatur iste amor quia
bona parva amari possunt quoad dominium et portionem et quoad
usum delectabilem primo modo amat ea avarus secundo modo gulosus
et luxuriosus.  Amor mali distinguitur in amorem proprii mali et
alieni sed quia nullus proprium malum amat in quantum tale sed
tantum in eo quod existimat bonum esse eo respectu cum homo
naturaliter habeat apetitum ad bonum et sic velle malum esset
contra voluntatem ipsius ut modo superius dixi nec umquam carnem
suam odio habuit ideo tamen amor alieni mali radix crit superbie
invidie et ire diversimode tamen quia in superbia est amor
proprii boni cum alieno malo.  Amat enim superbus sui
exaltationem et proximi dejectionem.  In invidia et ira est amor
alieni mali proprie differenter tamen nam in invidia amor alicui
mali ortum habet a propria malitia.  Invidus ideo vult malum
alterius. ne ille sibi parificetur.  In ira vero amor alieni mali
ortum habet a malo alterius nam qui irascitur alicui ideo vult ei
malum alterius. ne ille sibi parificetur.  In ira vero amor
alieni mali ortum habet a malo alterius nam qui rascitur alicui
ideo vult ei malum quia malum ab eo recepit cum ira dicatur
appetitus vindicte et sic hec tria vitia amorem nostrum
inordinatun reddunt ad proximum alia quatuor supradicta ad nos
ipsos et sic concludendo vitium accidie nos facit peccare ut
lentos tepidos ad sequendum et ad acquirendum verum bonum. idest.
deum quod bonum omnes appetunt sed confuse ut dicit hic textus et
Boetius in tertio ibi omnia mortalium cura etc.

	|115.  E chi.~ iste enim est superbus qui sui
exaltationem et proximi dejectionem amat.

	|118.  E chi.~ iste est invidus qui vult malum alterius
ne ille sibi parificetur.

	|122.  si fa delavenetta ghiocto.~ iste est iracundus qui
ideo malum aterius amat quia malum ab eo recepit cum ira dicatur
appetitus vindicte.

	|124.  triforme.~ scilicet. superbie Invidie et Ire.

	|126.  ordine corrotto.~ quia non amant homines ilud ita
ardenter sicut deberent sed tepide et tarde et ideo incurrunt in
vitium accidie.

	|137.  tre cierchi.~ in quibus puniuntur gulosi luxuriosi
et avari.

