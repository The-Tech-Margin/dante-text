PURGATORIO VIII

CHIOSE MARGINALI I.

CHIOSE SINCRONE

	|1.  volge il disio.~ quia amore caritatis et tenero motu
amoris amicorum de presenti relictorum vellent potius retrocedere
quam ultra ire et ideo desiderium quod habehant volvunt in non
eundo.

	|6.  ludire.~ eo quia non cantabant amplius salve reginam
-- |9.  com mano.~ ut dixit lucanus in primo. visoque manu
silentia iuxta.

	|18.  Te lucis.~ que salve regina. et ymnus iste cantatur
in completorio quem ymnum fecit beatus ambroxius.

	|17.  lynno intero.~ i. per totum residuum ymnus non
prima dixit.  Te lucia. ante terminum et alie secute eam fuerunt
dicentes rerum creator poscimus ut solita clementia sis presul ad
custodiam procul recedant somnia et noctium phantasmata hostem
nostrumque comprime ne polluantur corpora.

	|22.  exercito gentile.~ si diceremus quod anime
existentes in purgatorio temptarentur a demone diceremus contra
tomaxium in suo libro contra geniles et contra auctorem infra in
|XI.~ et |XXVI.~ capitulo et ideo auctor dicit quod acuamus bene
oculos nostri intellectus. ad discernendum verum modo credo quod
quicquid ipse auctor dicit de his animabus in tali ora ita
orantibus et canentibus reducatur ad homines in hoc mundo
viventes spiritualiter deo servientes et quodammodo se purgando a
malis commissis quorum moris est in tali ora orando canere et
jubilare deo in deprecationem et laudem dei et ejus matris in
consortio plurium hominum ut faciunt in sero canentes laudes ut
in multis locis fieri vidi et in hoc venit auctor ad suam
materiam ampliandam ut tangit et tractet de effectu orantionis
que interpretatur oris ratio.  Item interpretatur adscensus
intellectus in deum et ex hoc auctor fingit has animas ita manus
et occulos ad celos. idest. ad deum elevare.  Inde auctor volens
tangere de effectu talis orationis que est ut mediantibus angelis
tales prefati viri spirituales defendant se contra insultum et
tentationes demonicas maxime nocturno tempore nos agredientes ex
qluo disuadebat discipulis quod orarent ne intratrent in
temptationem subsequenter tangit hic auctor de istis doubus
angelis. cum duabus spatis igneis fugasse ita hunc serpentem.
idest. diabolum tentaturm truncis cuspidibus. ut denotet in hoc.
quod non sunt angelica subsidia nobis ad occisionem demonum sed
ad tuitiionem pro alis quas habent potest accipi celeris
orationis de eo quod dicit quomodo dictus serpens ita vovebat ad
dorsum lingendo capud et faciem humanam debemus intelligere de
sugestione diaboli nitente se reddere et facere placibilem ubi
est diformis.

	|53.  Nin gentile.~ iste fuit Ninus de Vicecomitibus de
pisis olim Judex Judicatus gallure insule sardinee et maritus
domne beatricis de Este ex qua genuit filiam nomine Johannam quo
nino mortuo dicta ejus uxor nupsit domno galeassio de
vicecomitibus de mediolano.

	|69.  li e guado~ ad intimandum cur hic hoc fecit primo
et hic secundo.

	|80.  Menalesi acampa.~ nam vicecomites de mediolano quod
quotiescumque populus mediolani exit ad campum in exercitum.
vexillum quod firebatur debet esse illum vicecomitum predictorum
cujus vexillum est serpens aczurrus in campo albo cum quodam
puero rubeo in ore dicti serpentis. modo dicit iste ninus
honorabilius fuisset domne beatrici habuisse in sepulchro suo
scultum gallum qui est signum armature dicti Nini quam vipera que
est armatura dictorum vicecomitum qui accampat.  idest. qui
campum et exercitum fingit mediolani.

	87.^stelo.~ quasi diceret quod respicit illuc ubi stelle
sunt tardiores aliis stellis sicut est illa pars rote in qua sunt
afficti radii rote que pars semper tangit axem currus et volvitur
minus velociter illa pars quam extrema pars rote respectu
circuli.

	89.^fucelle.~  Vult allegorice ostendere quod licet in
principio ascensionis hujus montis qui pro felicitate humana
figuratur. quatuor virtutes cardinales hominibus ad talem
directionem in principio expediant tamen in processu necessarium
est ut superveniant tres ille theologige virtutes pro quibus hec
tres stelle ponuntur hic.

	|120.  qui rafina.~ nam ipse communicavit cum omnibus de
domo sua civitatem buose. et castrum duosoli et cetera alia bona
que acquisivit in ysula sadenie ex dote cujusdam sue uxoris.

	|138.  sermone.~ eo quia eris explsus de florentia et
benigne ab illis de domo mea receptatus et sic quod audivistis
videbis.

