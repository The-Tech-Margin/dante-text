	|43.  Poco piu oltre.~  Et notandum est quod ecclesia
militans Christi quo collectio fidelium et catholicorum dicitur
tam olim credentium in Christum venturum quam hodie jam ventum
tipum et figuram spiritualiter tamen hujus paradisi ut supra in
precedenti capitulo dixi que ecclesia dicitur hedificari supra
petram ut Matthey capitulo 18. cujus ecclesie talem misticam
intelligentiam et cognitionem. per vitam activam purgatus homo in
hoc mundo capere potest. et debet ad sui perfectionem et
felicitatem humanam exercendo se circa studium sacre pagine
veteris et novi testamenti in qua fundata est dicta ecclesia
militans nostra.  Volens igitur nunc auctor in persona sui ut in
persona cujuslibet alterius purgati poetice scribere hic et
ostendere premissa invocatione quam hic facit.  Fingit modo hic
per hanc Mateldam figurantem dictam nostram vitam activam sibi
ostendi que sequuntur hic est quantum. scilicet. ad integumentum
quod activo suo studio circa talia operante concepit comprendere
prius de origine dicte ecclesie et secundario de ejus processu
que statim inferius gradatim ostendit auctor legendo prophetas et
alios sanctos scribentes deorigine et processu primitive veteris
et secunde nove ecclesie et maxime Iohannem in sua apocalipsi
capituto XXI.  Modo quia prius deus per spiritum sanctum eujus
dona sunt VII. scilicet. donum timoris amoris scientie
fortitudinis consilii sapientie et intellectus spiraculum dedit
prophetis et aliis sanctis in scribendo de incarnatione filii sui
et de institutione dicte ejus eccclesie primitive et secundarie
auctor primo fingit. se vidisse. hic ante omnia alia que hic
vidit preire hec

VII candelabra figurantia hec VII dona predicta.

