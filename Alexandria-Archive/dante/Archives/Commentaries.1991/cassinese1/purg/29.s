	|3.  Beati quorum tecta sunt peccata.~  Quamvis homo
peccator in hoc mundo penitens efficatur et contritus gratia
mediante ante tamen perfectam satisfationem et expiationem
commisorum peccatorum et habitationem virtutum consecutam semper
sibi detecta sunt ejus commissa peccata et in ejus presentia
conscientialiter unde potest dicere quod dicit Salmista quoniam
iniquitatem meam ego cognosco et et peccatum meam contra me est
semper at cum perfecte satisfecit et purgatus in hac vita est ab
omni fecte macula peccatorum et in totum habituatus ad virtuosa
ut est homo in totum ad vitam activam deventus vere potest dicere
verba que dicit auctor hic. videlicet. beati quorum remisse sunt
iniquitates et quorum tecta sunt peccata quasi dicat quod sua
peccata extineta sunt in conscientia sua. scilicet. activa vita
mediante que in ista Matelda figuratur.

